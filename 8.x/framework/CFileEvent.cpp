void
CFileEvent::Exit(int status)
{
  CApplicationSerializer::getInstance()->UnLockCompletely();
  DAQThread::Exit(status);
}

/*!
  Creates a new monitor given a filename.  The file is created/opened for 
  read/write and the fd resulting is used to create a new CFdMonitor (via new).

  \param pFilename   - Name of the file to create/open.
  \param pMonitor    - Name to give to the monitor.
  \param flags       - open(2) flags.

  Exceptions:
  - CErrnoException - If the file cannot be created/opened.
  - CDuplicateNameException - If the a monitor with that name  already exists.

  */
CFdMonitor*
CFileEvent::CreateMonitor(const char* pFilename, const char* pMonitor,
			  int flags)
{
  // Action is as follows:
  // 1. Attempt to open the file
  // 2. Create the monitor with the given name and fd.
  // 3. Save the fd in m_nFd.

  int fd = open(pFilename, flags);
  if(fd < 0) {
    throw CErrnoException("Opening file in CFileEvent::CreateMonitor-open(2)");
  }

  m_nFd = fd;

  return new CFdMonitor(pMonitor, fd);



}
/*!
    Set up the acessibility of the monitor.  This means that
    the access mask is translated into an initial set of calls to 
    MonitorReadable, MonitorWritable.

    \param AccessMask - Bitwise or of the access requested.

    Legitimate bits in AccessMask are:
    - CFileEvent::readable   - Monitor readability.
    - CFileEvent::writable   - Monitor writability.
    - CFileEvent::exceptions - Monitor for exceptions.

    */
void 
CFileEvent::SetupMonitor(int AccessMask)
{
  // Since this is a protected member we'll assume that it is only called
  // in a synchronized context.

  // Initialize everything to unmonitored:

  MonitorReadable(false);
  MonitorWritable(false);
  MonitorExceptions(false);

  // Now correct this based on AccessMask:

  if(AccessMask & readable) MonitorReadable(true);
  if(AccessMask & writeable)MonitorWritable(true);
  if(AccessMask & exceptions) MonitorExceptions(true);
} 
/*!
  Called to get a description of the object.
  Tells the world we're a file handling event, dumps the CEvent Description, 
  and gives the values of m_nFd, m_fCloseOnDestroy:
  */
string
CFileEvent::DescribeSelf()
{
  string Result(" File Handling event: ");
  char value[10];
  Result += CEvent::DescribeSelf();
  Result += string("File id is: ");
  sprintf(value, "%d", m_nFd);
  Result += value;
  Result += (m_fCloseOnDestroy ? string("Will close file on destruct") :
                                string("File remains open on destruct"));
  Result += '\n';
  return Result;
}
