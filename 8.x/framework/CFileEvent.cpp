/*!
  Construct a named file event from a char* object name, and a file
  descriptor.  The CFdMonitor is directly new'd into existence in the
  operation.
  \param pObjName  - Pointer to object name.
  \param fd        - File descriptor on already open file to monitor.
  \param access    - Bitwise or of the desired access to the file.

  Throws:
  - CDuplicateNameException - if this object or any subobject already have
                              this name.


  */
