/*! 

   This is an internal class which is automatically used as the 
   Reactor for File events.  About the only thing it does is call
   back to the file event.
   */

/*!
   Constructor for CFileEventReactor: Constructs the reactor by 
   - Calling the base class constructor (CReactor()).
   - Initializing m_rOwner to the event which is instantiating us.
   - Calling AppendClassInfo() so that our place in the class hierarchy is
     well defined.

   \param rName - Reference to the name given to this reactor.
   \param rOwner - Reference to the owning event.

 */
CFileEvent::CFileEventReactor::
CFileEventReactor(const string& rName, CFileEvent& rOwner) :
  CFdReactor(rName),
  m_rOwner(rOwner)
{
  AppendClassInfo();
}

/*!
      Called whenever the file becomes readable.  We create an
      istream for the file, and call back to m_rOwner's OnReadable member.
      This all makes the Event seem like a monolithic unit to the user....
      They just subclass it, open a file in an appropriate way and poof.
      
      \param rMonitor - Refers to the monitor on the file (unused).
      \param  fd       - File descriptor open on the file.

 */
void
CFileEvent::CFileEventReactor::OnReadable(CFdMonitor& rMonitor, int fd)
{
  fdistream infile(fd);

  m_rOwner.OnReadable(infile);
}

/*! 
    Called when a file descriptor becomes writable.  We just create a
    stream and bounce the call back to the CFileEvent's OnWritable
    member.  This presents a monolithic look and feel to the end user
    of this library.

    \param rMonitor - The file monitor which declared the event.(unused)
    \param fd       - The file id which is now writable 
                      (constructs the ostream)
    */
void
CFileEvent::CFileEventReactor::OnWritable(CFdMonitor& rMonitor, int fd)
{
  fdostream ofile(fd);
  m_rOwner.OnWritable(ofile);
}
/*!
    Called when an exceptional condition is encountered on a file.  We create
    an iostream and bounce the call back to m_rOwner's OnException.  This 
    creates the look and feel of a monolithic Event class.
 */
void
CFileEvent::CFileEventReactor::OnException(CFdMonitor& rMonitor, int fd)
{

  fdiostream io(fd);
  m_rOwner.OnException(io);
}

/*!
  Called when a wait on the file event times out.
  \param rMonitor  -referens to the event monitor.
  */
void
CFileEvent::CFileEventReactor::OnTimeout(CEventMonitor& rMonitor)
{
  int fd =m_rOwner.getFd();
  fdiostream io(fd);
  m_rOwner.OnTimeout(io);
}


