/*!
   Destructor:  In all of the construction methods, the monitor and reactor are
   dynamically instantiated via new (that's what CreateMonitor will do).
   We need to get the Monitor and the Reactor and delete them. 
   */
CFileEvent::~CFileEvent()
{
  delete &(getMonitor());	// Note these return references
  delete &(getReactor());	//  <--------------------+
  if(m_fCloseOnDestroy) close(m_nFd);
}

