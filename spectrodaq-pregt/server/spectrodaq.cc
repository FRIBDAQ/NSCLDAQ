class PageMgr : public DAQServNode {
  int operator()(int argc,char **argv) {
    DAQStatus status;          // Status work var
    DAQThreadId aId;           // Work Id
    PageMgrWatch1    aPageMgrWatcher;
    StatusWatcher    aStatusWatcher;
    DAQThreadId      aPageMgrWatcher_id;
    DAQThreadId      aLinkThreadMgr_id;
    DAQThreadId      aStatusWatcher_id;
    DAQThreadId      aLinkRQ_id,aSinkRQ_id;

    MARK(1);

    sync_begin(GlobalSync);
      SetProcessTitle("spectrodaq: running");
      if (!foregrd) {  // Become a daemon
        MARK(2003);
        Detach(true,true,true);         
      } else {   // Debug mode
        MARK(3003);
        cerr << "Attach a debugger to the main spectrodaq process pid=" << getpid() << " (eg, gdb spectrodaq " << getpid() << ") and/or press a key" << endl;
        getc(stdin); 
      }         
  
      MARK(4003);
      setsid();  // Start a new session so we can terminate our children
      signal(SIGTERM,sigfunc_Terminate);
      signal(SIGSEGV,sigfunc_Segv);
  
      MARK(5003);
      LOG((DAQCSTR("Starting the Page Manager Watcher Thread...")));
      aPageMgrWatcher_id = daq_dispatcher.Dispatch(aPageMgrWatcher);
      LOG(("Page Manager Watcher Thread Id = \"%s\"",aPageMgrWatcher_id.EncodeAsString().Get()));
  
      MARK(6003);
      LOG((DAQCSTR("Starting the Link RQ Thread...")));
      aLinkRQ_id = daq_dispatcher.Dispatch(*pLinkRQThread);
      LOG(("Link RQ Thread Id = \"%s\"",aLinkRQ_id.EncodeAsString().Get()));
  
      MARK(7);
      LOG((DAQCSTR("Starting the Sink RQ Thread...")));
      aSinkRQ_id = daq_dispatcher.Dispatch(*pSinkRQThread);
      LOG(("Sink RQ Thread Id = \"%s\"",aSinkRQ_id.EncodeAsString().Get()));
  
      MARK(8);
      LOG((DAQCSTR("Starting the Link Manger Watcher Thread...")));
      aLinkThreadMgr_id = daq_dispatcher.Dispatch(*pLinkThreadMgr);
      LOG(("Link Thread Manager Thread Id = \"%s\"",aLinkThreadMgr_id.EncodeAsString().Get()));
  
      MARK(9);
      LOG((DAQCSTR("Starting the Status Watcher Thread...")));
      aStatusWatcher_id = daq_dispatcher.Dispatch(aStatusWatcher);
      LOG(("Status Watcher Thread Id = \"%s\"",aStatusWatcher_id.EncodeAsString().Get()));
  
      MARK(10);
    sync_end; // GlobalSync

    try {
      // These joins are somewhat superflous since if the the thread
      // has a problem it kills the process, but we'll wait on it
      // here to avoid exiting and causing other problems. 
      MARK(11);
      aId = Join(aPageMgrWatcher_id,&status);
      LOG(("ThreadId = \"%s\" Exited",aId.EncodeAsString().Get()));
      LOG(("     Status: CTXT=\"%s\" MSG=\"%s\" CODE=%d",status.GetContextString(),status.GetStatusString(),status.GetStatusCode()));

      MARK(12);
      if (aId == aPageMgrWatcher_id) {
        LOG((DAQCSTR("The Page Manager Watcher Thread died.")));
      }

      MARK(13);
      aId = Join(aStatusWatcher_id,&status);
      LOG(("ThreadId = \"%s\" Exited",aId.EncodeAsString().Get()));
      LOG(("     Status: CTXT=\"%s\" MSG=\"%s\" CODE=%d",status.GetContextString(),status.GetStatusString(),status.GetStatusCode()));

      MARK(14);
      if (aId == aStatusWatcher_id) {
        LOG((DAQCSTR("The Status Watcher Thread died.")));
      }

      MARK(15);
      return(1);
    } catch (NSCLException& e) { 
      MARK(16);
      return(2);
    } catch(...) {
      MARK(17);
      LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQPageMgr::operator() Caught an unknown exception")));
      return(2);
    } 

    sync_begin(GlobalSync);
      SetProcessTitle("spectrodaq: done");

      MARK(18);
      if (pLinkThreadMgr != NULL) pLinkThreadMgr->Cancel();
      if (pLinkRQThread != NULL) pLinkRQThread->Cancel();
      if (pSinkRQThread != NULL) pSinkRQThread->Cancel();

      MARK(19);
      if (pBufferPager != NULL) delete pBufferPager;
      pBufferPager = NULL;
    sync_end;

    MARK(20);
    return(0);
  } 
};

