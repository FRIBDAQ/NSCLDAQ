// log_usage
//                  
// Log process usage
//
static void log_usage(const char *tag)
{
  int cnt;
  FILE *fp = NULL; 
  char cmd[80];
  char wrk[513];

  sprintf(cmd,"ps avmwx %ld",getpid());
  
  if ((fp = popen(cmd,"r")) != NULL) {
    daq_logger.printf("USAGE(%d): ",tag);
    while ((cnt = fread(wrk,1,512,fp)) > 0) {
      wrk[cnt] = '\0';
      daq_logger.print(wrk);
    }
    daq_logger.println();
    pclose(fp);
  }
}

