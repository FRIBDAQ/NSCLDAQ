class PageMgrThread : public DAQThread {
  int operator()(int argc,char **argv) {
    short hand;
    int rc;
    DAQString namestr;
    DAQThreadId aId;
    DAQBufferPage *pPage = NULL;
    DAQBufferGetPage_H *rqhand = new DAQBufferGetPage_H(RequestBuffer_h,pBufferPager);
    DAQBufferAccept_H *achand = new DAQBufferAccept_H(AcceptBuffer_h,pBufferPager);
    achand->SetClose(false);  // Persistent handler

    MARK(1);
    sync_begin(GlobalSync);
      namestr = namestr +  "PageMgrThread: on " + daq_server_config.GetBufferURL();
      SetThreadTitle(namestr);
  
      MARK(2002);
      sync_begin(rpc_server);
        rpc_server.Register(rqhand);
        rpc_server.Register(achand);
        rpc_server.Listen(daq_server_config.GetBufferURL());
      sync_end; // rpc_server
      MARK(3002);
    sync_end; // GlobalSync

    while(!killme) {
      MARK(4002);
      sync_begin(rpc_server);
        try {
          MARK(5002);
          hand = rpc_server.Accept(0x0fffffff);  // Wait for an RPC call..for essentially as long as needed.
          MARK(6002);
          hand = rpc_server.GetNextHandle(); // What handle should be process
          MARK(7);
        } catch(...) {
          LOGX(os_exception_factory.CreateBaseSystemException(DAQCSTR("PageMgrThread::operator() Caught an unknown exception")));
        } 
       
        switch(hand) {
          case RequestBuffer_h:
            MARK(8);
            try {
              rc = rpc_server(hand);   // Process an RPC call
            } catch(...) {
              // Ignore for now
            }  
            MARK(9);
            break;
          case AcceptBuffer_h:
            MARK(10);
            try {
              rc = rpc_server(hand);   // Process an RPC call
            } catch(...) {
              // Ignore for now
            }  
            MARK(11);
            break;
          case PersistentCall_h:
            MARK(12);
            try {
              rc = rpc_server(hand);   // Process an RPC call
            } catch(...) {
              // Ignore for now
            }  
            MARK(13);
            break;
          default:
            LOG(("Invalid RPC Call handle=%d %s",hand,DAQCSTR("")));
            break;
        }
  
        MARK(14);
      sync_end; // rpc_server
    }
    
    sync_begin(GlobalSync);
      delete rqhand;
    sync_end;

    MARK(15);
    return(0);
  } 
};

