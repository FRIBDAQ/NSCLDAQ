class DAQKickOff : public DAQServNode {
  // Node main routine
  int operator()(int argc,char **argv) {
    char copt;
    bool cfileok = false;
    DAQNodeId aId;           // Work Id
    DAQNodeId aPageMgr_id;   // Page manager Id
    DAQStatus status;        // Status work var
    PageMgr aPageMgr;        // The buffer manager process
    int pagesize_kb = BUFFER_PAGESIZE_KB;
    int pagespertick = 4;
    int shmsize_mb = BUFFER_SHMSIZE_MB;
    char *wp = NULL;

    // Add the globals to the memory tracker
    aBufferPagerCond.SetType();
    rpc_server.SetType();
    rpc_status.SetType();

    // Set the default URLs
    daq_server_config.SetStatusURL(SRV_STATUS_REQUEST_URL);
    daq_server_config.SetBufferURL(SRV_BUFFER_REQUEST_URL);
    daq_server_config.SetLinkURL(SRV_LINK_REQUEST_URL);
    daq_server_config.SetSinkURL(SRV_SINK_REQUEST_URL);

    // We want to do exec tracking
    DAQInitializeExecTracker();

    // Get the options
    copt = (char)getopt(argc,argv,OPTS);
    while (copt != -1) {
      switch(copt) {
         case 'v': 
                   cout << "Version: " << SPECTRODAQ_VERSION << endl;
                   exit(0);
                   break;
         case 'c': 
                   cfileok = ReadConfig(optarg);
                   break;
         case 'f': 
                   foregrd = true;
                   break;
         case 'h': 
         default:
                   usage(argv[0]);
                   exit(1);
                   break;
      }
      copt = (char)getopt(argc,argv,OPTS);
    }

    if (!cfileok) {
      cfileok = ReadConfig();
      if (!cfileok) {
        LOG((DAQCSTR("*** Error: Failed to sucessfully read/parse configuration file"))); 
        cerr << "*** Error: Failed to sucessfully read/parse configuration file" << endl;

        return(-1);      
      }
    }

    pagesize_kb = daq_server_config.GetPageDataSize();
    pagespertick = daq_server_config.GetPagesPerCycle();
    shmsize_mb = daq_server_config.GetShmSize();

    set_daqcommunicator_maxmsg(BUFFER_FULLPAGESIZE(pagesize_kb)*2);
    daq_logger.printf("Communicator maximum message size=%d %s\n",get_daqcommunicator_maxmsg(),DAQCSTR(""));

    LOG((DAQCSTR("Server Configuration..."))); 
    unsigned int addr = aId.GetIP();
    DAQString ipstr = aId.GetIPStr();

    daq_logger.printf("     Running on IP address: %s(%ul)\n",ipstr.str().c_str(),addr);
    daq_logger.printf("     Debug Level: %d\n",daq_server_config.GetDebug());
    daq_logger.printf("     sdaq-buff: ");
    daq_server_config.GetBufferURL().toStream(daq_logger);
    daq_logger.flush();
    daq_logger.printf("     sdaq-stat: ");
    daq_server_config.GetStatusURL().toStream(daq_logger);
    daq_logger.flush();
    daq_logger.printf("     sdaq-link: ");
    daq_server_config.GetLinkURL().toStream(daq_logger);
    daq_logger.flush();
    daq_logger.printf("     sdaq-sink: ");
    daq_server_config.GetSinkURL().toStream(daq_logger);
    daq_logger.flush();
    daq_logger.printf("     Page Data Size: %d KB\n",pagesize_kb);
    daq_logger.printf("     Pages Per Cycle: %d\n",pagespertick);
    daq_logger.printf("     Shm Segment Size: %dMB\n",shmsize_mb);

    LOG((DAQCSTR("Acquiring Shared Memory..."))); 

    try {
      if (daq_server_config.GetWorldAccess()) {
        pBufferPager = new DAQBufferPager(BUFFER_PAGESIZE(pagesize_kb),true);
      } else {
        pBufferPager = new DAQBufferPager(BUFFER_PAGESIZE(pagesize_kb),false);
      }
      pBufferPager->AddSeg(BUFFER_SEGSIZE(shmsize_mb,pagesize_kb));
    } catch (...) {
      LOG((DAQCSTR("DAQKickOff Failed to acquire shared memory.  Exiting.")));
      return(-1);
    } 

    daq_track_on;

    try {
      pLinkThreadMgr = new DAQLinkThreadMgr(pBufferPager);
      pLinkThreadMgr->SetPagesPerTick(pagespertick);
      pLinkRQThread = new DAQLinkRQThread(daq_server_config.GetLinkURL(),pLinkThreadMgr);
      pSinkRQThread = new DAQSinkRQThread(daq_server_config.GetSinkURL(),pLinkThreadMgr);
    } catch (...) {
      LOG((DAQCSTR("DAQKickOff Failed to create a link thread manager.  Exiting.")));
      return(-1);
    } 

    LOG((DAQCSTR("Spectrodaq Server Starting..."))); 

    try {
      LOG((DAQCSTR("Starting the Page Manager...")));
      SetProcessTitle("spectrodaq: starting...");
      aPageMgr_id = daq_dispatcher.Dispatch(aPageMgr);
      LOG(("Page Manager Id = \"%s\"",aPageMgr_id.EncodeAsString().Get()));

//      aId = Join(&status);
//      LOG(("NodeId = \"%s\" Exited",aId.EncodeAsString().Get()));
//     LOG(("     Status: CTXT=\"%s\" MSG=\"%s\" CODE=%d",status.GetContextString(),status.GetStatusString(),status.GetStatusCode()));

//      if (aId == aPageMgr_id) {
//        LOG((DAQCSTR("Page Manager died...")));
//        SetProcessTitle("spectrodaq: died...");
//      }
    } catch (NSCLException& e) { 
      // Ignore these -- probably already logged.
    } catch (...) {
      LOG((DAQCSTR("DAQKickOff Exiting.")));
      LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQKickOff::operator() Caught an unknown exception")));
      return(-1);
    }

    daq_track_off;
    return(0); 
  } 
};

