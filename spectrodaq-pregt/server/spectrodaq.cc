class StatusThread : public DAQThread {
  int operator()(int argc,char **argv) {
    short hand;
    DAQString namestr;
    int rc;
    DAQObjStat_H *objhand = new DAQObjStat_H(ObjStat_h);
    DAQThreadStat_H *tsshand = new DAQThreadStat_H(ThreadStat_h);
    DAQLinkStat_H *lsshand = new DAQLinkStat_H(LinkStat_h,pLinkThreadMgr);
    DAQServStat_H *sshand = new DAQServStat_H(ServStat_h,daq_server_config);
    DAQUsedPagesStat_H *upshand = new DAQUsedPagesStat_H(UsedPagesStat_h,pBufferPager);
    DAQFreePagesStat_H *fpshand = new DAQFreePagesStat_H(FreePagesStat_h,pBufferPager);
    DAQLogState_H *leshand = new DAQLogState_H(LogState_h);
    DAQDebugLevel_H *debhand = new DAQDebugLevel_H(DebugLevel_h);
    DAQPRQStat_H *prqhand = new DAQPRQStat_H(PRQStat_h);

    MARK(1);
    sync_begin(GlobalSync);
      namestr = namestr + "StatusThread: on " + daq_server_config.GetStatusURL();
      SetThreadTitle(namestr);

      MARK(2001);
      sync_begin(rpc_status);
        rpc_status.Register(objhand);
        rpc_status.Register(tsshand);
        rpc_status.Register(lsshand);
        rpc_status.Register(upshand);
        rpc_status.Register(fpshand);
        rpc_status.Register(debhand);
        rpc_status.Register(prqhand);
        rpc_status.Register(sshand);
        rpc_status.Register(leshand);
        rpc_status.Listen(daq_server_config.GetStatusURL());
      sync_end;
      MARK(3001);
    sync_end; // GlobalSync

    for(;;) {
      MARK(4001);
      sync_begin(rpc_status);
        try {
          MARK(5001);
          hand = rpc_status.Accept(0x0fffffff);  // Wait for an RPC call
          MARK(6001);
       
          switch(hand) {
            case ObjStat_h:
            case ThreadStat_h:
            case LinkStat_h:
            case UsedPagesStat_h:
            case FreePagesStat_h:
            case DebugLevel_h:
            case PRQStat_h:
            case LogState_h:
              MARK(7);
              rc = rpc_status(hand);   // Process an RPC call
              MARK(8);
              break;
            case ServStat_h:
              MARK(9);
              rc = rpc_status(hand);   // Process an RPC call
              MARK(10);
              break;
            case PersistentCall_h:
              MARK(11);
              rc = rpc_status(hand);   // Process an RPC call
              MARK(12);
              break;
            default:
              LOG(("Invalid RPC Status Call handle=%d %s",hand,DAQCSTR("")));
              break;
          }
  
          MARK(13);
        } catch (std::exception &se) {
          THROW(se);
        } catch (DAQException &e) {
          THROW(e);
        } catch (OSException &e) {
          THROW(e);
        } catch (NSCLException &e) {
          THROW(e);
        } catch(...) {
          OSException oe;
          oe = os_exception_factory.CreateBaseSystemException(DAQCSTR("StatusThread() Caught an exception"));
          LOG_AND_THROW(oe);
        } 
  
      sync_end; // rpc_status
    }
    
    sync_begin(GlobalSync);
      MARK(14);
      delete objhand;
      delete tsshand;
      delete lsshand;
      delete sshand;
      delete upshand;
      delete fpshand;
      delete debhand;
      delete prqhand;
      delete leshand;
      MARK(15);
    sync_end; // GlobalSync
    return(0);
  }
};

