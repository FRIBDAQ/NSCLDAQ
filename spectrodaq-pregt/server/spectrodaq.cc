// sigfunc_Terminate
//                  
// Termination function for the immortal
//                                      
static void sigfunc_Terminate(int sig)
{
  signal(SIGCHLD,SIG_IGN); // Ignore since we're exiting anyway

  LOG(("sigfunc_Terminate() process %d terminating on signal %d %s\n",getpid(),sig,DAQCSTR("")));

  DAQDestroyExecTracker();

//  if (pLinkThreadMgr != NULL) pLinkThreadMgr->Cancel();

  if (pBufferPager != NULL) delete pBufferPager;
  pBufferPager = NULL;

  kill(0,SIGTERM);  // Terminate everyone
  sleep(1);
  kill(0,SIGKILL);  // Kill everyone

  exit(0);
}

/*===================================================================*/
// sigfunc_Terminate
//                  
// Termination function for the immortal
//                                      
static void sigfunc_Segv(int sig)
{
  static bool dumped = false;

  LOG(("sigfunc_Segv() process %d caught a SEGV (signal %d) %s\n",getpid(),sig,DAQCSTR("")));

  if (!dumped) { 
    dumped = true;
    if (DAQGetExecTracker() != NULL) {
      daq_logger.println("*** Current execution tracking data ***");
      daq_logger.println(*DAQGetExecTracker());
//      DAQGetExecTracker()->Log(); 
      daq_logger.println();
    } 
  }

  if (pBufferPager != NULL) delete pBufferPager;
  pBufferPager = NULL;

  kill(0,SIGSEGV);  // Terminate everyone
  sleep(1);
  kill(0,SIGKILL);  // Kill everyone

  exit(0);
}

