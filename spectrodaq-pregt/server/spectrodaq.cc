class StatusWatcher : public DAQThread {
  int operator()(int argc,char **argv) {
    DAQStatus status;          // Status work var
    DAQString namestr;
    DAQThreadId aId;           // Work Id
    StatusThread     aStatusThread;
    DAQThreadId      aStatusThread_id;

    sync_begin(GlobalSync);
      namestr = namestr + "StatusWatcher: Watching the Status Thread";
      SetThreadTitle(namestr);

      LOG((DAQCSTR("Starting the Status Thread...")));
      aStatusThread_id = daq_dispatcher.Dispatch(aStatusThread);
      LOG(("Status Thread Id = \"%s\"",aStatusThread_id.EncodeAsString().Get()));
    sync_end; // GlobalSync

    for(;;) {
      try {
        aId = Join(aStatusThread_id,&status);
        LOG(("ThreadId = \"%s\" Exited",aId.EncodeAsString().Get()));
        LOG(("     Status: CTXT=\"%s\" MSG=\"%s\" CODE=%d",status.GetContextString(),status.GetStatusString(),status.GetStatusCode()));

        if (aId == aStatusThread_id) {
          LOG((DAQCSTR("The Status Thread died.  Restarting...")));
          aStatusThread_id = daq_dispatcher.Dispatch(aStatusThread);
          LOG(("Status Thread Id = \"%s\"",aStatusThread_id.EncodeAsString().Get()));
          if (aStatusThread_id == GetId()) {
            LOG((DAQCSTR("DAQStatusWatcher::operator() Failed to dispatch a new status thread")));
            DAQNode::Exit(1);
          }
        }
      } catch(...) {
        LOGX(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQStatusWatcher::operator() Caught an unknown exception")));
      } 
    }

    DAQNode::Exit(0);
    return(0); 
  } 
};

