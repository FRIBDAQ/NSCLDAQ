/**
* @class Block
* @brief Block of storage for allocating buffers. 
*
* Block of storage used by the buffer allocator for allocating 
* buffers.  In this implementation, Buffers are composed of 
* blocks of allocated storage.
*
* The template parameter BTYPE is the type primitive values 
* stored in this block.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class BTYPE>
class Block {
  public:
    /*==============================================================*/
    /** @fn GetLen()
    * @brief Get the length of this block.
    *
    * Return the length of this block in units of BTYPE.
    *
    * @param None
    * @return The length of this block.
    */                                                             
    int GetLen() {
      return(len);
    };

    /*==============================================================*/
    /** @fn Get()
    * @brief Return a pointer to the encapsulated data.
    *
    * Return a pointer to the encapsulated array of values of type
    * BTYPE.    
    *
    * @param None
    * @return A pointer to the encapsulated data.
    */                                                             
    virtual BTYPE *Get() {
      return(data);
    };

    /*==============================================================*/
    /** @fn IsDirty()
    * @brief Return if this block is dirty.
    *
    * Return true if this block is dirty.  That is, a block is dirty
    * if its data has been updated.  This is a virtual method that
    * needs to be overridden to be useful.
    *
    * @param None
    * @return If the block is dirty.
    *    @retval true If the block is dirty.
    *    @retval false Otherwise.
    */                                                             
    virtual bool IsDirty() {
      return(false);
    }

    /*==============================================================*/
    /** @fn IsLastPage()
    * @brief Return if this block is the last page in a buffer.
    *
    * Return true if this block is the last page in a buffer.  
    * That is, a buffer is made of multiple blocks.  This method 
    * will return true if this block is the last in a list of blocks
    * composing a buffer.  This is a virtual method that
    * needs to be overridden to be useful.
    *
    * @param None
    * @return If this block is the last block in a buffer.
    *    @retval true If the block is last.
    *    @retval false Otherwise.
    */                                                             
    virtual bool IsLastPage() {
      return(false);
    }

    /*==============================================================*/
    /** @fn IsInUse()
    * @brief Return if this block is in use.
    *
    * Return true if this block is in use.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return If this block is in use.
    *    @retval true If the block is in use.
    *    @retval false Otherwise.
    */                                                             
    virtual bool IsInUse() {
      return(false);
    }

    /*==============================================================*/
    /** @fn IsModified()
    * @brief Return if this block has been modified.
    *
    * Return true if this block has been modified since this flag
    * was last reset.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return If this block has been modified.
    *    @retval true If the block has been modified.
    *    @retval false Otherwise.
    */                                                             
    virtual bool IsModified() {
      return(false);
    }

    /*==============================================================*/
    /** @fn IsReadOnly()
    * @brief Return if this block is read only.
    *
    * Return true if this block is read only and should reject and
    * changes to its data.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return If this block is read only.
    *    @retval true If the block is read only.
    *    @retval false Otherwise.
    */                                                             
    virtual bool IsReadOnly() {
      return(false);
    }

    /*==============================================================*/
    /** @fn GetOwner()
    * @brief Return the owner of this block.
    *
    * Return the owner of this block.  Typically, the owner is
    * represented by the process id of the owning process.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The process id of the owner.
    */                                                             
    virtual pid_t GetOwner() {
      return(-1);
    }

    /*==============================================================*/
    /** @fn SetTag(unsigned int aTag)
    * @brief Set the routing tag for this block.
    *
    * Set the routing tag for this block.  Routing tags are
    * used to direct buffers to destination sinks for processing.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param aTag The desired routing tag.
    * @return None
    */                                                             
    virtual void SetTag(unsigned int aTag) {
    }

    /*==============================================================*/
    /** @fn SetDirty()
    * @brief Set the dirty bit for this block.
    *
    * Set the dirty bit for this block.  
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the dirty bit.
    */                                                             
    virtual bool SetDirty() {
      return(false);
    }

    /*==============================================================*/
    /** @fn ResetDirty()
    * @brief Reset the dirty bit for this block.
    *
    * Reset the dirty bit for this block.  
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the dirty bit.
    */                                                             
    virtual bool ResetDirty() {
      return(false);
    }

    /*==============================================================*/
    /** @fn SetNeverDirty()
    * @brief Set the never dirty bit for this block.
    *
    * Set the never dirty bit for this block.  A block is never
    * dirty if it shouldn't be considered as dirty even if the
    * dirty bit is set.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the never dirty bit.
    */                                                             
    virtual bool SetNeverDirty() {
      return(false);
    }

    /*==============================================================*/
    /** @fn ResetNeverDirty()
    * @brief Reset the never dirty bit for this block.
    *
    * Reset the never dirty bit for this block.  A block is never
    * dirty if it shouldn't be considered as dirty even if the
    * dirty bit is set.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the never dirty bit.
    */                                                             
    virtual bool ResetNeverDirty() {
      return(false);
    }

    /*==============================================================*/
    /** @fn SetModified()
    * @brief Set the modified bit for this block.
    *
    * Set the modified bit for this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the modified bit.
    */                                                             
    virtual bool SetModified() {
      return(false);
    }

    /*==============================================================*/
    /** @fn ResetModified()
    * @brief Reset the modified bit for this block.
    *
    * Reset the modified bit for this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the modified bit.
    */                                                             
    virtual bool ResetModified() {
      return(false);
    }

    /*==============================================================*/
    /** @fn SetLastPage()
    * @brief Set the last page bit for this block.
    *
    * Set the last page bit for this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the last page bit.
    */                                                             
    virtual bool SetLastPage() {
      return(false);
    }

    /*==============================================================*/
    /** @fn ResetLastPage()
    * @brief Reset the last page bit for this block.
    *
    * Reset the last page bit for this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the last page bit.
    */                                                             
    virtual bool ResetLastPage() {
      return(false);
    }

    /*==============================================================*/
    /** @fn SetInUse()
    * @brief Set this block as in use.
    *
    * Set this block as in use.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the in use bit.
    */                                                             
    virtual bool SetInUse() {
      return(false);
    }

    /*==============================================================*/
    /** @fn ResetInUse()
    * @brief Reset this block as not in use.
    *
    * Reset this block as not in use.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the in use bit.
    */                                                             
    virtual bool ResetInUse() {
      return(false);
    }

    /*==============================================================*/
    /** @fn SetReadOnly()
    * @brief Mark this block as read only.
    *
    * Mark this block as read only.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the read only bit.
    */                                                             
    virtual bool SetReadOnly() {
      return(false);
    }

    /*==============================================================*/
    /** @fn ResetReadOnly()
    * @brief Reset the read only status of this block.
    *
    * Reset the read only status of this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The current state of the read only bit.
    */                                                             
    virtual bool ResetReadOnly() {
      return(false);
    }

    /*==============================================================*/
    /** @fn SetPos(long aPos)
    * @brief Set the position of this block within a block list.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method is used to specify the position this block
    * occupies within a list.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param aPos This block's position within a list of blocks.
    * @return None
    */                                                             
    virtual void SetPos(long aPos) {
      return;
    }

    /*==============================================================*/
    /** @fn SetReqLen(long aLen)
    * @brief Set the requested length of the parent object.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method indicates the requested length of the object
    * (buffer) that is constructed using this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param aLen The requested length. 
    * @return None
    */                                                             
    virtual void SetReqLen(long aLen) {
      return;
    }

    /*==============================================================*/
    /** @fn GetReqLen()
    * @brief Return the requested length of the parent object.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method returns the requested length of the object
    * (buffer) that is constructed using this block.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The requested length.
    */                                                             
    virtual long GetReqLen() {
      return(0);
    }

    /*==============================================================*/
    /** @fn SetByteLen(long aLen)
    * @brief Set the length in bytes of the parent object.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method sets the requested length of the object
    * (buffer) that is constructed using this block in bytes.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param aLen Length in bytes.
    * @return None
    */                                                             
    virtual void SetByteLen(long aLen) {
      return;
    }

    /*==============================================================*/
    /** @fn GetByteLen()
    * @brief Get the length in bytes of the parent object.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method returns the requested length of the object
    * (buffer) that is constructed using this block in bytes.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The length in bytes.
    */                                                             
    virtual long GetByteLen() {
      return(0);
    }

    /*==============================================================*/
    /** @fn SetUsedLen(long aLen)
    * @brief Set the used length of the parent object.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method sets the number of used slots of the object
    * (buffer) that is constructed using this block in bytes.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param aLen Used length.
    * @return None
    */                                                             
    virtual void SetUsedLen(long aLen) {
      return;
    }

    /*==============================================================*/
    /** @fn GetUsedLen()
    * @brief Get the used length of the parent object.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method returns the number of used slots of the object
    * (buffer) that is constructed using this block in bytes.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The used length.
    */                                                             
    virtual long GetUsedLen() {
      return(0);
    }

    /*==============================================================*/
    /** @fn SetObjId(void *aId)
    * @brief Set the parent object identifier for this block.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method sets the parent object (buffer) identifier 
    * that is constructed using this block in bytes.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param aId The ID of the parent object.
    * @return None
    */                                                             
    virtual void SetObjId(void *aId) {
      return;
    }

    /*==============================================================*/
    /** @fn GetObjId()
    * @brief Get the parent object identifier for this block.
    *
    * Buffers are made up of lists of storage allocation blocks. 
    * This method returns the parent object (buffer) identifier 
    * that is constructed using this block in bytes.
    * This is a virtual method that needs to be overridden 
    * to be useful.
    *
    * @param None
    * @return The parent object identifier.
    */                                                             
    virtual unsigned int GetObjId() {
      return(0);
    }

    /*==============================================================*/
    /** @fn ~Block()
    * @brief Destructor.
    *
    * Destructor.
    *
    * @param None
    * @return None
    */                                                             
    ~Block() { 
      len = 0;
      data = NULL;
    };

  protected:
    /*==============================================================*/
    /** @fn Block()
    * @brief Default constructor.
    *
    * Default constructor.
    * 
    *
    * @param None
    * @return None
    */                                                             
    Block() { 
      len = 0;
      data = NULL;
    };

    /**
    * @var data
    * @brief Pointer to an array of block data.
    *
    * Pointer to an array of block data of type BTYPE.
    */
    BTYPE *data;      // Block data

    /**
    * @var len
    * @brief Length of this block.
    *
    * Length of this block.
    */
    int len;          // Data length
};

