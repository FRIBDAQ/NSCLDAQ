/**
* @class BufferAllocator
* @brief Allocator for constructing buffers.
*
* Allocator that allocates blocks of storage for constructing
* buffers.
*
* The BLKTYPE template parameter specifies the type of Blocks that
* will be used to construct buffers.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class BLKTYPE>
class BufferAllocator {
  public:
    /*==============================================================*/
    /** @fn ~BufferAllocator()
    * @brief Destructor.
    *
    * Destructor.
    *
    * @param None
    * @return None
    */                                                             
    ~BufferAllocator() {
     };

    /*==============================================================*/
  protected:
    /*==============================================================*/
    /** @fn BufferAllocator()
    * @brief Default constructor.
    *
    * Default constructor.
    *
    * @param None
    * @return None
    */                                                             
    BufferAllocator() {
    }; 

    /*==============================================================*/
    /** @fn Request(int n)
    * @brief Request a new chain of blocks.  
    *
    * Request a new chain of blocks and wait for the request to
    * complete. This is a pure virtual method. 
    *
    * @param n The data length needed.
    * @return A linked list of blocks.
    */                                                             
    virtual LinkedList<BLKTYPE> *Request(int n) = 0;

    /*==============================================================*/
    /** @fn Try(int n)
    * @brief Request a new chain of blocks.  
    *
    * Request a new chain of blocks but do not wait for the request to
    * complete. That is, if the request length cannot be allocated,
    * then simply return. This is a pure virtual method. 
    *
    * @param n The data length needed.
    * @return A linked list of blocks.
    */                                                             
    virtual LinkedList<BLKTYPE> *Try(int n) = 0; 

    /*==============================================================*/
    /** @fn Release(LinkedList<BLKTYPE> *b)
    * @brief Release a linked list of blocks.
    *
    * Release a linked list of blocks.
    * This is a pure virtual method. 
    *
    * @param b The linked list of blocks to release.
    * @return None
    */                                                             
    virtual void Release(LinkedList<BLKTYPE> *b) = 0;

    /*==============================================================*/
    /** @fn Route(LinkedList<BLKTYPE> *b)
    * @brief Route a linked list of blocks.
    *
    * Route a linked list of blocks.  That is, indicate that this
    * list of blocks can be sent on for processing.
    * This is a pure virtual method. 
    *
    * @param b The linked list of blocks to route.
    * @return None
    */                                                             
    virtual void Route(LinkedList<BLKTYPE> *b) = 0;

    /*==============================================================*/
    /** @fn Accept(unsigned int aTag,unsigned int aMask,struct timeval *pTO)
    * @brief Accept a list of blocks from else where.
    *
    * Accept a list of blocks that was Routed from else where.
    * This is a pure virtual method. 
    *
    * @param aTag A routing tag to match for acceptance.
    * @param aMask A routing mask to limit the match for acceptance.
    * @param pTO How long to wait for acceptance.
    * @return A linked list of blocks or NULL.
    */                                                             
    virtual LinkedList<BLKTYPE> *Accept(unsigned int aTag,unsigned int aMask = ALLBITS_MASK,struct timeval *pTO = NULL) = 0;

};

