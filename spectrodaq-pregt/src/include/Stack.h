/**
* @class StackItem
* @brief A class for encapsulating stack objects.
*
* A class for encapsulation and linking stack objects
* so they can be added to a stack.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LTYPE>
class StackItem : virtual public Printable {
  public:
    /*==============================================================*/
    /** @fn StackItem(LTYPE b)
    * @brief Constructor with an object.
    *
    * Constructor with an object.
    *
    * @param b An object.
    * @return this
    */                                                             
    StackItem(LTYPE b) { // Constructor
      myitem = b;        // Objects need to have operator=
      nxt = NULL; prv = NULL;
    }; 

    /*==============================================================*/
    /** @fn ~StackItem()
    * @brief Destructor.
    *
    * Destroy this stack item.
    *
    * @param None
    * @return None
    */                                                             
    ~StackItem() {};  // Destructor

    /*==============================================================*/
    /** @fn Get()
    * @brief Get the encapsulated object.
    *
    * Return the encapsulated object.
    *
    * @param None
    * @return The encapsulated object.
    */                                                             
    LTYPE Get() {return(myitem);}; // Get item encapsulated by this item 

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next stack item.
    *
    * Stack items are linked together using next and
    * previous pointers.  This method returns the stack item 
    * linked following this one.
    *
    * @param None
    * @return The next stack item.
    */                                                             
    StackItem<LTYPE> *GetNext() {return(nxt);};  // Get next stack item

    /*==============================================================*/
    /** @fn GetPrev()
    * @brief Get the previous stack item.
    *
    * Stack items are linked together using next and
    * previous pointers.  This method returns the stack item 
    * linked prior to this one.
    *
    * @param None
    * @return The previous stack item.
    */                                                             
    StackItem<LTYPE> *GetPrev() {return(prv);};  // Get prev stack item

    /*==============================================================*/
    /** @fn SetNext(StackItem<LTYPE> *b)
    * @brief Set the next stack item.
    *
    * Stack items are linked together using next and
    * previous pointers.  This method sets the stack item 
    * linked next this one to the one specified as a parameter.
    *
    * @param b The new next stack item pointer.
    * @return None
    */                                                             
    void *SetNext(StackItem<LTYPE> *b) {nxt = b;};  // Set the next stack item

    /*==============================================================*/
    /** @fn SetPrev(StackItem<LTYPE> *b)
    * @brief Set the previous stack item.
    *
    * Stack items are linked together using next and
    * previous pointers.  This method sets the stack item 
    * linked prior to this one to the one specified as a parameter.
    *
    * @param b The new next stack item pointer.
    * @return None
    */                                                             
    void SetPrev(StackItem<LTYPE> *b) {prv = b;};   // Set the prev stack item

    /*==============================================================*/
    /** @fn operator=(const StackItem<LTYPE>& l)
    * @brief Assignment operator.
    *
    * Assign a different stack item to this one.
    *
    * @param l The stack item to assign to this one.
    * @return this
    */                                                             
    StackItem<LTYPE>& operator=(const StackItem<LTYPE>& l) {
      myitem = l.myitem;
      nxt = l.nxt;
      prv = l.prv;
      return(*this);
    };

    /*==============================================================*/
    /** @fn void toStream(PrintStreamIface& aStream)
    * @brief Print this object on a PrintStream.
    *
    * Print this object on a PrintStream.
    *
    * @param aStream The PrintStream.
    * @return None
    */                                                             
    void toStream(PrintStreamIface& aStream) {
      aStream.print("Item=[");
      aStream.print(myitem);
      aStream.print("],Ptr=");
      aStream.print((void*)this); 
      aStream.print(",Next=");
      aStream.print(nxt);
      aStream.print(",Prev=");
      aStream.print(prv);
    };

  protected:
    /*==============================================================*/
    /** @fn StackItem()
    * @brief Default constructor.
    *
    * Default constructor.  Protected and should not be called
    * directly.
    *
    * @param None
    * @return this
    */                                                             
    StackItem() { };  // Default constructor

  private:
    /**
    * @var myitem
    * @brief Encapsulated object.
    *
    * Object encapsulated by this stack item.
    */
    LTYPE  myitem;  // Object encapsulated by this stack item

    /**
    * @var nxt
    * @brief Next stack item.
    *
    * Stack item linked following this one. 
    */
    StackItem<LTYPE> *nxt;  // Next stack item

    /**
    * @var prv
    * @brief Previous stack item.
    *
    * Stack item linked prior to this one. 
    */
    StackItem<LTYPE> *prv;  // Prev stack item
};

