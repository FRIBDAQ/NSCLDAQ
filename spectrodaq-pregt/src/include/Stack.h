/**
* @class Stack
* @brief Template stack class.
* 
* Template class definition for general value stack object.                 
* The LITEM template parameter represents the type of 
* StackItems to be stored in this stack.  The MUTEX template
* parameter represents an implementation of the MutexIface class
* that can be used for concurency control.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LITEM,class MUTEX>
class Stack : virtual public Printable {
  public:
    /*==============================================================*/
    /** @fn Stack()
    * @brief Default constructor.
    *
    * Default constructor.  A stack constructed using this
    * default constructor will be dynamically resized.
    *
    * @param None
    * @return this
    */                                                             
    Stack() 
      {stacktop = NULL; stackbot = NULL; scnt = 0; maxscnt = 0;}; 

    /*==============================================================*/
    /** @fn Stack(int aSize)
    * @brief Constructor with a maximum stack size.
    *
    * Constructor with a maximum stack size.  Zero or negative
    * indicates that the stack should be dynamically resized.
    *
    * @param aSize The maximum stack size.
    * @return this
    */                                                             
    Stack(int aSize) 
      {stacktop = NULL; stackbot = NULL; scnt = 0; maxscnt = aSize;}; 

    /*==============================================================*/
    /** @fn Empty()
    * @brief Empty this stack.
    *
    * Remove all items from this stack.
    *
    * @param None
    * @return None
    */                                                             
    void Empty() {
       int j;
       StackItem<LITEM> *i,*i2;
    
       objmutex.Lock();
  
       i = stackbot; 
       for (j = 0; j < scnt; j++) { 
         if (i != NULL) {
           i2 = i->GetNext(); 
           delete i;
           i = i2;
         }
       }

       stackbot = stacktop = NULL;
       scnt = 0;

       objmutex.UnLock();
    };
  
    /*==============================================================*/
    /** @fn ~Stack()
    * @brief Destructor.
    *
    * Destroy this stack.
    *
    * @param None
    * @return None
    */                                                             
    ~Stack() {
       Empty();
     };

    /*==============================================================*/
    /** @fn Push(LITEM nitm,bool lockme)    
    * @brief Push an item on the stack.
    *
    * Push an item the stack.  
    *
    * @param nitm The item to push on the stack.
    * @param lockme If the stack should be locked during the push.
    * @return If the push was successful.
    *   @retval true If the push was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Push(LITEM nitm,bool lockme=true) {
      StackItem<LITEM> *itm;

      if (lockme) objmutex.Lock();

      if ((maxscnt > 0)&&(scnt >= maxscnt)) { // Full
        if (lockme) objmutex.UnLock();
        return(false); 
      }

      itm  = new StackItem<LITEM>(nitm);

      if (itm != NULL) {   
        if (stacktop == NULL) {
          stackbot = itm;
          stacktop = itm; 
          itm->SetNext(NULL);
          itm->SetPrev(NULL);
        } else {
          stacktop->SetNext(itm);
          itm->SetPrev(stacktop);
          itm->SetNext(NULL);
          stacktop = itm;
        }
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }

      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn Pop()
    * @brief Pop an item off the stack.
    *
    * Pop an item off the stack.
    *
    * @param None
    * @return The item from the top of the stack or null.
    */                                                             
    LITEM Pop() {
      LITEM itm;
      StackItem<LITEM> *bit,*pbit;

      objmutex.Lock();

      if (stacktop != NULL) { 
        bit = stacktop;
        pbit = stacktop->GetPrev(); 
        itm = stacktop->Get();

        if (pbit != NULL) pbit->SetNext(NULL);
        stacktop = pbit;
        if (stacktop == NULL) stackbot = NULL;
        scnt--;
        delete bit;
      }

      objmutex.UnLock();
      return(itm);
    };

    /*==============================================================*/
    /** @fn Peek()
    * @brief Peek at the item on the top of the stack.
    *
    * Peek at the item on the top of the stack.  Does not
    * remove the top item.
    *
    * @param None
    * @return The item from the top of the stack or null.
    */                                                             
    LITEM Peek() {
      LITEM itm;

      objmutex.Lock();
      if (stacktop != NULL) itm = stacktop->Get();
      objmutex.UnLock();

      return(itm);
    };

    /*==============================================================*/
    /** @fn IsEmpty()
    * @brief Check if the stack is empty.
    *
    * Check if the stack contains no items.
    *
    * @param None
    * @return If the stack is empty.
    *   @retval true If the stack is empty.  
    *   @retval false Otherwise.
    */                                                             
    bool IsEmpty() {if (scnt < 1) return(true); else return(false);};
  
    /*==============================================================*/
    /** @fn Count()
    * @brief Return the number of items on the stack.
    *
    * Return the current count of items on the stack.
    *
    * @param None
    * @return The number of items on the stack.
    */                                                             
    int Count() {return(scnt);};
  
    /*==============================================================*/
    /** @fn IsFull()
    * @brief Check if the stack is full.
    *
    * Check if the stack contains its maximum number of items.
    *
    * @param None
    * @return If the stack is full.
    *   @retval true If the stack is full.  
    *   @retval false Otherwise.
    */                                                             
    bool IsFull() {return((scnt >= maxscnt)&&(maxscnt > 0));};
  
    /*==============================================================*/
    /** @fn Resize(int aSize)
    * @brief Change the maximum stack size.
    *
    * Change the maximum size of the stack.  Return true if
    * resize is successful.  Notice that you can resize to
    * a size smaller than the current number of items in the
    * stack, but you won't be able to add anything until enough
    * items have been deleted to reduce the number of items under
    * the max.
    *
    * @param aSize The new maximum size.
    * @return If the resize was successful.
    *   @retval true If the resize was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Resize(int aSize) {
      objmutex.Lock();
      if (aSize < 0) maxscnt = 0;    // Dynamic stack
      maxscnt = aSize;
      objmutex.UnLock();
      return(true);
    }
  
    /*==============================================================*/
    /** @fn void toStream(PrintStreamIface& aStream)
    * @brief Print this object on a PrintStream.
    *
    * Print this object on a PrintStream.
    *
    * @param aStream The PrintStream.
    * @return None
    */                                                             
    virtual void toStream(PrintStreamIface& aStream) {
      int i;
      StackItem<LITEM> *p;

      objmutex.Lock(); 
      aStream.print("Bottom=");
      aStream.print(stackbot);
      aStream.print(",Top=");
      aStream.print(stacktop);
      aStream.print(",Count=");
      aStream.print(scnt);
      aStream.print(",Max=");
      aStream.println(maxscnt);

      p = stackbot;
      for (i = 0; i < scnt; i++) { 
        this->str << "  Item " << i << ": ";
        if (p != NULL) {
          aStream.println(*p);
        } else {
          aStream.println("(nil pointer)");
        }
        p = p->GetNext();
      }

      objmutex.UnLock();
    }

    /*==============================================================*/
  protected:
    /**
    * @var objmutex
    * @brief Concurrency control mutex.
    *
    * Mutex for synchronizing multiple thread usage of this stack.
    */
    MUTEX objmutex;       // A mutex for this object

    /*==============================================================*/
  private:
    /**
    * @var stackbot
    * @brief Stack bottom.
    *
    * Item at the bottom of the stack (first item added).
    */
    StackItem<LITEM> *stackbot;  // Tail of stack

    /**
    * @var stacktop
    * @brief Stack top.
    *
    * Item at the top of the stack (last item added).
    */
    StackItem<LITEM> *stacktop;  // Head of stack

    /**
    * @var scnt
    * @brief Number of stack items.
    *
    * Current count of the number of items on the stack.
    */
    int scnt;             // Number of stack items.

    /**
    * @var maxscnt
    * @brief Maximum stack size.
    *
    * Maximum number of items that can be added to the stack.
    */
    int maxscnt;          // Max number of stack items.
};

