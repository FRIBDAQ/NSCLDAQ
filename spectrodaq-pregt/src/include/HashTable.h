/**
* @class HashTableItem
* @brief Template class for constructing a hash table item.
*
* Template class for constructing a hash table item.  HashTableItems
* are inserted into HashTable.  The template parameter STYPE is
* the type of the object stored by objects of this type.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class STYPE>
class HashTableItem : virtual public Printable, public Synchronizable {
  public:
    /*==============================================================*/
    /** @fn HashTableItem()
    * @brief Constructor.
    *
    * Default constructor.
    * 
    *
    * @param None
    * @return this
    */                                                             
    HashTableItem() {
      refcnt = NULL;
      Init((long)0,NULL);
    }

    /*==============================================================*/
    /** @fn HashTableItem(const char *nam,STYPE *itm)
    * @brief Constructor with a named item.
    *
    * Constructor with a named item.  Constructs a HashTableItem
    * containing an item that is hashed using a specified name.
    *
    * @param nam A character string to use as a hash key.
    * @param itm The item to be hashed.
    * @return this
    */                                                             
    HashTableItem(const char *nam,STYPE *itm) {
      DAQString str;
      refcnt = NULL;
      str = nam;
      Init(str,itm);
    }

    /*==============================================================*/
    /** @fn HashTableItem(const DAQString& nam,STYPE *itm)
    * @brief Constructor with a named item.
    *
    * Constructor with a named item.  Constructs a HashTableItem
    * containing an item that is hashed using a specified name.
    *
    * @param nam A DAQString reference to use as a hash key.
    * @param itm The item to be hashed.
    * @return this
    */                                                             
    HashTableItem(const DAQString& nam,STYPE *itm) {
      refcnt = NULL;
      Init(nam,itm);
    }

    /*==============================================================*/
    /** @fn HashTableItem(const long aNum,STYPE *itm)
    * @brief Constructor with a numbered item.
    *
    * Constructor with a numbered item.  Constructs a HashTableItem
    * containing an item that is hashed using a long integer.
    *
    * @param aNum A long integer to use as a hash key.
    * @param itm The item to be hashed.
    * @return this
    */                                                             
    HashTableItem(const long aNum,STYPE *itm) {
      refcnt = NULL;
      Init(aNum,itm);
    }

    /*==============================================================*/
    /** @fn ~HashTableItem()
    * @brief Destructor.
    *
    * Destructor.
    * 
    * @param None
    * @return None
    */                                                             
    ~HashTableItem() {
       Nullify();
    }

    /*==============================================================*/
    /** @fn GetName()
    * @brief Return this item's name.
    *
    * Return this item's name as a DAQString.
    * 
    * @param None
    * @return The DAQString version of this item's hash key.
    */                                                             
    DAQString& GetName() {
      return(hashname);
    }

    /*==============================================================*/
    /** @fn GetNumeric()
    * @brief Return this item's numeric name.
    *
    * Return this item's numeric name as a long integer.
    * 
    * @param None
    * @return The numeric version of this item's hash key.
    */                                                             
    long GetNumeric() {
      return(hashnumeric);
    }

    /*==============================================================*/
    /** @fn Set(const DAQString& nam,STYPE *itm)
    * @brief Assign a named item to this HashTableItem.
    *
    * Assign a named item to this HashTableItem.
    *
    * @param nam A DAQString reference to use as a hash key.
    * @param itm The item to be hashed.
    * @return None
    */                                                             
    void Set(const DAQString& nam,STYPE *itm) {
      Nullify();
      Init(nam,itm);
    }

    /*==============================================================*/
    /** @fn Set(const long aNum,STYPE *itm)
    * @brief Assign a numbered item to this HashTableItem.
    *
    * Assign a numbered item to this HashTableItem.
    *
    * @param aNum A long integer to use as a hash key.
    * @param itm The item to be hashed.
    * @return None
    */                                                             
    void Set(const long aNum,STYPE *itm) {
      Nullify();
      Init(aNum,itm);
    }

    /*==============================================================*/
    /** @fn GetItem()
    * @brief Return a pointer to the item stored in this HashTableItem.
    *
    * Return a pointer to the item stored in this HashTableItem.
    *
    * @param None
    * @return A pointer to the item stored in this HashTableItem.
    */                                                             
    STYPE *GetItem() {
      return(hashitem);
    }

    /*==============================================================*/
    /** @fn IsNull()
    * @brief Returns if this HashTableItem has nothing assigned to it.
    *
    * Returns if this HashTableItem has nothing assigned to it.
    * That is, if there is no item stored in this HashTableItem, this
    * method will return true.
    *
    * @param None
    * @return If this is a null item.
    *   @retval true If this item is null. 
    *   @retval false Otherwise.
    */                                                             
    bool IsNull() {
      if (hashitem == NULL) return(true);
      else return(false); 
    }

    /*==============================================================*/
    /** @fn IsNumeric()
    * @brief Return if this is a numeric item.
    *
    * Return if this a numeric item.  That is, if the item stored 
    * in this HashTableItem is hash by a long integer, this method
    * will return true.
    *
    * @param None
    * @return If this is a numeric item.
    *   @retval true If this item is numeric.
    *   @retval false Otherwise.
    */                                                             
    bool IsNumeric() {
      return(isnumeric);
    }

    /*==============================================================*/
    /** @fn Nullify()
    * @brief Make this HashTableItem null.
    *
    * Make this HashTableItem null.  That is, make it such that
    * this HashTableItem stores no real item.
    *
    * @param None
    * @return None
    */                                                             
    void Nullify() {
      if (refcnt != NULL) {
         (*refcnt)--;
         if ((hashitem != NULL)&&((*refcnt) <= 0)) delete hashitem;
         if ((*refcnt) <= 0) {
           delete refcnt;
           refcnt = NULL;
         }
      }
      hashitem = NULL;
      hashname = "";
      hashnumeric = -1;
    }

    /*==============================================================*/
    /** @fn operator=(const HashTableItem<STYPE>& aItem)
    * @brief Assignment operator.
    *
    * Assign a different HashTableItem to this.
    *
    * @param aItem The HashTableItem to assign to this.
    * @return A reference to this.
    */                                                             
    HashTableItem<STYPE>& operator=(const HashTableItem<STYPE>& aItem) {
      Nullify();
      hashitem = aItem.hashitem;
      hashname = aItem.hashname;
      hashnumeric = aItem.hashnumeric;
      isnumeric = aItem.isnumeric;
      refcnt = aItem.refcnt;
      if (refcnt != NULL) (*refcnt)++; 
      return(*this);
    }

    /*==============================================================*/
    /** @fn void toStream(PrintStreamIface& aStream)
    * @brief Print this object to a PrintStream.
    *
    * Print this object to a PrintStream.
    *
    * @param aStream The PrintStream.
    * @return None
    */                                                             
    virtual void toStream(PrintStreamIface& aStream) {
      if (!IsNull()) {
        aStream.print("\"");
        if (isnumeric) aStream.print(hashnumeric);
        else aStream.print(hashname);
        aStream.print("\" == \"");
        aStream.print(*hashitem);
        aStream.print("\"");
      }
    }

  /*==============================================================*/
  protected:
    /*==============================================================*/
    /** @fn Init(const DAQString& nam,STYPE *itm)
    * @brief Initialize this HashTableItem.  
    *
    * Initialize this HashTableItem.
    *
    * @param nam A DAQString reference to use as a hash key.
    * @param itm The item to be hashed.
    * @return None
    */                                                             
    void Init(const DAQString& nam,STYPE *itm) {
      hashnumeric = -1;
      isnumeric = false;

      if ((nam.GetLen() <= 0)||(itm == NULL)) {
        hashitem = NULL;
        hashname = "";
        refcnt = NULL;
      } else {
        hashname = nam;
        hashitem = itm;
        refcnt = new int;
        (*refcnt) = 1;
      }
    }
     
    /*==============================================================*/
    /** @fn Init(const long aNum,STYPE *itm)
    * @brief Initialize this HashTableItem.  
    *
    * Initialize this HashTableItem.
    *
    * @param aNum A long integer to use as a hash key.
    * @param itm The item to be hashed.
    * @return None
    */                                                             
    void Init(const long aNum,STYPE *itm) {
      hashname = "";
      isnumeric = true;

      if (itm == NULL) {
        hashitem = NULL;
        hashnumeric = -1;
        refcnt = NULL;
      } else {
        hashnumeric = aNum;
        hashitem = itm;
        refcnt = new int;
        (*refcnt) = 1;
      }
    }
     
  /*==============================================================*/
  private:
    /**
    * @var hashitem
    * @brief The item stored in this HashTableItem.
    *
    * The item stored in this HashTableItem.
    */
    STYPE *hashitem;        // Hash table item

    /**
    * @var hashname
    * @brief DAQString hashname
    *
    * DAQString hashname.
    */
    DAQString hashname;     // Hash table item name

    /**
    * @var hashnumeric
    * @brief Numeric hash key.
    *
    * Numeric hash key.
    */
    long hashnumeric;       // Hash table item numeric name

    /**
    * @var isnumeric
    * @brief True if this a numeric hash.
    *
    * True if this item has a numeric hash key.
    */
    bool isnumeric;         // true if this is a numeric hash

    /**
    * @var refcnt
    * @brief Reference count.
    *
    * Reference count for this item.
    */
    int *refcnt;            // Reference count
};

