    /*==============================================================*/
    /** @fn GetFirst()
    * @brief Get the first nonnull item from the hash table.
    *
    * Get the first nonnull item from the hash table.  Although
    * a hash table doesn't logically have a first item, it is 
    * useful to be able to order the items in the hash table
    * such that iterative processing is possible.  Note that
    * the order used has nothing to do with the order items were
    * entered into the table.
    *
    * @param None
    * @return The first item or null if none exists.
    */                                                             
    STYPE *GetFirst() {
      STYPE *wrk;

      cur = 0;

      while (htable[cur].IsNull() && (cur <= slots)) cur++;

      if ((cur > slots)||(htable[cur].IsNull())) wrk = NULL;
      else wrk = htable[cur].GetItem();

      return(wrk);
    };

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next nonnull item from the hash table.
    *
    * Get the next nonnull item from the hash table (see GetFirst).  
    * Although a hash table doesn't logically have a first item, 
    * it is useful to be able to order the items in the hash table
    * such that iterative processing is possible.  Note that
    * the order used has nothing to do with the order items were
    * entered into the table.
    *
    * @param None
    * @return The first item or null if none exists.
    */                                                             
    STYPE *GetNext() {
      STYPE *wrk;

      cur++;
      if (cur > slots) return(NULL);

      while (htable[cur].IsNull() && (cur <= slots)) cur++;

      if ((cur > slots)||(htable[cur].IsNull())) wrk = NULL;
      else wrk = htable[cur].GetItem();

      return(wrk);
    };

    /*==============================================================*/
    /** @fn operator()(DAQString& nam)
    * @brief Find a named item in the hash table.
    *
    * Find a named item in the hash table.
    *
    * @param nam A DAQString hash key to locate.
    * @return A pointer to the item or null if no item was found.
    */                                                             
    STYPE *operator()(DAQString& nam) {
      long hval,pos;
      STYPE *wrk;

      hval = HashFunc(nam);
      if (hval < 0) return(NULL);

      pos = hval; 
      while (pos <= slots) {
        if ((htable[pos].IsNull())||(htable[pos].GetName() != nam)) pos++;
        else break;
      }
 
      if (pos > slots) wrk = NULL;
      else if (htable[pos].IsNull()) wrk = NULL;
      else wrk = htable[pos].GetItem();
      return(wrk);
    };
 
