    virtual void toStream(PrintStreamIface& aStream) {
      int i;

      for (i = 0; i <= slots; i++) {
        if (!htable[i].IsNull()) {
          aStream.printf("Slot[%d] ",i); 
          aStream.println(htable[i]);
        }
      }
    }

