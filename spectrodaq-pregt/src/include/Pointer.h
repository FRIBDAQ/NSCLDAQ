/*=====================================================================*/
/** @class BasicPointer
* @brief Interface definition for a basic pointer type object.
*
* This interface class defines the pure virtual methods
* Nullify(), IsNull() and GetIndex() that are needed in 
* any Pointer type object.
*
* @author  Eric Kasten                                                          
* @version 0.4.0                                                                
*/  
class BasicPointer {
  public:
    virtual void Nullify() = 0; // Nullify pointer
    virtual bool IsNull() = 0;  // True if NULL 
    virtual int GetIndex() = 0; // Return the position to which you point
  protected:
    BasicPointer() { };       // Constructor
};
 
/*=====================================================================*/
/** @class Pointer
* @brief Template class definition for a basic pointer type object.
*
* Template class definition for a basic pointer type object.
* Pointer objects expect to reference objects that support
* operator[].  Also, these objects must support a ReferenceBy()
* and DereferenceBy() function.
*
* @author  Eric Kasten                                                          
* @version 0.4.0                                                                
*/  
template <class STYPE,class ATYPE>
class Pointer : public BasicPointer {
  public:
    /*==============================================================*/
    /** @fn Pointer::Pointer()
    * @brief Constructor.
    *
    * Basic Constructor method for this.
    *                                                             
    * @param None
    * @return this
    */ 
    Pointer() {
      ptr = NULL;
      Init(NULL);
    }; 

    /*==============================================================*/
    /** @fn Pointer::Pointer(const Pointer<STYPE,ATYPE>& s)
    * @brief Copy constructor.
    *
    * Copy constructor method for this.
    *                                                             
    * @param s Another Pointer to copy. 
    * @return this
    */                                                             
    Pointer(const Pointer<STYPE,ATYPE>& s) {
      ptr = s.ptr;
      pos = s.pos;
      if (ptr != NULL) ptr->ReferenceBy(this); 
    }; 

    /*==============================================================*/
    /** @fn Pointer::Pointer(STYPE *s)
    * @brief Constructor with subscriptable.
    *
    * Constructor with subscribtable object for this.
    *                                                             
    * @param s A subscriptable object.
    * @return this
    */
    Pointer(STYPE *s) {
      ptr = NULL;
      Init(s);
    }; 

    /*==============================================================*/
    /** @fn Pointer::~Pointer()
    * @brief Destructor
    *
    * Class Destructor.                                                 
    *
    * @param None
    * @return None
    */
    ~Pointer() {
      if (ptr != NULL) ptr->DereferenceBy(this); 
      ptr = NULL;
      pos = 0;
    };

    /*==============================================================*/
    /** @fn void Pointer::Dump(PrintStreamIface& aStream)
    * @brief Dump this pointer.
    *
    * Dump a formated representation of this pointer pointer to a PrintStream.  
    * Used for debugging purposes.
    *
    * @param aStream The PrintStream to which to dump this pointer.
    * @return None
    */                                                             
    void Dump(PrintStreamIface& aStream) {
      aStream.print("Pointer(");
      aStream.print((void *)this);
      aStream.print(") ptr=");
      aStream.print((void *)ptr);
      aStream.print(" pos=");
      aStream.println(pos);
    }

    /*==============================================================*/
    /** @fn void Pointer::Nullify()
    * @brief Make this Pointer null.
    *
    * Set this Pointer to NULL by dereferencing the object to
    * which it points and setting the position and pointer fields
    * to null.
    *
    * @param None
    * @return None
    */                                                             
    inline void Nullify() {
      if (ptr != NULL) ptr->DereferenceBy(this); 
      ptr = NULL;
      pos = 0;
    }

    /*==============================================================*/
    /** bool Pointer::IsNull()
    * @brief Return true if this is a null pointer.
    *
    * Return true if this is a null pointer (as if Nullify() had
    * been called on this).
    *
    * @param None
    * @return Whether this Pointer is null.
    *   @retval true This Pointer is null.
    *   @retval false This Pointer isn't null.
    */                                                             
    inline bool IsNull() {
      return(ptr == NULL);
    }

    /*==============================================================*/
    /** @fn size_t CopyIn(void *pMem,size_t aOset,size_t aLen)
    * @brief Copy a memory location to an array.
    *
    * Copy the contents of a memory location into this array.
    * The copy begins at the position indicated (in primitive 
    * type units) within the array and continues for the 
    * specified length.
    *
    * @param pMem The memory array to which to copy to the array.
    * @param aOset Offset position frow which to begin copying. 
    * @param aLen Number of primitive units to copy
    * @return The number of primitive units copied
    * @throw DAQUninitialized For a null encapsulated subscriptable object.
    */                                                             
    size_t CopyIn(void *pMem,size_t aOset,size_t aLen) {
      if (ptr == NULL) {
        LOG_AND_THROW(daq_exception_factory.CreateException(
							    DAQCSTR("Pointer::CopyIn() Null pointer access"),
							    DAQEXCPID(DAQOutOfBounds)));
        return(-DAQEXCPID(DAQOutOfBounds));
      }
      return ptr->CopyIn(pMem,pos+aOset,aLen); 
    }

    /*==============================================================*/
    /** @fn size_t CopyOut(void *pMem,size_t aOset,size_t aLen)
    * @brief Copy a array to a memory location.
    *
    * Copy the contents of this array to a memory location.
    * The copy begins at the position indicated (in primitive 
    * type units) within the array and continues for the 
    * specified length.
    *
    * @param pMem The memory location to which to copy the array.
    * @param aOset Offset position from which to begin coping. 
    * @param aLen Number of primitive units to copy.
    * @return The number of primitive units copied
    * @throw DAQUninitialized For a null encapsulated subscriptable object.
    */                                                             
    size_t CopyOut(void *pMem,size_t aOset,size_t aLen) {
      if (ptr == NULL) {
        LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Pointer::CopyOut() Null pointer access"),DAQEXCPID(DAQOutOfBounds)));
        return(-DAQEXCPID(DAQOutOfBounds));
      }
      return ptr->CopyOut(pMem,pos+aOset,aLen); 
    }

    /*==============================================================*/
    /** @fn size_t Write(int aFd,size_t aOset ,size_t aLen)
    * @brief Write an array to a file descriptor.
    *
    * Write the contents of this array to a file descriptor.
    * The write begins at the position indicated (in primitive 
    * type units) within the array and continues for the 
    * specified length.
    *
    * @param aFd The file descriptor on which to write
    * @param aOset Offset position frow which to begin writing. 
    * @param aLen Number of primitive units to write
    * @return The number of primitive units written
    * @throw DAQUninitialized For a null encapsulated subscriptable object.
    */                                                             
    size_t Write(int aFd,size_t aOset,size_t aLen) {
      if (ptr == NULL) {
        LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Pointer::Write() Null pointer access"),DAQEXCPID(DAQOutOfBounds)));
        return(-DAQEXCPID(DAQOutOfBounds));
      }
      return ptr->Write(aFd,pos+aOset,aLen); 
    }

    /*==============================================================*/
    /** @fn size_t Read(int aFd,size_t aOset,size_t aLen)
    * @brief Read a array to a file descriptor.
    *
    * Read in the contents of this array from a file descriptor.
    * The read begins at the position indicated (in primitive 
    * type units) within the array and continues for the 
    * specified length.
    *
    * @param aFd The file descriptor from which to read
    * @param aOset Offset position frow which to begin reading. 
    * @param aLen Number of primitive units to read 
    * @return The number of primitive units read 
    * @throw DAQUninitialized For a null encapsulated subscriptable object.
    */                                                             
    size_t Read(int aFd,size_t aOset,size_t aLen) {
      if (ptr == NULL) {
        LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Pointer::Read() Null pointer access"),DAQEXCPID(DAQOutOfBounds)));
        return(-DAQEXCPID(DAQOutOfBounds));
      }
      return ptr->Read(aFd,pos+aOset,aLen); 
    }

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator=(const Pointer<STYPE,ATYPE>& s)
    * @brief Assign a Pointer to this.
    *
    * Assign another pointer to this.
    *
    * @param s Another Pointer that has the same template signature.
    * @return this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator=(const Pointer<STYPE,ATYPE>& s) {
      bool same_ptr = true;

      if (ptr != s.ptr) same_ptr = false;

      if ((!same_ptr)&&(ptr != NULL)) ptr->DereferenceBy(this); 
      ptr = s.ptr;
      pos = s.pos;
      if ((!same_ptr)&&(ptr != NULL)) ptr->ReferenceBy(this); 
      return(*this);
    }

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator-=(const long i)
    * @brief Subtract from this
    *
    * Subtract an (const) integer value from the index held by this.
    *
    * @param i An integer value by which to decrement this
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator-=(const long i) {
      pos -= i;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator-=(Pointer<STYPE,ATYPE>& v)
    * @brief Subtract from this
    *
    * Subtract the value held by another Pointer from the 
    * index held by this.
    * 
    * @param v The other pointer to subtract
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator-=(Pointer<STYPE,ATYPE>& v) {
      pos -= v.pos;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator+=(const long i)
    * @brief Add to this
    *
    * Add an (const) integer value to the index held by this.
    *
    * @param i An integer value to add to this
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator+=(const long i) {
      pos += i;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator+=(Pointer<STYPE,ATYPE>& v)
    * @brief Add to this 
    *
    * Add the value held by another Pointer to the 
    * index held by this.
    *
    * @param v Another Pointer that has the same template signature.
    * @return this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator+=(Pointer<STYPE,ATYPE>& v) {
      pos += v.pos;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator*=(const long i)
    * @brief Multiply this
    *
    * Multiply the index held by this by an (const) integer value.
    *
    * @param i An integer value by which to multiply this
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator*=(const long i) {
      pos *= i;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator*=(Pointer<STYPE,ATYPE>& v)
    * @brief Multiply this
    *
    * Multiply the index held by this by another Pointer.
    *
    * @param v The other pointer by which to multiply
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator*=(Pointer<STYPE,ATYPE>& v) {
      pos *= v.pos;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator/=(const long i)
    * @brief Divide this
    *
    * Divide the index held by this by an (const) integer value.
    *
    * @param i An integer value by which to divide this
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator/=(const long i) {
      pos /= i;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator/=(Pointer<STYPE,ATYPE>& v)
    * @brief Divide this
    *
    * Divide the index held by this by another Pointer.
    *
    * @param v The other pointer by which to divide
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator/=(Pointer<STYPE,ATYPE>& v) {
      pos /= v.pos;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator++()
    * @brief Prefix increment this 
    *
    * Prefix increment the index held by this by one.
    *
    * @param None
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator++() {
      ++pos;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer Pointer::operator++(int)
    * @brief Postfix increment this
    *
    * Postfix increment the index held by this by one.
    *
    * @param None
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE> operator++(int) {
      Pointer<STYPE,ATYPE> wptr;
      wptr = *this;
      pos++;
      return(wptr);
    };

    /*==============================================================*/
    /** @fn Pointer& Pointer::operator--()
    * @brief Prefix decrement this
    *
    * Prefix decrement the index held by this by one.
    *
    * @param None
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE>& operator--() {
      --pos;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Pointer Pointer::operator--(int)
    * @brief Postfix decrement this
    *
    * Postfix decrement the index held by this by one.
    *
    * @param None
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE> operator--(int) {
      Pointer<STYPE,ATYPE> wptr;
      wptr = *this;
      pos--;
      return(wptr);
    };

    /*==============================================================*/
    /** @fn long Pointer::operator|(const Pointer<STYPE,ATYPE>& v)
    * @brief Calculate difference
    *
    * Calcluate the difference between the indexes of two pointers.
    *
    * @param v Another pointer for calculating the difference
    * @return A long integer value representing the difference
    */                                                             
    inline long operator|(const Pointer<STYPE,ATYPE>& v) {
       return(pos - v.pos);
    };

    /*==============================================================*/
    /** @fn Pointer Pointer::operator+(long v)
    * @brief Add an integer value
    * 
    * Add an (long) integer value to the index held by this.
    * 
    * @param v An integer value to add to this
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE> operator+(long v) {
      Pointer<STYPE,ATYPE> wptr;
      wptr = *this;
      wptr += v;
      return(wptr);
    };

    /*==============================================================*/
    /** @fn Pointer Pointer::operator-(long v)
    * @brief Subtract an integer value
    * 
    * Subtract an (long) integer value to the index held by this.
    * 
    * @param v An integer value to subtract from this
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE> operator-(long v) {
      Pointer<STYPE,ATYPE> wptr;
      wptr = *this;
      wptr -= v;
      return(wptr);
    };

    /*==============================================================*/
    /** @fn Pointer Pointer::operator*(long v)
    * @brief Multiply by an integer value
    * 
    * Multiply the index held by this by an (long) integer value.
    * 
    * @param v An integer value to by which to multiply
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE> operator*(long v) {
      Pointer<STYPE,ATYPE> wptr;
      wptr = *this;
      wptr *= v;
      return(wptr);
    };

    /*==============================================================*/
    /** @fn Pointer Pointer::operator/(long v)
    * @brief Divide by an integer value
    * 
    * Divide the index held by this by an (long) integer value.
    * 
    * @param v An integer value to by which to divide
    * @return A reference to this
    */                                                             
    inline Pointer<STYPE,ATYPE> operator/(long v) {
      Pointer<STYPE,ATYPE> wptr;
      wptr = *this;
      wptr /= v;
      return(wptr);
    };

    /*==============================================================*/
    /** @fn int Pointer::operator<(long v)
    * @brief Less than operator
    * 
    * Determine if the index held by this is less than a (long)
    * integer value.
    * 
    * @param v An integer value to compare to this
    * @return An integer indicating if this is less than v
    *   @retval !=0 this is less than v
    *   @retval ==0 this isn't less than v
    */                                                             
    inline int operator<(long v) {
      return(pos < v);
    };

    /*==============================================================*/
    /** @fn int Pointer::operator<(Pointer<STYPE,ATYPE>& p)
    * @brief Less than operator
    * 
    * Determine if the index held by this is less than another 
    * Pointer index.
    * 
    * @param p Another index to compare to this
    * @return An integer indicating if this is less than p
    *   @retval !=0 this is less than p
    *   @retval ==0 this isn't less than p
    */                                                             
    inline int operator<(Pointer<STYPE,ATYPE>& p) {
      return((pos < p.pos)&&(ptr == p.ptr));
    };

    /*==============================================================*/
    /** @fn int Pointer::operator>(long v)
    * @brief Greater than operator
    * 
    * Determine if the index held by this is greater than a (long)
    * integer value.
    * 
    * @param v An integer value to compare to this
    * @return An integer indicating if this is greater than v
    *   @retval !=0 this is greater than v
    *   @retval ==0 this isn't greater than v
    */                                                             
    inline int operator>(long v) {
      return(pos > v);
    };

    /*==============================================================*/
    /** @fn int Pointer::operator>(Pointer<STYPE,ATYPE>& p)
    * @brief Greater than operator
    * 
    * Determine if the index held by this is greater than another 
    * Pointer index.
    * 
    * @param p Another index to compare to this
    * @return An integer indicating if this is greater than p
    *   @retval !=0 this is greater than p
    *   @retval ==0 this isn't greater than p
    */                                                             
    inline int operator>(Pointer<STYPE,ATYPE>& p) {
      return((pos > p.pos)&&(ptr == p.ptr));
    };

    /*==============================================================*/
    /** @fn int Pointer::operator<=(long v)
    * @brief Less than or equal operator
    * 
    * Determine if the index held by this is less than or equal to 
    * a (long) integer value.
    * 
    * @param v An integer value to compare to this
    * @return An integer indicating if this is less than or equal to v
    *   @retval !=0 this is less than or equal to v
    *   @retval ==0 this isn't less than or equal to v
    */                                                             
    inline int operator<=(long v) {
      return(pos <= v);
    };

    /*==============================================================*/
    /** @fn int Pointer::operator<=(Pointer<STYPE,ATYPE>& p)
    * @brief Less than or equal operator
    * 
    * Determine if the index held by this is less than or equal to
    * another Pointer index.
    * 
    * @param p Another index to compare to this
    * @return An integer indicating if this is less than or equal to p
    *   @retval !=0 this is less than or equal to p
    *   @retval ==0 this isn't less than or equal to p
    */                                                             
    inline int operator<=(Pointer<STYPE,ATYPE>& p) {
      return((pos <= p.pos)&&(ptr == p.ptr));
    };

    /*==============================================================*/
    /** @fn int Pointer::operator>=(long v)
    * @brief Greater than or equal operator
    * 
    * Determine if the index held by this is greater than or equal to 
    * a (long) integer value.
    * 
    * @param v An integer value to compare to this
    * @return An integer indicating if this is greater than or equal to v
    *   @retval !=0 this is greater than or equal to v
    *   @retval ==0 this isn't greater than or equal to v
    */                                                             
    inline int operator>=(long v) {
      return(pos >= v);
    };

    /*==============================================================*/
    /** @fn int Pointer::operator>=(Pointer<STYPE,ATYPE>& p)
    * @brief Greater than or equal operator
    * 
    * Determine if the index held by this is greater than or equal to
    * another Pointer index.
    * 
    * @param p Another index to compare to this
    * @return An integer indicating if this is greater than or equal to p
    *   @retval !=0 this is greater than or equal to p
    *   @retval ==0 this isn't greater than or equal to p
    */                                                             
    inline int operator>=(Pointer<STYPE,ATYPE>& p) {
      return((pos >= p.pos)&&(ptr == p.ptr));
    };

    /*==============================================================*/
    /** @fn int Pointer::operator!=(long v)
    * @brief Not equal operator
    * 
    * Determine if the index held by this is not equal to 
    * a (long) integer value.
    * 
    * @param v An integer value to compare to this
    * @return An integer indicating if this is not equal to v
    *   @retval !=0 this is not equal to v
    *   @retval ==0 this is equal to v
    */                                                             
    inline int operator!=(long v) {
      return(pos != v);
    };

    /*==============================================================*/
    /** @fn int Pointer::operator!=(Pointer<STYPE,ATYPE>& p)
    * @brief Not equal operator
    * 
    * Determine if the index held by this is not equal to
    * another Pointer index.
    * 
    * @param p Another index to compare to this
    * @return An integer indicating if this is not equal to p
    *   @retval !=0 this is not equal to p
    *   @retval ==0 this is equal to p
    */                                                             
    inline int operator!=(Pointer<STYPE,ATYPE>& p) {
      return((pos != p.pos)||(ptr != p.ptr));
    };

    /*==============================================================*/
    /** @fn ATYPE& Pointer::operator*()
    * @brief Dereference
    *
    * Dereference this at the current index position.  That is,
    * return the value at the current index position held by this.
    *
    * @param None
    * @return A reference to the value at the index held by this
    * @throw DAQOutOfBounds If the current index is invalid
    */                                                             
    inline ATYPE& operator*() {
      static ATYPE garbage;

      if (ptr == NULL) {
        LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Pointer::operator*() Null pointer access"),DAQEXCPID(DAQOutOfBounds)));
        return(garbage);
      }
 
      return((*ptr)[pos]);
    };

    /*==============================================================*/
    /** @fn ATYPE& Pointer::operator[](const int& idx)
    * @brief Subscript access to this buffer
    *
    * This method provides subscript access to the data structure
    * underlying this pointer.  The data returned is from the 
    * underlying data structure at the index derived from 
    * adding this pointer's current position to the subscript
    * passed to this method.
    *
    * @param idx The position (starting at 0) to access.
    * @return The primitive value at the specified location
    * @throw DAQOutOfBounds If the current index is invalid
    */                                                             
    inline ATYPE& operator[] (const int& idx) {
      static ATYPE garbage;

      if (ptr == NULL) {
        LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Pointer::operator[](const int&) Null pointer access"),DAQEXCPID(DAQOutOfBounds)));
        return(garbage);
      }
 
      return((*ptr)[pos+idx]);
    }

    /*==============================================================*/
    /** @fn int Pointer::operator==(const Pointer<STYPE,ATYPE>& s) const
    * @brief Equal operator
    * 
    * Determine if the index held by this is equal to
    * another Pointer index.
    * 
    * @param p Another index to compare to this
    * @return An integer indicating if this is equal to s
    *   @retval !=0 this is equal to s
    *   @retval ==0 this is not equal to s
    */                                                             
    inline int operator== (const Pointer<STYPE,ATYPE>& s) const {
      return((s.ptr == ptr)&&(s.pos == pos));
    };

    /*==============================================================*/
    /** @fn int Pointer::operator==(const char *s) const
    * @brief Equal operator
    *
    * Equality operator (principally for comparing to NULL)
    *
    * @param s A pointer value to compare to this
    * @return An integer indicating if this is equal to s
    *   @retval !=0 this is equal to s
    *   @retval ==0 this is not equal to s
    */                                                             
    inline int operator== (const char *s) const {
      return((s == NULL)&&(ptr == NULL));
    };

    /*==============================================================*/
    /** @fn STYPE* Pointer::Get()
    * @brief Return the object to which this points.
    *
    * Return the underlying subscriptable object to which this points.
    * 
    * @param None
    * @return A pointer to the underlying object
    */                                                             
    STYPE *Get() {
      return(ptr);
    }; 
  
    /*==============================================================*/
    /** @fn int Pointer::GetIndex()
    * @brief Return pointer index
    *
    * Return the index position pointed to by this.
    *
    * @param None
    * @return The index position
    */                                                             
    int GetIndex() {
      return(pos);
    }; 
  
    /*==============================================================*/
    /** @fn void Pointer::Init(STYPE *s)
    * @brief Initialize
    *
    * Initialize this with a subscriptable object.
    *
    * @param s A pointer to a subscriptable object.
    * @return None
    */                                                             
    void Init(STYPE *s) {
      if (ptr != NULL) ptr->DereferenceBy(this); 
      ptr = s;
      pos = 0;
      if (ptr != NULL) ptr->ReferenceBy(this); 
    }; 

    /*==============================================================*/
  protected:
    /**
    * @var ptr
    * @brief A pointer to the underlying subscriptable object
    *
    * A pointer to the underlying subscriptable object pointed
    * to by this. 
    */
    STYPE *ptr;           

    /**
    * @var pos
    * @brief Current pointer index
    *
    * Current pointer index position.
    */
    int pos; 
};

