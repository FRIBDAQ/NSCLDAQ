// operator+(Pointer<>)
//                                                             
// Add a pointer to the pointer (add pos vars and cast as the left
// pointer -- that is, if the ptr vars aren't the same, interpret
// as the result belonging to the pointer on the left) 
//                                                             
template <class STYPE,class ATYPE>
inline Pointer<STYPE,ATYPE> operator+(const Pointer<STYPE,ATYPE>& v1,Pointer<STYPE,ATYPE>& v2) {
  Pointer<STYPE,ATYPE> p(v1);
  p += v2.GetIndex();
  return(p);
};

