#define ARRAYBADALLOC_MSGSIZE 1024

class ArrayBadAlloc : public std::exception {
  public:
    ArrayBadAlloc() {
      strcpy(msg,"ArrayBadAlloc");
    }

    const char* what() {
      return(msg);
    }

    ArrayBadAlloc& set_msg(const char *aMsg,int howbig) {
      if (aMsg == NULL) return(*this);
      snprintf(msg,ARRAYBADALLOC_MSGSIZE,"Bad array allocation: \"%s\". Size=%d.",aMsg,howbig);
      return(*this);
    }
  private:
    char msg[ARRAYBADALLOC_MSGSIZE+1];
};

