    /** @fn Dump(PrintStreamIface& aStream,int aCnt)
    * @brief Print the Array in hex and character.
    *
    * Print a specified number of Array values using a hex and
    * character representation.
    *
    * @param aStream The PrintStream.
    * @param aCnt The number of values to print.
    * @return None
    */                                                             
    void Dump(PrintStreamIface& aStream,int aCnt)
    {
      ios::fmtflags flags;
      unsigned char ch;
      unsigned short v;
      int i,j,cnt = 0,len;
      unsigned char *p,tmp[ARRAY_DUMP_CHARCOUNT+1];
   
      flags = aStream.flags();
      len = aCnt < this->scnt ? aCnt : this->scnt;
      p = (unsigned char *)this->vals;
      aStream.hex();
      aStream.width(8);
      aStream.fill('0');
      aStream.print(0); 
      aStream.print(": ");
    
      for (i = 0; i < len; i++) {
        if (cnt > ARRAY_DUMP_CHARCOUNT) {
          aStream.println();
          aStream.print("          ");
    
          for (j = 0; j < cnt; j++) {
            ch = tmp[j];
    
            if (isprint(ch)&&(!iscntrl(ch))) aStream.printf(" %c",ch);
            else aStream.printf("  ");
          }
   
          aStream.println();
          aStream.hex();
          aStream.width(8);
          aStream.fill('0');
          aStream.printf("%d: ",i);
          cnt = 0;
        }
  
        v = (*p);  
        aStream.hex();
        aStream.width(2);
        aStream.fill('0');
        aStream.print(v);
        tmp[cnt] = (*p);
        cnt++; p++;
      }
    
      if (cnt > 0) {
        aStream.println();
        aStream.print("          ");
      }

      for (j = 0; j < cnt; j++) {
        ch = tmp[j];
    
        if (isprint(ch)&&(!iscntrl(ch))) aStream.printf(" %c",ch);
        else aStream.printf("  ");
      }
   
      aStream.println();
      aStream.flags(flags);
      aStream.flush();
    }

