    unsigned int GetMask() {
      return(routemask);
    }

    /*==============================================================*/
  protected:
    /*==============================================================*/
    /** @fn Init(int l)
    * @brief Initialize this buffer
    *
    * Initialize this buffer.  This is usually called by a constructor
    * method.  A size in primitive units (BTYPE) can be specified.
    * 
    * @param l A size in primitive units
    * @return None 
    */                                                             
    void Init(int l) {
      LinkedList<BLKTYPE> *lst = NULL;

      routetag = 0;
      originip = 0;
      routemask = ALLBITS_MASK;
      cmax = 0;
      rlen = 0;
      scnt = 0;
      lastidx = -1; 
      lastblk = NULL;

      if (l > 0) {
        lst = Request(l); 
        if (lst != NULL) {
          blocks.Append(*lst);
          cmax = CountList(blocks);
          if (cmax < l) rlen = cmax;
          else rlen = l;
          delete lst;
        }
      }
    }

    /*==============================================================*/
    /** @fn ExpandToPageSize() 
    * @brief Expand this buffer's size to the current maximum.
    *
    * Expand this buffer's size to the current maximum.  The 
    * current maximum is determined by the number of blocks
    * acquired to fullfil the user's requested buffer size.
    * This is an internal utility method.
    *
    * @parm None
    * @return The current size
    */                                                             
    int ExpandToPageSize() {
      rlen = cmax;
      return(rlen);
    }

    /*==============================================================*/
    /** @fn Nullify()
    * @brief Nullify all pointers to this buffer.
    *
    * Nullify all pointers to this buffer.  That is, if there are
    * any Pointer references to this buffer they should be updated
    * such that they point to no object.  This is an internal
    * utility method.
    *
    * @param None
    * @return None
    */                                                             
    void Nullify() {
//      Pointer<Buffer<ALLOC,BLKTYPE,BTYPE>,BTYPE> *p;
      BasicPointer *p;

      p = bag.GetFirst();
      while(!bag.Bottom()) {
        p->Nullify(); 
        p = bag.GetNext();
      }
 
      bag.Empty();
    }

    /*==============================================================*/
    /** @fn CountList(LinkedList(BLKTYPE>& lst)
    * @brief Calculate the length of a list of blocks.
    *
    * Calculate the length of a list of blocks, and set the block
    * Positions. This is an internal utility function.
    *
    * @param lst The list of blocks to process
    * @return The calculated length.
    */                                                             
    int CountList(LinkedList<BLKTYPE>& lst) {
      int s; 
      int cnt;
      unsigned int thisid;
      BLKTYPE *bwpre = NULL,*bwlast = NULL,*bw = NULL;  

      s = 0; cnt = 0;
      bw = lst.GetFirst();
      bwpre = NULL; bwlast = NULL;
      if (bw != NULL) thisid = bw->GetObjId();

      while (!lst.Bottom()) {
        s += bw->GetLen();
        bw->SetNeverDirty();  // Change for PAGEPHASE
        bw->SetObjId(thisid);
        bw->SetPos(cnt);
        bw->SetInUse();
        bw->SetTag(routetag);
        if (bwpre != NULL) bwpre->ResetLastPage();
        if ((bwlast == NULL)&&(s >= rlen)) bwlast = bw;
        cnt++;
        bwpre = bw;
        bw = lst.GetNext();
      }

      if (bwlast != NULL) bwlast->SetLastPage();

      return(s);
    }

    /*==============================================================*/
    /**
    * @var routetag
    * @brief Buffer routing tag
    *
    * The routing tag used to Route() this buffer
    */
    unsigned int routetag; 

    /**
    * @var routemask
    * @brief Buffer routing mask
    *
    * The routing mask used to Route() this buffer
    */
    unsigned int routemask;

    /**
    * @var originip
    * @brief Buffer origin IP
    *
    * The buffer origin IP used to Route() this buffer
    */
    unsigned int originip; 

    /**
    * @var rlen
    * @brief Requested length of this buffer
    *
    * The requested length of this buffer in primitive units
    */
    int rlen;    

    /**
    * @var cmax 
    * @brief Total length of this buffer
    *
    * The total length of this buffer in primitive units
    */
    int cmax;              // Total length of buffer

    /**
    * @var scnt
    * @brief Slots used in this buffer
    *
    * The number of slots used in this buffer in primitive units
    */
    int scnt;              // Spaces used

    /**
    * @var lastblk
    * @brief Last block of the buffer blocks
    *
    * The last block in the chain of blocks that make up this buffer
    */
    BLKTYPE *lastblk;      

    /**
    * @var lastidx
    * @brief Last block index of the buffer blocks
    *
    * The last block index in the chain of blocks that make up this buffer
    */
    int lastidx;          

    /**
    * @var lastval
    * @brief Last used value
    *
    * Last used value for checking dirty and other internal uses
    */
    BTYPE lastval;    

    /**
    * @var blocks
    * @brief Chain of blocks
    *
    * The chain of blocks that makes up this buffer.
    */
    LinkedList<BLKTYPE> blocks;

    /**
    * @var bag
    * @brief Pointer reference bag
    *
    * Kind of morbid, eh.  Just a bag to keep track of pointer
    * references to this buffer.
    */
    PointerBag<BasicPointer> bag; 

    /*==============================================================*/
  private:
      Pointer<Buffer<ALLOC,BLKTYPE,BTYPE>,BTYPE> ptr;
