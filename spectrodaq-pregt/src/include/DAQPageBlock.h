    /** @fn CopyToThis(const DAQPageBlock<BTYPE>& blk)
    * @brief Copy a DAQPageBlock to this one.
    *
    * Copy a different DAQPageBlock to this one.  Simply
    * points to other page block's data and sets the reference count
    * appropriately.
    *
    * @param blk The other DAQPageBlock.
    * @return None
    */                                                             
    DAQPageBlock<BTYPE>& CopyToThis(const DAQPageBlock<BTYPE>& blk) {
      CheckReference();

      DAQBufferPage::CopyToThis(blk);

      url = blk.url;

      data = blk.data;
      len = blk.len; 

      refcnt = blk.refcnt;                 // There can be only one
      refcnt++;                            // Increment new counter;

      return(*this);
    }

