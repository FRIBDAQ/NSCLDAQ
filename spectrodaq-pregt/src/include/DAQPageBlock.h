    void ReleaseData() {
      SetRelease(); 
      ResetInUse(); 
      data = NULL;
      len = 0;
    }

    /*==============================================================*/
    /** @fn CheckReference()
    * @brief Check the reference count prior to deleting.
    *
    * Check the reference count for this page block and delete the
    * underlying data structures if it has gone zero.  Prevents
    * deletion of data as long as the data is in use.
    *
    * @param None
    * @return None
    */                                                             
    void CheckReference() {
      refcnt.CheckReference();

      if (refcnt() <= 0) {
        ReleaseData();
      }
    }

