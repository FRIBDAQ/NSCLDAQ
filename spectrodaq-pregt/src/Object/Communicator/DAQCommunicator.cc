/** @fn int DAQCommunicator::SendMsg(DAQCommMsgIface& msg,int cnt)
* @brief Send a message as a DAQ communicator message.
*
* Send a DAQ communicator message as a message.
* Allows the specification of the number of bytes to be sent.
*
* @param msg A DAQ communicator message.
* @param cnt The number of bytes to send.
* @return The number of bytes sent.
*/
int DAQCommunicator::SendMsg(DAQCommMsgIface& msg,int cnt)
{
  char s[MSGHEAD_BYTE_LENGTH+1];
  int rc;
  int l;

  if (commstate != DAQCOMM_CONNECTED) {
//    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::SendMsg() Attempt to Send when not connected"),DAQEXCPID(DAQComm)));
    return(-1);
  }

  sync_begin(writesync);
    msg.GetMsgHeader(s);  // Make sure the header info has been updated
  
    if (msg.head.len < 1) {
      return(0);
    }
  
    if (cnt < msg.head.len) {
      l = cnt;
    } else {
      l = msg.head.len;
    }
  
    try {
      msg.ResizeData(l);
    } catch (...) {
      Close();
      daq_logger.printf("DAQCommunicator::SendMsg() Could not resize data. %s\n",DAQCSTR(""));
      return(-1);
    } 
  
    xusecount++;  // Increment the use count
  
    if (msg.head.marker != DAQCOMMMSG_MARKER) {
      daq_logger.printf("DAQCommunicator::SendMsg() Sending message with invalid message header. %s\n",DAQCSTR(""));
    }
  
    if ((rc = netio_write(net_funcs,fd,msg.GetMsgHeader(s),msg.GetMsgHeaderLen(),&cli)) < msg.GetMsgHeaderLen()) {
      Close();
   
      if (rc == 0) { // EOF
        return(0);
      } else {
        if (rc < 0) errno = -rc;
        LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::SendMsg() Write error")));
        return(-1);
      }
    }
  
    xoctets += rc;
  
    if ((rc = netio_write(net_funcs,fd,(char*)(msg.GetData()),l,&cli)) < l) {
      Close();
  
      if ((rc == 0)||(errno == EPIPE)) {  // EOF
        return(0);
      } else {
        if (rc < 0) errno = -rc;
        LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::SendMsg() Write error")));
        return(-1);
      }
    }
  
    xoctets += rc;
  sync_end; // writesync
  return(l);
}

