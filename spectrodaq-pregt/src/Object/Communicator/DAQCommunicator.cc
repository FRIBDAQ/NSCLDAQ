/** @fn bool DAQCommunicator::CanRecv()
* @brief Check if this communicator can receive.
*
* Check if this communicator can immediately receive a message 
* without blocking.
*
* @param None
* @return If the communicator is ready to receive.
*/
bool DAQCommunicator::CanRecv()
{
  int rc = 0;

  if (commstate != DAQCOMM_CONNECTED) {
    return(false);
  }

  sync_begin(readsync);
    rc = netio_isreadable(net_funcs,fd); 
  sync_end;

  if (rc > 0) return(true);
  else return(false);
}

/*===================================================================*/
/** @fn bool DAQCommunicator::CanSend()
* @brief Check if this communicator can send.
*
* Check if this communicator can immediately send a message 
* without blocking.
*
* @param None
* @return If the communicator is ready to send.
*/
bool DAQCommunicator::CanSend()
{
  int rc = 0;

  if (commstate != DAQCOMM_CONNECTED) {
    return(false);
  }

  sync_begin(writesync);
    rc = netio_iswritable(net_funcs,fd);
  sync_end;

  if (rc > 0) return(true);
  else return(false);
}

