/** @fn DAQCommunicator& DAQCommunicator::operator=(const DAQCommunicator& com)
* @brief Copy another communicator to this one.
*
* Copy another communicator to this one (assignment operator).
*
* @param com The communicator to duplicate.
* @return A reference to this communicator.
*/
DAQCommunicator& DAQCommunicator::operator=(const DAQCommunicator& com)
{
  sync_begin(readsync);
  sync_begin(writesync);
    CopyToThis(com);
  sync_end;
  sync_end;
  return(*this);
}

