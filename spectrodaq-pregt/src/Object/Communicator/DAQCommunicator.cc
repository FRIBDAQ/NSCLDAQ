/** @fn void DAQCommunicator::Connect(DAQURL& aurl,DAQCommInfo& info)
* @brief Connect to a remote host.
*
* Connect to a remote host.
*
* @param aurl The URL of the remote host.
* @param info Information about this communicator (currently unused).
* @return None
*/
void DAQCommunicator::Connect(DAQURL& aurl,DAQCommInfo& info)
{
  if (commstate != DAQCOMM_CLOSED) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::Connect() Attempt to Connect when not closed"),DAQEXCPID(DAQComm)));
  }

  parms.nolinger = 0;      // Linger on close
  parms.reuseaddr = 0;     // Don't reuse this address
  parms.nodelay  = 1;      // Don't delay
  parms.nonblock = 0;      // Block

  try {
    url = aurl;
  } catch(DAQException& e) {
    commstate = DAQCOMM_CLOSED;
    THROW(e);
    return;
  }

  origport = url.GetPort();
  port = origport;

  if ((net_funcs = netio_setproto(url.GetProto())) == NULL) {
    commstate = DAQCOMM_CLOSED;
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::Connect() Failed to set protocol"),DAQEXCPID(DAQComm)));
    return;
  }

  if ((fd = netio_open(net_funcs,(char*)(url.GetIPStr().Get()),(char*)(url.GetHostName().Get()),port,&srv,&cli,parms)) < 0) {
    commstate = DAQCOMM_CLOSED;
    LOG(("OSException(DAQCommunicator::Connect()): Failed to open socket with URL \"%s\" %s",url.Get().Get(),DAQCSTR("")));
    errno = -fd; // Reset the errno just in case
    LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::Connect() Failed to open socket")));
    return;
  }

  if (refcnt.IsNull()) refcnt = 1;
  else ++refcnt;         

  sable.fd = fd;
  commstate = DAQCOMM_CONNECTED;
}

