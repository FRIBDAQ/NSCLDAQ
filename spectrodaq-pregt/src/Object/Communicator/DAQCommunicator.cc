/** @fn DAQCommunicator& DAQCommunicator::CopyToThis(const DAQCommunicator& com)
* @brief Copy another communicator to this one.
*
* Copy another communicator to this one.
*
* @param com The communicator to duplicate.
* @return A reference to this communicator.
*/
DAQCommunicator& DAQCommunicator::CopyToThis(const DAQCommunicator& com)
{
  Close();

  sable = com.sable;
  accfd = com.accfd;
  memcpy((char *)&srv,(char *)&com.srv,sizeof(sockaddr_netio));
  memcpy((char *)&cli,(char *)&com.cli,sizeof(sockaddr_netio));
  memcpy((char *)&parms,(char *)&com.parms,sizeof(netio_parms));
  commstate = com.commstate;
  port = com.port;
  origport = com.origport;
  url = com.url;
  net_funcs = com.net_funcs;

  rusecount = com.rusecount;
  xusecount = com.xusecount;
  roctets= com.roctets;
  xoctets = com.xoctets;

  mode = com.mode;

  netio_buffer_copy(&nbuffer,&(com.nbuffer));

  fd = com.fd; 
  refcnt = com.refcnt;                     // There can be only one
  if (!refcnt.IsNull()) ++refcnt;          // Increment new counter;

  return(*this);
}

