/** @fn void DAQCommunicator::Open(DAQURL& aurl,DAQCommInfo& info)
* @brief Open this communicator.
*
* Open this communicator.  Must be called before Accept().
*
* @param aurl The URL indicating binding information (port).
* @param info Information about the communicator (currently unused).
* @return None
*/
void DAQCommunicator::Open(DAQURL& aurl,DAQCommInfo& info)
{
  if (commstate != DAQCOMM_CLOSED) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::Open() Attempt to Open when not closed"),DAQEXCPID(DAQComm)));
  }

  parms.nolinger = 0;      // Linger on close
  parms.reuseaddr = 1;     // Reuse this address
  parms.nodelay  = 1;      // Don't delay
  parms.nonblock = 0;      // Block

  try {
    url = aurl;
  } catch(DAQException& e) {
    commstate = DAQCOMM_CLOSED;
    THROW(e);
    return;
  }

  origport = url.GetPort();
  port = origport;

  if ((net_funcs = netio_setproto(url.GetProto())) == NULL) {
    commstate = DAQCOMM_CLOSED;
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::Open() Failed to set protocol"),DAQEXCPID(DAQComm)));
    return;
  }

  if ((fd = netio_servinit(net_funcs,&port,&srv,parms)) < 0) {
    commstate = DAQCOMM_CLOSED;
    LOG(("OSException(DAQCommunicator::Open()): Failed to initialize server with URL \"%s\"",url.Get().Get()));
    LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::Open() Failed to initialize server")));
    return;
  } 

  if (refcnt.IsNull()) refcnt = 1;
  else ++refcnt;         

  sable.fd = fd; 
  url.SetPort(port);
  commstate = DAQCOMM_OPEN;
}

/*===================================================================*/
/** @fn void DAQCommunicator::Close()
* @brief Close this communicator.
*
* Close this communicator. 
*
* @param bool If the communicator should be locked during this operation.
* @return None
*/
void DAQCommunicator::Close()
{
  if (commstate == DAQCOMM_CLOSED) return;

  if (!refcnt.IsNull()) { 
    --refcnt;
    if (refcnt() <= 0) {
      netio_close(net_funcs,fd);
    }
  }

  fd = -1;
  sable.fd = -1; 
  accfd = -1;
  rusecount = 0;
  xusecount = 0;
  roctets = 0;
  xoctets = 0;
  url.SetPort(origport);
  netio_buffer_init(&nbuffer);

  commstate = DAQCOMM_CLOSED;
}

