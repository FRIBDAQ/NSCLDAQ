/** @fn DAQCommunicator::DAQCommunicator()
* @brief Default constructor.
*
* Default constructor.
*
* @param None
* @return this
*/
DAQCommunicator::DAQCommunicator() 
{
  SetType(DAQTYPEID(DAQCommunicator)); 

  parms.sndbuf_size = -1;  // Use default 
  parms.rcvbuf_size = -1;  // Use default
  parms.nolinger = 0;      // Linger on close
  parms.reuseaddr = 1;     // Reuse this address
  parms.nodelay  = 1;      // Don't delay
  parms.nonblock = 0;      // Block

  fd = -1;
  sable.fd = -1; 
  accfd = -1;
  port = 0;
  origport = 0;
  mode = DAQCOMM_MODE_MSG;
  commstate = DAQCOMM_CLOSED;
  bzero((char *)&cli,sizeof(struct sockaddr));
  bzero((char *)&srv,sizeof(struct sockaddr));

  rusecount = 0;
  xusecount = 0;
  roctets = 0;
  xoctets = 0;

  netio_buffer_init(&nbuffer);

  signal(SIGPIPE,SIG_IGN);
}

