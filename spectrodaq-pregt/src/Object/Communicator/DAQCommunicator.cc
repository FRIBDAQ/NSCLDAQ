/** @fn int DAQCommunicator::RecvMsg(DAQCommMsgIface& msg,int cnt)
* @brief Receive a message.
*
* Receive a DAQ communicator message as a message.
*
* @param msg A DAQ communicator message.
* @param cnt The number of bytes to receive.
* @return The number of bytes received.
*/
int DAQCommunicator::RecvMsg(DAQCommMsgIface& msg,int cnt)
{
  int rc;
  int l,l2;
  char wrk[MSGHEAD_BYTE_LENGTH+1];
  DAQMemory workmem;

  if (cnt < 1) {
    return(0);
  }

  if (cnt > __daqcommunicator_maxmsg) {
    daq_logger.printf("DAQCommunicator::RecvMsg() Requested message of size=%d when current maximum is %d %s\n",l,__daqcommunicator_maxmsg,DAQCSTR(""));
    return(-1);
  }

  if (commstate != DAQCOMM_CONNECTED) {
//    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::RecvMsg() Attempt to Recv when not connected"),DAQEXCPID(DAQComm)));
    return(-1);
  }

  sync_begin(readsync);
    rusecount++;  // Increment the use count
  
    if ((rc = netio_readall(net_funcs,fd,wrk,msg.GetMsgHeaderLen(),&nbuffer,&cli)) < msg.GetMsgHeaderLen()) {
      Close();
  
      if (rc == 0) { // EOF
        return(0);
      } else {
        if (rc < 0) errno = -rc;
        LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::RecvMsg() Read error")));
        return(-1);
      } 
    }
  
    roctets += rc;
  
    if (!msg.SetMsgHeader(wrk)) {
      daq_logger.printf("DAQCommunicator::RecvMsg() Could not set message header for msg received on fd=%d return_code=%d %s\n",fd,rc,DAQCSTR(""));
      Close();
      LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::RecvMsg() Could not set message header"),DAQEXCPID(DAQInval)));
      return(-1);
    }
  
    l = l2 = msg.head.len;
    l = cnt < l ? cnt : l;
  
    if (l > __daqcommunicator_maxmsg) {
      daq_logger.printf("DAQCommunicator::RecvMsg() Attempted to receive a message of size=%d when current maximum is %d %s\n",l,__daqcommunicator_maxmsg,DAQCSTR(""));
      Close();
      LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::RecvMsg() Recving message larger than current maximum allowed."),DAQEXCPID(DAQInval)));
      return(-1);
    }
  
    workmem.Resize(l); 
    if (workmem.length < l) l = workmem.length; // Couldn't resize
  
    if (l <= 0) {
      Close();
      LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::RecvMsg() Null message received")));
      return(-1);
    }
  
    if ((rc = netio_readall(net_funcs,fd,workmem.data,l,&nbuffer,&cli)) < l) {
      Close();
  
      if (rc == 0) { // EOF
        return(0);
      } else { 
        if (rc < 0) {
          if (rc < 0) errno = -rc;
        } else {
          msg.SetData(workmem.data,rc);   
          daq_logger.printf("DAQCommunicator::RecvMsg() Read error on msg with tag=%d.  Expected byte count = %d, received byte count = %d %s\n",msg.GetMsgTag(),l,rc,DAQCSTR(""));
        }
  
        LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::RecvMsg() Read error")));
        return(-1);
      }
    }
  
    msg.SetData(workmem.data,rc);
    roctets += rc;
  
    if (l < l2) {
      try {
        workmem.Resize(l2-l+2);
      } catch(...) {
        Close();
        daq_logger.printf("DAQCommunicator::RecvMsg() Could not resize data. %s\n",DAQCSTR(""));
        return(-1);  // Incomplete transactions are potential problems
      }
      if ((rc = netio_readall(net_funcs,fd,workmem.data,l2-l,&nbuffer,&cli)) < 0) {
        Close();
        return(-1);  // Incomplete transactions are potential problems
      }
      roctets += rc;
    }
  sync_end; // readsync
  return(l);
}

