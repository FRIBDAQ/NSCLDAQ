/** @fn void DAQCommunicator::flush()
* @brief Flush this stream.
*
* Flush this stream.
*
* @param None
* @return None
*/                                                                  
void DAQCommunicator::flush() {
  char *p;
  DAQCommMsg msg;
  sync_begin(writesync);
    mychararray.Append('\0');
    p = (char*)(mychararray.Get());
    msg = p;
    Send(msg);
    mychararray.Set('\0');
  sync_end;
}

