/** @fn short DAQCommunicator::Poll(short aEvents,int aTimeOut)
* @brief Poll this communicator.
*
* Poll this communicator for input (POLLIN) or output (POLLOUT) events.
* This calls the netio library poll().  Events typically correspond
* to those specified for the system poll(2) command. 
*
* @param aEvents Events to pool for.
* @param aTimeOut Time out in milliseconds.
* @return The number of events detected.
*/
short DAQCommunicator::Poll(short aEvents,int aTimeOut)
{
  int rc = 0;

  if (commstate == DAQCOMM_CLOSED) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::Poll() Attempt to Poll when not open"),DAQEXCPID(DAQComm)));
  }

  sync_begin(writesync);
  sync_begin(readsync);
    rc = netio_poll(net_funcs,fd,aEvents,aTimeOut);
  sync_end;
  sync_end;

  return(rc);
}

