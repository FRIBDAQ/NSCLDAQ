/** @fn int DAQCommunicator::RecvTelnet(DAQCommMsgIface& msg,int cnt)
* @brief Receive telnet data as a message.
*
* Receive telnet data as a message.
*
* @param msg A DAQ communicator message.
* @param cnt The maximum number of bytes to receive.
* @return The number of bytes received.
*/
int DAQCommunicator::RecvTelnet(DAQCommMsgIface& msg,int cnt)
{
  char *p = NULL;
  int l,cc,siz;

  l = RecvRawUntil(msg,cnt,'\n');

  // If we got a message then strip out the special telnet
  // characters.  In this case, the easiest way is to simply
  // turn the special characters into blanks.
  if (l > 0) {
    p = (char*)(msg.GetData());
    siz = cc = msg.GetDataLength();
    while (cc > 0) {
      switch((unsigned char)*p) {
        case IAC: // Get rid of the telnet options
          if (cc >= 3) { // Replace with blanks 
            cc -= 3; siz -= 3;
            memmove(p,p+3,cc);
          }
          break;
        case '\0': // Remove if not at the end of the msg
          if (cc > 1) {
            (*p) = ' '; p++; cc--;
          }
          break;
        case '\n': // Remove if at the end of msg
        case '\r':
          if (cc < 3) {
            (*p) = '\0'; p++; siz -= cc; cc--; 
          }
          break;
        default: 
          p++; cc--;
          break;
      }
    }

    try {
      msg.ResizeData(siz+1);
    } catch(...) {
      daq_logger.printf("DAQCommunicator::RecvTelnet() Could not resize data. %s\n",DAQCSTR(""));
      return(-1);
    }
  }

  return(l);
}

