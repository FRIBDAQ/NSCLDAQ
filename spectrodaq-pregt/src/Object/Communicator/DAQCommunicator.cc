/** @fn DAQCommunicator DAQCommunicator::Accept(DAQCommInfo& info)
* @brief Accept client connections.
*
* Server-side accept client connections.  Returns a new communicator.
*
* @param info Information about this communicator (currently unused).
* @return A new comunicator.
*/
DAQCommunicator DAQCommunicator::Accept(DAQCommInfo& info)
{
  DAQCommunicator work_dcom; 
  char wstr[NETIO_MAX_PROTO_STRING+1];
  DAQString u;

  if (commstate != DAQCOMM_OPEN) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQCommunicator::Accept() Attempt to Accept when not open"),DAQEXCPID(DAQComm)));
  }

  sync_begin(readsync);
    commstate = DAQCOMM_ACCEPTING;
  
    if ((accfd = netio_accept(net_funcs,fd,&cli,parms)) < 0) {
      // I don't set the state to closed here because accept is notorious for
      // exiting with an error because of reentry from a signal.  This
      // really isn't fatal, but is annoying.
      commstate = DAQCOMM_OPEN;
//      LOG(("OSException(DAQCommunicator::Accept()): Failed accept for URL \"%s\" %s",url.Get().Get(),DAQCSTR("")));
//      LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQCommunicator::Accept() Failed to accept")));
      return(work_dcom);
    } else {
      work_dcom.fd = accfd; 
      work_dcom.sable.fd = accfd; 
      work_dcom.accfd = -1; 
      memcpy((char *)&work_dcom.srv,(char *)&cli,sizeof(srv));
      memcpy((char *)&work_dcom.cli,(char *)&srv,sizeof(cli));
      memcpy((char *)&work_dcom.parms,(char *)&parms,sizeof(netio_parms));
      work_dcom.commstate = DAQCOMM_CONNECTED;
      netio_buffer_init(&(work_dcom.nbuffer));
    
      if (url.GetSockDomain() == AF_INET) {
        work_dcom.port = ntohs(((struct sockaddr_in *)&cli)->sin_port);
      } else {
        work_dcom.port = url.GetPort();
      }
  
      work_dcom.origport = work_dcom.port;
      work_dcom.net_funcs = net_funcs; 
      
      work_dcom.refcnt = 1;
  
      netio_prototostr(url.GetProto(),wstr);
      u = wstr;
      u += "://";
      if (url.GetSockDomain() == AF_INET) {
        u += inet_ntoa(((struct sockaddr_in *)&cli)->sin_addr);
      } else {
        u += "127.0.0.1";
      }
  
      u += ":"; 
      snprintf(wstr,NETIO_MAX_PROTO_STRING,"%d",work_dcom.port);
      u += wstr; 
      u += "/";
   
      work_dcom.url = u;
      work_dcom.xusecount = 0;
      work_dcom.rusecount = 0;
      work_dcom.roctets = 0;
      work_dcom.xoctets = 0;
    }
  
    commstate = DAQCOMM_OPEN;
  sync_end; // readsync
  return(work_dcom);
}

