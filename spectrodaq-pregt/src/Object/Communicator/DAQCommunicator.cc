/** @fn void DAQCommunicator::write(const char *s,int n)
* @brief Write data on this communicator.
*
* Write data on this communicator.
*
* @param s The data to write.
* @param n The number of characters to write.
* @return None
*/                                                                  
void DAQCommunicator::write(const char *s,int n) {
  try {
    int len = (n >= 0) ? n : 0;
    if ((len > 0)&&(s != NULL)) write(s,len);
    flush();
  } catch(std::exception &excp) {
    THROW(excp);
  }
}

