// DAQNode::Join  
//               
// Wait for a process to exit.  Returns the nodeid of this process
// if there's an error.
//                           
DAQNodeId DAQNode::Join(DAQNodeId& aId,DAQStatus *ep)
{
   int rcode = 0;
   int myerrno = 0;
   pid_t rc;
   union wait status; 

   if (!nodeid.Peers(aId)) {
     // Would be nice to add code that allowed for Joining with
     // Nodes on other machines. 

     if (ep != NULL) ep->UpdateStatus("DAQNode::Join() NodeId specified isn't local to this machine, cannot Join()",-1,"Not a Peer");

     LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQNode::Join() NodeId specified isn't local to this machine, cannot Join()")));
     return(nodeid);
   }

   signal(SIGCHLD,SIG_DFL);   // Reset the signal so we get it 

   if ((rc = waitpid(aId.location.val.pid,(int *)&status,WNOHANG)) <= 0) {
     if (errno == ECHILD) {  // The child already exited 
       if (ep != NULL) {
         if (WIFEXITED(status)) rcode =  WEXITSTATUS(status); // Exited normally
         else rcode = -1;   // Didn't exit normally

         ep->UpdateStatus("DAQNode::Join() Node has already exited or is unavailable",rcode,"Exited");
       } 

       signal(SIGCHLD,reapchild_func);
       return(aId);
     }
   }

   while((rc = wait3(&status,0,(struct rusage *)0)) >= 0) {
     if (rc == aId.location.val.pid) {
       if (ep != NULL) {
         if (WIFEXITED(status)) rcode =  WEXITSTATUS(status); // Exited normally
         else rcode = -1;   // Didn't exit normally

         ep->UpdateStatus("DAQNode::Join() Node has exited",rcode,"Exited");
       }
  
       break;
     }
   }

   myerrno = errno;

   signal(SIGCHLD,reapchild_func);  // Set the sig handler back

   if ((rc < 0)&&(myerrno == ECHILD)) { 
     if (ep != NULL) {
       ep->UpdateStatus("DAQNode::Join() No child processes available",-ECHILD,"No Children");
     }
   } else if (rc < 0) {
     LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQNode::Join() wait3 failure")));
     return(nodeid);
   }

   return(aId);
}

/*===================================================================*/
// DAQNode::Join  
//               
// Wait for any process to exit.  Returns either the NodeId of the
// process that exited or the NodeId of this process if an error
// occured.  
//                           
DAQNodeId DAQNode::Join(DAQStatus *ep)
{
   DAQNodeId aId;
   int rcode = 0;
   int myerrno = 0;
   pid_t rc;
   union wait status; 

   signal(SIGCHLD,SIG_DFL);   // Reset the signal so we get it 

   while((rc = wait3(&status,0,(struct rusage *)0)) >= 0) {
     if (rc >= 0) {
       aId.location.val.pid = rc;

       if (ep != NULL) {
         if (WIFEXITED(status)) rcode =  WEXITSTATUS(status); // Exited normally
         else rcode = -1;   // Didn't exit normally

         ep->UpdateStatus("DAQNode::Join() Node has exited",rcode,"Exited");
       }

       break;
     }
   }

   myerrno = errno;

   signal(SIGCHLD,reapchild_func);  // Set the sig handler back

   if ((rc < 0)&&(myerrno == ECHILD)) { 
     if (ep != NULL) {
       ep->UpdateStatus("DAQNode::Join() No child processes available",0,"No Children");
     }
   } else if (rc < 0) {
     LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("DAQNode::Join() wait3 failure")));
     return(nodeid);
   }

   return(aId);
}

/*===================================================================*/
// DAQNode::Join  
//               
// Wait for a thread to exit.  Return
//                           
DAQThreadId DAQNode::Join(DAQThreadId& aThreadId,DAQStatus *aStatus)
{
   DAQThreadId tid;
   int rc;

   rc = __spectrodaq_daqthread_join(aThreadId,aStatus);
   errno = rc;
  
   if (rc != 0) return(tid);
   else return(aThreadId);
}

