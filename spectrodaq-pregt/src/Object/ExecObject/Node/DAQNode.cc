// DAQNode::Detach 
//                
// Detach (daemonize) process.
//                           
int DAQNode::Detach(bool close_cout,bool close_cerr,bool close_cin)
{
  if (initialized) {
    setsid();
    umask(0);
    setpgrp();

#ifdef SIGTTOU
    signal(SIGTTOU,SIG_IGN); 
#endif
#ifdef SIGTTIN 
    signal(SIGTTIN,SIG_IGN); 
#endif
#ifdef SIGTSTP 
    signal(SIGTSTP,SIG_IGN); 
#endif
#ifdef SIGHUP
    signal(SIGHUP,SIG_IGN); 
#endif

    if (close_cin) close(0); 
    if (close_cout) close(1); 
    if (close_cerr) close(2); 
  } else {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("DAQNode::Detach() Node has not been initialized; cannot detach"),DAQEXCPID(DAQOperation)));
    return(EPERM);
  }

  return(0);
}

