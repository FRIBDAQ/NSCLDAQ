// reentrant_gethostname
//                    
// Thread safe version of gethostname 
//                              
static Synchronizable reentrant_gethostname_sync; 
int reentrant_gethostname(char *hn,size_t l)
{
  sync_begin(reentrant_gethostname_sync);
    int rc = 0; 
    rc = gethostname(hn,l);
    return(rc);
  sync_end;
}

/*===================================================================*/
// reentrant_gethostbyname
//                    
// Thread safe version of gethostbyname 
//                              
static Synchronizable reentrant_gethostbyXXX_sync; 
struct hostent *reentrant_gethostbyname(struct hostent *php,const char *nm)
{
  sync_begin(reentrant_gethostbyXXX_sync);
    struct hostent *hp = NULL;
    if (php == NULL) return(php); 
    hp = gethostbyname(nm);
    if (hp != NULL) memcpy(php,hp,sizeof(struct hostent));
    return(php);
  sync_end;
}

/*===================================================================*/
// reentrant_gethostbyaddr
//                    
// Thread safe version of gethostbyaddr
//                              
struct hostent *reentrant_gethostbyaddr(struct hostent *php,const char *addr, int l, int t)
{
  sync_begin(reentrant_gethostbyXXX_sync);
    struct hostent *hp = NULL;
    if (php == NULL) return(php); 
    hp = gethostbyaddr(addr,l,t);
    if (hp != NULL) memcpy(php,hp,sizeof(struct hostent));
    return(php);
  sync_end;
}

