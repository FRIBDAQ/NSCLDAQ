/** @fn DAQString::DAQString()
* @brief Default constructor.
*
* Default constructor.
*
* @param None
* @return this
*/                                                                  
DAQString::DAQString()
{
  SetType(DAQTYPEID(DAQString)); 
}

/*===================================================================*/
/** @fn DAQString::DAQString(const char *s)
* @brief Constructor with char*.
*
* Constructor with char*.
*
* @param s A NULL terminated array of characters.
* @return this
*/                                                                  
DAQString::DAQString(const char *s) 
{
  SetType(DAQTYPEID(DAQString)); 
  CopyToThis(s);
}

/*===================================================================*/
/** @fn DAQString::DAQString(const char *s,int l)
* @brief Constructor with char* and a size.
*
* Constructor with char* and a size.
*
* @param s An array of characters.
* @param l Number of characters in s.
* @return this
*/                                                                  
DAQString::DAQString(const char *s,int l) 
{
  SetType(DAQTYPEID(DAQString)); 
  if (l > 0) Set(s,l);
}

/*===================================================================*/
/** @fn DAQString::DAQString(const DAQString& s)
* @brief Copy constructor.
*
* Copy constructor.
*
* @param s A string to copy.
* @return this
*/                                                                  
DAQString::DAQString(const DAQString& s) 
{
  SetType(DAQTYPEID(DAQString)); 
  CopyToThis(s);
}

/*===================================================================*/
