/** @fn void DAQString::ReferenceBy(BasicPointer *p)
* @brief Tell this Array about a Pointer reference.
*
* Add a Pointer to this strings's bag of pointers.  This bag
* collects all pseudo-Pointer objects such that descruction
* of this string properly dereferences the pointers pointing
* to it.
*
* @param  p A Pointer to this string.
* @return None
*/                                                             
void DAQString::ReferenceBy(BasicPointer *p) {
  bag.Add(p);
}

/*==============================================================*/
/** @fn void DAQString::DereferenceBy(BasicPointer *p)
* @brief Dereference a Pointer that points to this string.
*
* Remove a Pointer reference from this strings's bag of pointers.
* Effectively dereferences the pointer.
*
* @param p A Pointer to this string.
* @return None
*/                                                             
void DAQString::DereferenceBy(BasicPointer *p) {
  bag.Delete(p);
}

/*==============================================================*/
/** @fn void DAQString::Nullify()
* @brief Nullify all the Pointers to this array.
*
* Nullify all the Pointers to this array.
*
* @param None
* @return None
*/                                                             
void DAQString::Nullify() {
  BasicPointer *p;

  p = bag.GetFirst();
  while(!bag.Bottom()) {
    p->Nullify(); 
    p = bag.GetNext();
  }
 
  bag.Empty();
}

