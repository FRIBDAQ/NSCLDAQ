#define DAQSTRING_SET(TYP) void DAQString::Set(const TYP aVal) { \
  ostringstream mystrstream; \
  mystrstream.width(myios.width()); \
  mystrstream.precision(myios.precision()); \
  mystrstream.flags(myios.flags()); \
  mystrstream << aVal; \
  mychararray.Set(mystrstream.str().c_str(),mystrstream.str().size()); }

