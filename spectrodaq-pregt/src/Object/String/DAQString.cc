/** @fn void DAQString::Dump(PrintStreamIface& aStream)
* @brief Print this string in hex and character.
*
* Print this string in hex and character representation.
*
* @param aStream The output stream.
* @return None
*/                                                                  
void DAQString::Dump(PrintStreamIface& aStream) {
  ios::fmtflags flags;
  unsigned char ch;
  unsigned short v;
  int i = 0,j,cnt = 0,len;
  len = str().size(); 
  unsigned char *vals = (unsigned char*)(mychararray.Get());
  unsigned char *p,tmp[STRING_DUMP_CHARCOUNT+1];
   
  flags = aStream.flags();
  p = (unsigned char *)vals;
  aStream.hex();
  aStream.width(8);
  aStream.fill('0');
  aStream.print(i);
  aStream.print(": ");
    
  for (i = 0; i < len; i++) {
    if (cnt > STRING_DUMP_CHARCOUNT) {
      aStream.println();
      aStream.print("          ");
    
      for (j = 0; j < cnt; j++) {
        ch = tmp[j];
        if (isprint(ch)&&(!iscntrl(ch))) {
          aStream.print(" ");
          aStream.print((const char)ch);
        } else {
          aStream.print("  ");
        }
      }
  
      aStream.println(); 
      aStream.hex();
      aStream.width(8);
      aStream.fill('0');
      aStream.print(i);
      aStream.print(": ");
      cnt = 0;
    }
  
    v = (*p);  
    aStream.hex();
    aStream.width(2);
    aStream.fill('0');
    aStream.print(v);
    tmp[cnt] = (*p);
    cnt++; p++;
  }
    
  if (cnt > 0) {
    aStream.println(); 
    aStream.print("          ");
  } 

  for (j = 0; j < cnt; j++) {
    ch = tmp[j];
    if (isprint(ch)&&(!iscntrl(ch))) {
      aStream.print(" ");
      aStream.print((const char)ch);
    } else {
      aStream.print("  ");
    }
  }
   
  aStream.println();
  aStream.flags(flags);
  aStream.flush();
}                                         

