/** @fn PrintStreamIface& PrintStream::format(const char *fmat,...)
* @brief Print a formated message.
*
* Print a formated message.
*
* @param fmat The format string.
* @return A reference to this.
*/
PrintStreamIface& PrintStream::format(const char *fmat,...)
{
  int l = strlen(fmat);
  char txt[MAX_PRINTLINE_LENGTH+1]; 
  txt[0] = '\0';
  va_list alst;      
  va_start(alst,fmat);
  vsnprintf(txt,MAX_PRINTLINE_LENGTH,fmat,alst);
  va_end(alst);                                                        
  mychararray.Append(txt,strlen(txt));
  if (l > 0) if (fmat[l-1] == '\n') flush();
  return(*this);
}

