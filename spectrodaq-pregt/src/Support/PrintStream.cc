/** @fn PrintStreamIface& PrintStream::append(const string& v)
* @brief Append a string to this print stream.
*
* Append a string to this print stream.
*
* @param v The string to print.
* @return A reference to this.
*/
PrintStreamIface& PrintStream::append(const string& v) {
  ostringstream mystrstream;
  mystrstream.width(myios.width());
  mystrstream.precision(myios.precision());
  mystrstream.flags(myios.flags());
  mystrstream.fill(fillchar);
  mystrstream << v;
  mychararray.Append(mystrstream.str().c_str(),mystrstream.str().size());
  return(*this);
}

