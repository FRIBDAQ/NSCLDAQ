#include <unistd.h>

/*=====================================================================*/
/**
* @class BagItem
* @brief Template class for defining bag items.
*
* Template class for defining bag items that will later be stored
* in a Bag.  The DAQOBJ template parameter defines the type of
* object that will be encapsulated in a BagItem.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class DAQOBJ>
class BagItem {
  public:
    /*==============================================================*/
    /** @fn BagItem(DAQOBJ& b)
    * @brief Constructor.
    *
    * BagItem Constructor with an object to encapsulate. 
    *
    * @param b The object to encapsulate in this BagItem.
    * @return None
    */                                                             
    BagItem(DAQOBJ& b) { // Constructor
      myitem = b;        // Objects need to have operator=
      nxt = NULL; prv = NULL;
    }; 

    /*==============================================================*/
    /** @fn ~BagItem()
    * @brief Destructor.
    *
    * Destructor.
    *
    * @param None
    * @return None
    */                                                             
    ~BagItem() {};  // Destructor

    /*==============================================================*/
    /** @fn Get()
    * @brief Return the encapsulated object.
    *
    * Return the object encapsulated by this BagItem.
    *
    * @param None
    * @return The encapsulated object.
    */                                                             
    DAQOBJ& Get() {return(myitem);}; // Get item encapsulated by this item 

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next BagItem following this one.
    *
    * Get the next BagItem following this one.  BagItems are 
    * chained together as a linked list in a Bag such that they
    * can be easily stored and manipulated.
    *
    * @param None
    * @return The next BatItem following this one or NULL.
    */                                                             
    BagItem<DAQOBJ> *GetNext() {return(nxt);};  // Get next bag item

    /*==============================================================*/
    /** @fn GetPrev()
    * @brief Get the previous BagItem following this one.
    *
    * Get the previous BagItem prior to this one.  BagItems are 
    * chained together as a linked list in a Bag such that they
    * can be easily stored and manipulated.
    *
    * @param None
    * @return The pervious BatItem prior to this one or NULL.
    */                                                             
    BagItem<DAQOBJ> *GetPrev() {return(prv);};  // Get prev bag item

    /*==============================================================*/
    /** @fn SetNext(BagItem<DAQOBJ> *b)
    * @brief Set the next BagItem.  
    *
    * Set the next BagItem to a specified BagItem. BagItems are 
    * chained together as a linked list in a Bag such that they
    * can be easily stored and manipulated.
    *
    * @param b A BagItem to point to next.
    * @return None
    */                                                             
    void SetNext(BagItem<DAQOBJ> *b) {nxt = b;};  // Set the next bag item

    /*==============================================================*/
    /** @fn SetPrev(BagItem<DAQOBJ> *b)
    * @brief Set the previous BagItem.  
    *
    * Set the previous BagItem to a specified BagItem. BagItems are 
    * chained together as a linked list in a Bag such that they
    * can be easily stored and manipulated.
    *
    * @param b A BagItem to point to point at prior.
    * @return None
    */                                                             
    void SetPrev(BagItem<DAQOBJ> *b) {prv = b;};   // Set the prev bag item

    /*==============================================================*/
    /** @fn operator=(const BagItem<DAQOBJ>& b)
    * @brief Assignment of one BagItem to this one.
    *
    * Assign a different BagItem to this one.
    *
    * @param b A BagItem to assign to this one.
    * @return A reference to this BagItem.
    */                                                             
    BagItem<DAQOBJ>& operator=(const BagItem<DAQOBJ>& b) {
      myitem = b.myitem;
      nxt = b.nxt;
      prv = b.prv;
      return(*this); 
    };

  protected:
    /*==============================================================*/
    /** @fn BagItem()
    * @brief Default constructor.
    *
    * Default constructor.
    *
    * @param None
    * @return None
    */                                                             
    BagItem() { };  // Default constructor

  private:
    /**
    * @var myitem
    * @brief Object held by this BagItem.
    *
    * Object encapsulated by this BagItem.
    */
    DAQOBJ  myitem;  

    /**
    * @var nxt
    * @brief Next BagItem.
    *
    * Pointer to the next BagItem following this one.
    */
    BagItem<DAQOBJ> *nxt; 

    /**
    * @var prv
    * @brief Previous BagItem
    *
    * Pointer to the previous BagItem prior to this one.
    */
    BagItem<DAQOBJ> *prv; 
};

/*=====================================================================*/
/**
* @class Bag
* @brief Template class defining a bag object.
* 
* Template class definition for general bag object.                 
* This object acts as a bag for other objects.  No order is assumed.
* Class takes the type of items to kept in the bag as an argument. 
* 
* NOTE: the items placed in the bag must support the = (assignment)
* and the == (equality) operators!
*
* The BITEM template parameter is a bag item defined using the
* BagItem template class.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class BITEM>
class Bag {
  public:
    /*==============================================================*/
    /** @fn Bag()
    * @brief Default constructor.
    *
    * Default constructor.
    *
    * @param None
    * @return None
    */                                                             
    Bag() {qtail = NULL; qhead = NULL; cur = NULL; scnt = 0; usecur = false;}; 

    /*==============================================================*/
    /** @fn Empty()
    * @brief Empty the Bag.
    *
    * Set the bag to an empty state.
    *
    * @param None
    * @return None
    */                                                             
    void Empty() {
       BagItem<BITEM> *i,*i2;
      
       i = qhead; 
       while (i != NULL) {
         i2 = i->GetNext(); 
         delete i;
         i = i2;
       }

       usecur = false;
       cur = qhead = qtail = NULL;
       scnt = 0;
    };
  
    /*==============================================================*/
    /** @fn ~Bag()
    * @brief Destructor.
    *
    * Destructor.  Empties the Bag.
    * 
    * @param None
    * @return None
    */                                                             
    ~Bag() {
       Empty();
     };

    /*==============================================================*/
    /** @fn Add(BITEM& nitm)
    * @brief Add an item to the Bag.
    *
    * Add an item (BagItem) to the Bag.
    *
    * @param nitm The BagItem to add to this Bag.
    * @return If the addition was successful.
    *   @retval true The addition was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Add(BITEM& nitm) {
      BagItem<BITEM> *itm;
      itm  = new BagItem<BITEM>(nitm);

      if (itm != NULL) {   
        if (qtail == NULL) {
          qhead = itm;
          qtail = itm; 
          itm->SetNext(NULL);
          itm->SetPrev(NULL);
        } else {
          itm->SetNext(qhead);
          itm->SetPrev(NULL);
          qhead->SetPrev(itm); 
          qhead = itm;
        }
        scnt++;
        return(true);
      }
      return(false);
    };

    /*==============================================================*/
    /** @fn Delete(BITEM& itm)
    * @brief Delete an item from this bag.
    *
    * Delete an item (BagItem) from this Bag.
    *
    * @param itm The BagItem to delete from this Bag.
    * @return If the deletion was successful.
    *   @retval true The deletion was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Delete(BITEM& itm) {
      BagItem<BITEM> *bit,*wrk,*pbit;

      if (qhead != NULL) {
        if (cur != NULL) {
          if (cur->Get() == itm) {
            cur = cur->GetNext();
            usecur = true;
          }
        }

        if (qhead->Get() == itm) {
          wrk = qhead; 
          qhead = qhead->GetNext();
          if (qhead != NULL) qhead->SetPrev(NULL);
          scnt--;
          delete wrk;
          if (qhead == NULL) qtail = NULL;
          return(true);
        } else if (qtail->Get() == itm) {
          wrk = qtail; 
          qtail = qtail->GetPrev();
          if (qtail != NULL) qtail->SetNext(NULL); 
          scnt--;
          delete wrk;
          return(true);
        } else {
          bit = qhead;
          while (bit != NULL) {
            if (bit->Get() == itm) {
              wrk = bit->GetNext();
              pbit = bit->GetPrev(); 
              if (pbit != NULL) pbit->SetNext(wrk);
              if (wrk != NULL) wrk->SetPrev(pbit);
              scnt--;
              delete bit;
              return(true);
            } 
            bit = bit->GetNext();
          }
        }
      } 
      return(false);
    };

    /*==============================================================*/
    /** @fn IsEmpty()
    * @brief Check if the Bag is empty.
    *
    * Check if the Bag is empty.
    *
    * @param None
    * @return If the Bag is empty.
    *   @retval true If the Bag is empty.
    *   @retval false Otherwise.
    */                                                             
    bool IsEmpty() {if (scnt < 1) return(true); else return(false);};
  
    /*==============================================================*/
    /** @fn Size()
    * @brief Return the size of this bag.
    *
    * Return the number of items (BagItem) stored in this Bag.
    *
    * @param None
    * @return The number of items in this Bag.
    */                                                             
    int Size() {return(scnt);};
  
    /*==============================================================*/
    /** @fn GetFirst()
    * @brief Get the first item in the Bag.
    *
    * Although no guarantee is maintained about the order of
    * items in a Bag, a Bag maintains an implicit order such that
    * it is possible to iterate through the contained items.
    * This method returns the so-called first item in the bag and
    * needs to be called prior to invoking GetNext().
    *
    * @param None
    * @return The first BagItem in the Bag.
    */                                                             
    BITEM& GetFirst() {
      static BITEM garbage;
      if (qhead == NULL) {
        cur = NULL;
        return(garbage);
      }

      cur = qhead;
      return(cur->Get());
    };

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next item in the Bag.
    *
    * Although no guarantee is maintained about the order of
    * items in a Bag, a Bag maintains an implicit order such that
    * it is possible to iterate through the contained items.
    * This method returns the so-called next item in the bag. 
    * GetFirst() should be called prior to calling GetNext() for
    * the first time.
    *
    * @param None
    * @return The next BagItem in the Bag.
    */                                                             
    BITEM& GetNext() {
      static BITEM garbage;

      if (qhead == NULL) {
        cur = NULL;
        return(garbage);
      }

      if (usecur) {
        usecur = false;
        if (cur == NULL) cur = qhead;
      } else {                                                                  
        if (cur != NULL) cur = cur->GetNext();
        if (cur == NULL) cur = qhead;
      }                                                                         
                                                                                
      if (cur != NULL) return(cur->Get());
      else return(garbage);
    };

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the next item in the Bag.
    *
    * Although no guarantee is maintained about the order of
    * items in a Bag, a Bag maintains an implicit order such that
    * it is possible to iterate through the contained items.
    * This method returns the so-called next item in the bag. 
    * GetFirst() should be called prior to calling GetNext() for
    * the first time.  This is the prefix operator.
    *
    * @param None
    * @return The next BagItem in the Bag.
    */                                                             
    BITEM& operator++() {
      return(GetNext());
    };

    /*==============================================================*/
    /** @fn operator++(int)
    * @brief Get the next item in the Bag.
    *
    * Although no guarantee is maintained about the order of
    * items in a Bag, a Bag maintains an implicit order such that
    * it is possible to iterate through the contained items.
    * This method returns the so-called next item in the bag. 
    * GetFirst() should be called prior to calling GetNext() for
    * the first time.  This is the postfix operator.
    *
    * @param None
    * @return The next BagItem in the Bag.
    */                                                             
    BITEM& operator++(int) {
      return(GetNext());
    };

    /*==============================================================*/
    /** @fn Bottom()
    * @brief Check if the bag bottom has been reached.
    *
    * Check if the bottom of a bag has been reached.  This is
    * typically checked while iterating using GetNext().  The bottom
    * of the bag is reached when all items have been processed.
    * This method simply checks to see if the current pointer is NULL,
    * thus it will return true if GetFirst() has never been called.
    *
    * @param None
    * @return If the bag bottom has been reached.
    *   @retval true At the bottom of the bag.
    *   @retval false Otherwise.
    */                                                             
    bool Bottom() {
      if (cur == NULL) return(true);
      if (usecur) return(false);
      return(cur->GetNext() == NULL);
    };
    
    /*==============================================================*/
    /** @fn Dump(PrintStreamIface& aStream)
    * @brief Dump the Bag contents.
    *
    * Print the Bag contents on a PrintStream.  Essentially, a
    * diagnostic method.
    *
    * @param aStream The PrintStream.
    * @return None
    */                                                             
    void Dump(PrintStreamIface& aStream) {
      int cnt;
      ios::fmtflags flags;
      BagItem<BITEM> *bit;

      flags = aStream.flags();
      bit = qhead;

      cnt = 1; 
      aStream.printf("There are %d items in the bag",scnt);
      while(bit != NULL) {
        aStream.dec();
        aStream.width(8);
        aStream.fill(' ');
        aStream.printf("%d: ",cnt);
        aStream.print("BagItem=");
        aStream.print(bit);
        aStream.print(" Next=");
        aStream.print(bit->GetNext());
        aStream.println();
        cnt++; 
        bit = bit->GetNext();
      }

      aStream.flags(flags);
      aStream.flush();
    };

    /*==============================================================*/
  private:
    /**
    * @var qhead
    * @brief Queue head.
    *
    * A Bag is represented as a simple queue.  This variable is the
    * head of the queue.
    */
    BagItem<BITEM> *qhead;

    /**
    * @var qtail
    * @brief Queue tail.
    *
    * A Bag is represented as a simple queue.  This variable is the
    * tail of the queue.
    */
    BagItem<BITEM> *qtail;

    /**
    * @var cur
    * @brief Current bag item.
    *
    * Pointer to the current bag item being processed by iteration.  
    * Used by the GetFirst() and GetNext() methods.
    */
    BagItem<BITEM> *cur; 

    /**
    * @var scnt
    * @brief Number of bag items.
    *
    * Number of BagItems stored in this Bag.
    */
    int scnt;        

    /**
    * @var usecur
    * @brief True if current item should be used.
    *
    * True if current item should be returned by GetNext().
    * Special test to handle the head of the queue.
    */
    bool usecur;      
};

