/** @fn CleanPage(DAQBufferPage *pPage)
* @brief Clean a buffer page.
*
* Clean a buffer page.  That is, reset the headers to an
* initialized, but neutral unused state.
*
* @param pPage The page to clean.
* @return None
*/                                                             
void DAQBufferPager::CleanPage(DAQBufferPage *pPage)
{
  if (pPage != NULL) {
    pPage->SetRelease();
    pPage->SetOwner(0);
    pPage->SetUser(0);
    pPage->SetObjId(0);
    pPage->SetTag(0);
    pPage->SetPos(0);
    pPage->ResetDirty();
    pPage->SetNeverDirty();
    pPage->ResetOOB();
    pPage->ResetModified();
    pPage->ResetReadOnly();
    pPage->ResetXmit();
    pPage->ResetInUse();
    pPage->SetLastPage();
    pPage->SetClockTick(0);
    pPage->ResetNode();
    // SECURE - Hmmm, maybe we should zero the page data here for
    // security reasons?  It may be a little intensive...
  }
}

