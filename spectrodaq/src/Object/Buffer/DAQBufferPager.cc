/** @fn IsHighTide()
* @brief Check if the pager is in high tide.
*
* Acquiring a buffer page usually requires that the page pool 
* has not exceeded its high water mark.  In a high tide 
* condition, only SpectroDAQ system processes should be allowed 
* to acquire pages under certain circumstances to help ensure 
* that the scheduler can continue to process pages.  This
* method checks if the pager is in a high tide state.
*
* @param None
* @return If the pager is in high tide.
*   @retval true If the pager is in high tide.
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::IsHighTide()
{
  int fcnt = 0;

  if (warntide) return(true);

  fcnt = GetFreeCount();
  if (hightide) {
    if (fcnt >= lowwater) hightide = false;
  } else if (fcnt <= highwater) {
    hightide = true;
  }

  return(hightide);
}

