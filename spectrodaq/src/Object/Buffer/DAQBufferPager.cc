/** @fn GetPage(bool NoLimit,bool UseWork)
* @brief Get a page from the page pool.
*
* Get a page from the page pool.  Acquiring a buffer page
* usually requires that the page pool has not exceeded its
* high water mark.  In a high tide condition, only SpectroDAQ
* system processes should be allowed to acquire pages under
* certain circumstances to help ensure that the scheduler
* can continue to process pages.
*
* @param NoLimit Specify if high water marks should be ignored.
* @param UseWork Specify if the acquired page should be added to the work list.
* @return A buffer page.
*   @retval A buffer page pointer.
*   @retval Null if not pages are available or watermarks are exceeded.
*/                                                             
DAQBufferPage *DAQBufferPager::GetPage(bool NoLimit,bool UseWork)
{
  DAQBufferPage *pPage = NULL;
  int fcnt = 0;

  objmutex.Lock();

  // Test the low and high water marks to see if we should get
  // pages for this request.
  if (!NoLimit) {
    if (warntide) {
      objmutex.UnLock();
      return(NULL);
    }

    fcnt = GetFreeCount();
    if (hightide) {
      if (fcnt < lowwater) {
        objmutex.UnLock();
        return(NULL);
      } else {
        hightide = false;
      }
    } else if (fcnt <= highwater) {
      hightide = true;
      objmutex.UnLock();
      return(NULL);
    }
  }   

  // Now go get the pages.
  pPage = freelist.GetFirst();
  if (pPage == NULL) {
    objmutex.UnLock();
    return(NULL);
  }

  pPage->ResetRelease();

  bufferclock++;
  pPage->SetObjId(bufferclock);
  pPage->SetLastPage();

  freelist.Delete(pPage);

  if (UseWork) {
    worklist.AppendUniq(pPage);
  } else if (freezeused) {
    tempusedlist.AppendUniq(pPage);
  } else {
    usedlist.AppendUniq(pPage);
  }

  objmutex.UnLock();
  return(pPage);
}

