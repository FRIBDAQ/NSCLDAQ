/** @fn ReleasePage(DAQBufferPage *pPage,bool lockme)
* @brief Release a buffer page by pointer.
*
* Release a buffer page.  Note that this is a short circuit release.
* That is, the release is based on using the specified pointer as the
* search key.  If you want to release based on what the page is rather
* than a pointer, then you want to use one of the other ReleasePage
* methods.
*
* @param pPage A pointer to the page to release.
* @param lockme If an attempt to lock the pager should be made.
* @return If the release was successful.
*   @retval true If the release was successful.
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::ReleasePage(DAQBufferPage *pPage,bool lockme)
{
  if (pPage == NULL) return(false);

  if (lockme) objmutex.Lock();

  CleanPage(pPage);

  if (freezeused) {
    tempdellist.AppendUniq(pPage); 
    if (lockme) objmutex.UnLock();
    return(true);   // Should we really return true?
  }

  if (usedlist.Delete(pPage)) {
     freelist.AppendUniq(pPage);
  } 

  if (lockme) objmutex.UnLock();
  return(true);
}

/*==============================================================*/
/** @fn ReleasePages(DAQPageLinkedList& aPList)
* @brief Release a list of buffer pages.
*
* Release a list of buffer pages.  Note that this expects a list of
* page pointers.
* 
* @param aPList A list of page pointers.
* @return If the release was successful.
*   @retval true If the release was successful.
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::ReleasePages(DAQPageLinkedList& aPList)
{
  DAQBufferPage *pPage = NULL;

  pPage = aPList.GetFirst();
  while (pPage != NULL) {
    aPList.Delete(pPage);
    ReleasePage(pPage,true);
    pPage = aPList.GetNext();
  } 

  return(true);
}

