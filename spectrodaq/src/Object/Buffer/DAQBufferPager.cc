/** @fn ReleasePage(pid_t pid,long shmkey,long shmsiz,long pagestart,long pagelength)
* @brief Release a buffer page.
*
* Release a buffer page.  The page is identified by a set of
* specified characteristics and not simply by a pointer to the
* page.
*
* @param pid The process id associated with the target page.
* @param shmkey The target page's shared memory key.
* @param shmsiz The target page's shared memory segment size.
* @param pagestart The target page's page start.
* @param pageslength The target page's page length.
* @return If the release was successful.
*   @retval true If the release was successful.
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::ReleasePage(pid_t pid,long shmkey,long shmsiz,long pagestart,long pagelength)
{
  DAQBufferPage *pPage = NULL;

  objmutex.Lock();

  pPage = usedlist.GetFirst();
  while (!usedlist.Bottom()) {
    if (pPage != NULL) {
      if ((pPage->GetStart() == pagestart)&&
            (pPage->GetLength() == pagelength)&&
            (pPage->GetSeg().GetKey() == shmkey)&&
            (pPage->GetSeg().GetSegSize() == shmsiz)) {
          ReleasePage(pPage,false);
          objmutex.UnLock();
          return(true);
      }
    }
    pPage = usedlist.GetNext();
  }
  
  objmutex.UnLock();
  return(false);
}

