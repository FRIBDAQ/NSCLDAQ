/** @fn GetWorkPage(bool NoLimit)
* @brief Get a work page.
*
* Work pages are pages that are used for temporary manipulation
* of pages and should not be processed by the scheduler.
* This method returns a page from the page pool and places
* the page in the work list.  Acquiring a buffer page
* usually requires that the page pool has not exceeded its
* high water mark.  In a high tide condition, only SpectroDAQ
* system processes should be allowed to acquire pages under
* certain circumstances to help ensure that the scheduler
* can continue to process pages.
*
* @param NoLimit True if the high water marks should be ignored.
* @return A buffer page.
*   @retval A buffer page pointer.
*   @retval Null if not pages are available or watermarks are exceeded.
*/                                                             
DAQBufferPage *DAQBufferPager::GetWorkPage(bool NoLimit)
{
  return(GetPage(NoLimit,true));
}

