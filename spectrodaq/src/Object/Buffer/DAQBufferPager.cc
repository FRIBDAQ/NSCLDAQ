/** @fn Work2Used(DAQBufferPage *pPage)
* @brief Move a work page to the used list.
*
* Work pages are pages that are used for temporary manipulation
* of pages and should not be processed by the scheduler.
* The method converts a work page into a used page that will
* be processed by the scheduler.
*
* @param pPage The work page to convert.
* @return If the converion was successful.
*   @retval true If the conversion was successful.
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::Work2Used(DAQBufferPage *pPage)
{
  bool rc = true;

  objmutex.Lock();

  if (worklist.Delete(pPage)) {
    if (freezeused) {
      tempusedlist.AppendUniq(pPage);
    } else {
      usedlist.AppendUniq(pPage);
    }
  } 

  objmutex.UnLock();

  return(rc);
}

