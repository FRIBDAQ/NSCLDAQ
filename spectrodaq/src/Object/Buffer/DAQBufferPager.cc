/** @fn DAQBufferPager(bool aWS)
* @brief Constructor.
*
* Constructor where the default world access can be overridden.
* World access is by default false, however, this may inhibit
* client processes that are not members of the right Unix group from
* acquiring access to the shared memory segment used for building
* buffer pages.
*
* @param aWS Specify if world access should be granted.
* @return this
*/                                                             
DAQBufferPager::DAQBufferPager(bool aWS) 
{
  SetType(DAQTYPEID(DAQBufferPager)); 
  pagesize = DEFAULTPAGESIZE + sizeof(DAQBufferPageHeader);
  freezeused = 0; 
  bufferclock = 0;
  h2ocount = 0;
  lowwater = 0;
  highwater = 0;
  pagecount = 0;
  requesttick = 0;
  maxrequest = H2O_MINPAGEREQ;
  hightide = false;
  warntide = false;
  worldaccess = aWS;
}

/*==============================================================*/
/** @fn DAQBufferPager(long aSize,bool aWS)
* @brief Constructor with page size.
*
* Constructor where the page size can specified and the
* default world access can be overridden.
* World access is by default false, however, this may inhibit
* client processes that are not members of the right Unix group from
* acquiring access to the shared memory segment used for building
* buffer pages.
*
* @param aSize Desired page size.
* @param aWS Specify if world access should be granted.
* @return this
*/                                                             
DAQBufferPager::DAQBufferPager(long aSize,bool aWS) 
{
  SetType(DAQTYPEID(DAQBufferPager)); 
  
  if (aSize > 0) pagesize = aSize + sizeof(DAQBufferPageHeader);
  else pagesize = DEFAULTPAGESIZE + sizeof(DAQBufferPageHeader);

  freezeused = 0; 
  bufferclock = 0; 
  h2ocount = 0; 
  lowwater = 0;
  highwater = 0;
  pagecount = 0;
  requesttick = 0;
  maxrequest = H2O_MINPAGEREQ;
  hightide = false;
  warntide = false;
  worldaccess = aWS;
}

/*==============================================================*/
/** @fn ~DAQBufferPage()
* @brief Destructor.
*
* Destroy this pager.
*
* @param None
* @return None
*/                                                             
DAQBufferPager::~DAQBufferPager()
{
  DAQBufferPage *pPage = NULL;
  DAQShmseg *pSeg = NULL;

  pPage = freelist.GetFirst();
  while (!freelist.Bottom()) {
    if (pPage != NULL) {
      freelist.Delete(pPage);
      delete pPage;
    }
    pPage = freelist.GetNext();
  }

  pPage = usedlist.GetFirst();
  while (!usedlist.Bottom()) {
    if (pPage != NULL) {
      usedlist.Delete(pPage);
      delete pPage;
    }
    pPage = usedlist.GetNext();
  }

  pPage = tempusedlist.GetFirst();
  while (!tempusedlist.Bottom()) {
    if (pPage != NULL) {
      tempusedlist.Delete(pPage);
      delete pPage;
    }
    pPage = tempusedlist.GetNext();
  }

  pSeg = seglist.GetFirst();
  while (!seglist.Bottom()) {
    if (pSeg != NULL) {
      seglist.Delete(pSeg);
      pSeg->ForceDestroy();  // Pager's gone, force destruction
      delete pSeg;
    }
    pSeg = seglist.GetNext();
  }

  tempdellist.Empty(); 
  tempusedlist.Empty();
  usedlist.Empty();
  freelist.Empty();
  seglist.Empty();
}

