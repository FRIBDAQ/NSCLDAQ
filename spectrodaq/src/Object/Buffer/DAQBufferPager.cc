/** @fn AddSeg(long aSize)
* @brief Add a shared memory segment to this pager.
*
* Add a shared memory segment to this pager.  The page size
* may be specified as a parameter.  
*
* @param aSize Desired page size.   
* @return If the segment addition is successful.
*   @retval true If segment additions was successful. 
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::AddSeg(long aSize)
{
  DAQShmseg *pSeg = NULL;

  objmutex.Lock();

  if (aSize <= 0) {
    objmutex.UnLock();
    return(false);
  } 

  pSeg = new DAQShmseg(aSize,worldaccess);
  pSeg->Destroy(true);
  seglist.Append(pSeg);

  Populate(*pSeg);

  objmutex.UnLock();

  return(true);
}

