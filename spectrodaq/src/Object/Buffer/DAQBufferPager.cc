/** @fn CalcWaterMarks()
* @brief Calculate the high and low water marks.
*
* Acquiring a buffer page usually requires that the page pool 
* has not exceeded its high water mark.  In a high tide 
* condition, only SpectroDAQ system processes should be allowed 
* to acquire pages under certain circumstances to help ensure 
* that the scheduler can continue to process pages.  This
* method calculates the high and low water marks from the
* independent water mark statistic.
*
* @param None
* @return None
*/                                                             
void DAQBufferPager::CalcWaterMarks()
{
  highwater = (h2ocount + ((h2ocount ? 1 : 0) * H2O_INBOVERHEAD)) * maxrequest; 
  highwater = (highwater <= (pagecount/3)) ? highwater : (pagecount/3);
  lowwater = 0;
  if (highwater > 0) {
    lowwater = highwater + (highwater/2);
    lowwater = (lowwater < (highwater+maxrequest)) ? (highwater+maxrequest) : lowwater; 
    lowwater = (lowwater <= (pagecount/2)) ? lowwater : (pagecount/2);
  }
}

