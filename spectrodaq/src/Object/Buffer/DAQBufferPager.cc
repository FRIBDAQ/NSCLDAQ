/*==============================================================*/
/** @fn ThawUsedList()
* @brief Freeze the used list.
* 
* The used list can be frozen so some scheduling activities can 
* be completed without the used list changing.  This method
* implements a method for thawing the used list.  Note that
* the used list can be frozen multiple times and is only
* thawed after an equal number of calls to ThawUsedList().
* Once the used list is thawed, the temporary used and deletion
* lists are resynchronized with their nontemporary counterparts.
*
* @param None
* @return None
*/                                                             
void DAQBufferPager::ThawUsedList()
{
  DAQBufferPage *pPage = NULL;

  objmutex.Lock();

  if (freezeused) freezeused--;

  if (!freezeused) {
    usedlist.AppendUniq(tempusedlist);
    tempusedlist.Empty();

    pPage = tempdellist.GetFirst();
    while (!tempdellist.Bottom()) {
      if (pPage != NULL) {
        CleanPage(pPage);
        usedlist.Delete(pPage);
        freelist.AppendUniq(pPage);
      }
      pPage = tempdellist.GetNext();
    }

    tempdellist.Empty();
  }

  objmutex.UnLock();
}

