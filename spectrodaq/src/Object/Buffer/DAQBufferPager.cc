/** @fn ReleasePage(pid_t pid,DAQBufferPage& aPage)
* @brief Release a buffer page.
*
* Release a buffer page.  The page is identified by a set of
* specified characteristics and not simply by a pointer to the
* page.  Note that this release depends on the shared memory key,
* shared memory segment size, page start and page length specified
* as part of the aPage parameter for comparison. 
*
* @param pid The process id associated with the target page.
* @param aPage A page that has the same characteristics as the one to delete.
* @return If the release was successful.
*   @retval true If the release was successful.
*   @retval false Otherwise.
*/                                                             
bool DAQBufferPager::ReleasePage(pid_t pid,DAQBufferPage& aPage)
{
  return ReleasePage(pid,aPage.GetSeg().GetKey(),aPage.GetSeg().GetSegSize(),aPage.GetStart(),aPage.GetLength());
}

