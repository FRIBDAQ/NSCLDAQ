/** @fn Populate(DAQShmseg& aSeg)
* @brief Populate the pager with a shared memory segment.
*
* Populate the free list with pages created using the
* specified shared memory segment.
*
* @param aSeg A shared memory segment. 
* @return None
*/                                                             
void DAQBufferPager::Populate(DAQShmseg& aSeg)
{
  DAQBufferPage *pPage = NULL;
  int idx,len;
  long segsiz;

  if (aSeg.GetSeg() == NULL) return;

  segsiz = aSeg.GetSegSize();
  if (segsiz <= 0) return;

  idx = 0;

  while (idx < segsiz) {
    len = (pagesize < (segsiz - idx)) ? pagesize : (segsiz - idx);

    // Partial pages cause problems at the moment, so drop the
    // extra memory for the moment.
    if (len >= pagesize) {
      pPage = new DAQBufferPage(aSeg,idx,len);
      CleanPage(pPage);
      freelist.AppendUniq(pPage); 
      pagecount++;
    }

    idx += len;
  }

  CalcWaterMarks();
}

