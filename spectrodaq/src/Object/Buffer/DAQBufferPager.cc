/** @fn GetPages(DAQPageLinkedList& aPList,long aByteCount,bool NoLimit,bool UseWork)
* @brief Get a list of pages from the page pool.
*
* Get a list of pages from the page pool.  Acquiring a buffer page
* usually requires that the page pool has not exceeded its
* high water mark.  In a high tide condition, only SpectroDAQ
* system processes should be allowed to acquire pages under
* certain circumstances to help ensure that the scheduler
* can continue to process pages.
*
* @param aPList Append pages to this list.
* @param aByteCount The number of bytes desired.
* @param NoLimit Specify if high water marks should be ignored.
* @param UseWork Specify if the acquired pages should be added to the work list.
* @return The number of bytes actually acquired.
*/                                                             
long DAQBufferPager::GetPages(DAQPageLinkedList& aPList,long aByteCount,bool NoLimit,bool UseWork)
{
  long cnt = 0;
  long pcnt = 0;
  DAQBufferPage *pPage = NULL;

  if (aByteCount <= 0) return(0);

  cnt = 0;

  // Initialize... 
  pPage = GetPage(NoLimit,UseWork);
  if (pPage == NULL) return(0);

  cnt += pPage->GetLength();
  aPList.AppendUniq(pPage);  

  // and Loop...
  while ((cnt < aByteCount)&&(pPage != NULL)) {
    pPage = GetPage(NoLimit,UseWork);
    if (pPage != NULL) {
      aPList.AppendUniq(pPage);  
      cnt += pPage->GetLength();
    }
  }

  // Figure out max request
  pcnt = aPList.Count(); 
  if (pcnt >= maxrequest) {
    maxrequest = pcnt;
    requesttick = bufferclock;
    CalcWaterMarks(); 
  } else if (H2O_TIMEOUTDELTA < (bufferclock - requesttick)) {
    maxrequest = (maxrequest/2) > pcnt ? (maxrequest/2) : pcnt; 
    requesttick = bufferclock;
    if (maxrequest < H2O_MINPAGEREQ) maxrequest = H2O_MINPAGEREQ;
    CalcWaterMarks(); 
  }

  // If so, then we failed to get enough pages to satisfy the byte length.
  // So, release all the pages.
  if (cnt < aByteCount) {
    pPage = aPList.GetFirst();
    while (pPage != NULL) {
      aPList.Delete(pPage);
      ReleasePage(pPage,true);
      pPage = aPList.GetNext();
    } 

    cnt = 0;
  } 

  return(cnt);
}

