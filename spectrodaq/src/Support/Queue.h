/**
* @class Queue
* @brief Template queue class.
* 
* Template class definition for general value queue object.                 
* The LITEM template parameter represents the type of 
* QueueItems to be stored in this stack.  The MUTEX template
* parameter represents an implementation of the MutexIface class
* that can be used for concurency control.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LITEM,class MUTEX>
class Queue : public Printable {
  public:
    /*==============================================================*/
    /** @fn Queue()
    * @brief Default constructor.
    *
    * Default constructor.  A queue constructed using this
    * default constructor will be dynamically resized.
    *
    * @param None
    * @return this
    */                                                             
    Queue() 
      {qtail = NULL; qhead = NULL; cur = NULL; scnt = 0; maxscnt = 0; usecur = false;}; 

    /*==============================================================*/
    /** @fn Queue(int aSize)
    * @brief Constructor with a maximum queue size.
    *
    * Constructor with a maximum queue size.  Zero or negative
    * indicates that the queue should be dynamically resized.
    *
    * @param aSize The maximum queue size.
    * @return this
    */                                                             
    Queue(int aSize) 
      {qtail = NULL; qhead = NULL; cur = NULL; scnt = 0; maxscnt = aSize; usecur = false;}; 

    /*==============================================================*/
    /** @fn Empty()
    * @brief Empty this queue.
    *
    * Remove all items from this queue.
    *
    * @param None
    * @return None
    */                                                             
    void Empty() {
       int j;
       QueueItem<LITEM> *i,*i2;
    
       objmutex.Lock();
  
       i = qhead; 
       for (j = 0; j < scnt; j++) { 
         if (i != NULL) {
           i2 = i->GetNext(); 
           delete i;
           i = i2;
         }
       }

       usecur = false;
       cur = qhead = qtail = NULL;
       scnt = 0;

       objmutex.UnLock();
    };
  
    /*==============================================================*/
    /** @fn ~Queue()
    * @brief Destructor.
    *
    * Destroy this queue.
    *
    * @param None
    * @return None
    */                                                             
    ~Queue() {
       Empty();
     };

    /*==============================================================*/
    /** @fn Prepend(LITEM nitm,bool lockme)    
    * @brief Prepend an item to the queue.
    *
    * Prepend an item to the queue.  
    *
    * @param nitm The item to prepend to the stack.
    * @param lockme If the queue should be locked during the prepend.
    * @return If the prepend was successful.
    *   @retval true If the prepend was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Prepend(LITEM *nitm,bool lockme=true) {
      QueueItem<LITEM> *itm;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if ((maxscnt > 0)&&(scnt >= maxscnt)) { // Full
         if (lockme) objmutex.UnLock();
         return(false); 
      }

      itm  = new QueueItem<LITEM>(nitm);

      if (itm != NULL) {   
        if (qhead == NULL) {
          qhead = itm;
          qtail = itm; 
          itm->SetNext(itm);
          itm->SetPrev(itm);
        } else {
          itm->SetNext(qhead);
          itm->SetPrev(qtail);
          qhead->SetPrev(itm); 
          qtail->SetNext(itm); 
          qhead = itm;
        }
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }

      if (lockme) objmutex.UnLock();

      return(false);
    };

    /*==============================================================*/
    /** @fn Append(LITEM nitm,bool lockme)    
    * @brief Append an item to the queue.
    *
    * Append an item to the queue.  
    *
    * @param nitm The item to append to the queue.
    * @param lockme If the queue should be locked during the append.
    * @return If the append was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Append(LITEM *nitm,bool lockme=true) {
      QueueItem<LITEM> *itm;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if ((maxscnt > 0)&&(scnt >= maxscnt)) { // Full
        if (lockme) objmutex.UnLock();
        return(false); 
      }

      itm  = new QueueItem<LITEM>(nitm);

      if (itm != NULL) {   
        if (qhead == NULL) {
          qhead = itm;
          qtail = itm; 
          itm->SetNext(itm);
          itm->SetPrev(itm);
        } else {
          qtail->SetNext(itm);
          qhead->SetPrev(itm);
          itm->SetPrev(qtail);
          itm->SetNext(qhead);
          qtail = itm;
        }
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }

      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn Append(Queue<LITEM,MUTEX>& l)    
    * @brief Append a queue to the queue.
    *
    * Append a queue to the queue.  
    *
    * @param l The queue to append to the queue.
    * @return If the append was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Append (Queue<LITEM,MUTEX>& l) {
      QueueItem<LITEM> *itm;

      objmutex.Lock();

      if (maxscnt > 0) {
        if (scnt >= maxscnt) {
          objmutex.UnLock();
          return(false); // Full
        }

        if ((maxscnt - scnt) < l.Count()) {
          objmutex.UnLock();
          return(false); // Will be full
        }
      }

      itm = l.qhead;

      // Handle the first item
      if (itm != NULL) {
        Append(itm->Get(),false);
        itm = itm->GetNext();
      }

      // Now do the rest
      while ((itm != l.qhead)&&(itm != NULL)) {   
        Append(itm->Get(),false);
        itm = itm->GetNext();
      }

      objmutex.UnLock();
      return(true);
    };

    /*==============================================================*/
    /** @fn Prepend(Queue<LITEM,MUTEX>& l)    
    * @brief Prepend a queue to the queue.
    *
    * Prepend a queue to the queue.  
    *
    * @param l The queue to prepend to the queue.
    * @return If the prepend was successful.
    *   @retval true If the prepend was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Prepend (Queue<LITEM,MUTEX>& l) {
      QueueItem<LITEM> *itm;

      objmutex.Lock();

      if (maxscnt > 0) {
        if (scnt >= maxscnt) {
          objmutex.UnLock();
          return(false); // Full
        }

        if ((maxscnt - scnt) < l.Count()) {
          objmutex.UnLock();
          return(false); // Will be full
        }
      }

      itm = l.qtail;

      // Handle the first item
      if (itm != NULL) {
        Prepend(itm->Get(),false);
        itm = itm->GetPrev();
      }

      // Now do the rest
      while ((itm != l.qtail)&&(itm != NULL)) {   
        Prepend(itm->Get(),false);
        itm = itm->GetPrev();
      }

      objmutex.UnLock();
      return(true);
    };

    /*==============================================================*/
    /** @fn Delete(LITEM *itm)    
    * @brief Delete an item from the queue.
    *
    * Delete an item from the queue.
    *
    * @param itm The item to delete.
    * @return If the deletion was successful.
    *   @retval true If the deletion was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Delete(LITEM *itm) {
      QueueItem<LITEM> *bit,*wrk,*pbit;

      if (itm == NULL) return(false);

      objmutex.Lock();

      if (qhead != NULL) {
        if (cur != NULL) if (cur->Get() == itm) {
           cur = cur->GetNext();
           usecur = true;
        }

        if (qhead->Get() == itm) {
          wrk = qhead; 
          qhead = qhead->GetNext();
          if (qhead != wrk) { // More than one item
            qhead->SetPrev(qtail); 
            qtail->SetNext(qhead); 
          } else { // One item
            qhead = NULL; qtail = NULL;
          }
          scnt--;
          delete wrk;
          objmutex.UnLock();
          return(true);
        } else {
          bit = qhead->GetNext();
          while ((bit != qhead)&&(bit != NULL)) {
            if (bit->Get() == itm) {
              wrk = bit->GetNext();
              pbit = bit->GetPrev(); 
              if (qtail == bit) qtail = pbit;
              if (pbit != NULL) pbit->SetNext(wrk);
              if (wrk != NULL) wrk->SetPrev(pbit);
              scnt--;
              delete bit;
              objmutex.UnLock();
              return(true);
            } 
            bit = bit->GetNext();
          }
        }
      } 
      objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn IsEmpty()
    * @brief Check if the queue is empty.
    *
    * Check if the queue contains no items.
    *
    * @param None
    * @return If the queue is empty.
    *   @retval true If the queue is empty.  
    *   @retval false Otherwise.
    */                                                             
    bool IsEmpty() {if (scnt < 1) return(true); else return(false);};
  
    /*==============================================================*/
    /** @fn Count()
    * @brief Return the number of items in the queue.
    *
    * Return the current count of items in the queue.
    *
    * @param None
    * @return The number of items in the queue.
    */                                                             
    int Count() {return(scnt);};
  
    /*==============================================================*/
    /** @fn IsFull()
    * @brief Check if the queue is full.
    *
    * Check if the queue contains its maximum number of items.
    *
    * @param None
    * @return If the queue is full.
    *   @retval true If the queue is full.  
    *   @retval false Otherwise.
    */                                                             
    bool IsFull() {return((scnt >= maxscnt)&&(maxscnt > 0));};
  
    /*==============================================================*/
    /** @fn Resize(int aSize)
    * @brief Change the maximum queue size.
    *
    * Change the maximum size of the queue.  Return true if
    * resize is successful.  Notice that you can resize to
    * a size smaller than the current number of items in the
    * queue, but you won't be able to add anything until enough
    * items have been deleted to reduce the number of items under
    * the max.
    *
    * @param aSize The new maximum size.
    * @return If the resize was successful.
    *   @retval true If the resize was successful.
    *   @retval false Otherwise.
    */                                                             
    bool Resize(int aSize) {
      objmutex.Lock();
      if (aSize < 0) maxscnt = 0;    // Dynamic queue
      maxscnt = aSize;
      objmutex.UnLock();
      return(true);
    }
  
    /*==============================================================*/
    /** @fn GetHead()
    * @brief Get the first item in the queue.
    *
    * Get the first item in the queue.  Does not remove the
    * item.
    *
    * @param None
    * @return The first item in the queue.
    */                                                             
    LITEM *GetHead() {
      LITEM *itm;

      objmutex.Lock();
      if (qhead == NULL) {
        cur = NULL;
        objmutex.UnLock();
        return(NULL);
      }

      cur = qhead;
      itm = cur->Get();
      objmutex.UnLock();
      return(itm);
    };

    /*==============================================================*/
    /** @fn GetTail()
    * @brief Get the last item in the queue.
    *
    * Get the last item in the queue.  Does not remove the
    * item.
    *
    * @param None
    * @return The last item in the queue.
    */                                                             
    LITEM *GetTail() {
      LITEM *itm;

      objmutex.Lock();
      if (qtail == NULL) {
        cur = NULL;
        objmutex.UnLock();
        return(NULL);
      }

      cur = qtail;
      itm = cur->Get();
      objmutex.UnLock();
      return(itm);
    };

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next item from the queue.
    *
    * Get the next item from the queue.  Does not remove the
    * item.  GetHead() or GetTail() needs to be called prior to using this
    * It is possible to move back and forth between items in the queue
    * using GetNext() and GetPrev().
    *
    * @param None
    * @return The next item in the queue.
    */                                                             
    LITEM *GetNext(bool lockme=true) {
      LITEM *itm = NULL;

      if (lockme) objmutex.Lock();
      if (qhead == NULL) {
        cur = NULL;
        if (lockme) objmutex.UnLock();
        return(NULL);
      }

      if (usecur) {
        usecur = false;
        if (cur != NULL) itm = cur->Get();
        if (lockme) objmutex.UnLock();
        return(itm);
      } else {
        if (cur != NULL) cur = cur->GetNext();

        if (cur != NULL) itm = cur->Get();
        if (lockme) objmutex.UnLock();
        return(itm);
      }
    };

    /*==============================================================*/
    /** @fn GetPrev()
    * @brief Get the previous item from the queue.
    *
    * Get the previous item from the queue.  Does not remove the
    * item.  GetHead() or GetLast() needs to be called prior to 
    * using this method.  It is possible to move back and forth
    * between items in the queue using GetNext() andn GetPrev().
    *
    * @param None
    * @return The previous item in the queue.
    */                                                             
    LITEM *GetPrev(bool lockme=true) {
      LITEM *itm = NULL;

      if (lockme) objmutex.Lock();
      if (qhead == NULL) {
        cur = NULL;
        if (lockme) objmutex.UnLock();
        return(NULL);
      }

      if (cur != NULL) cur = cur->GetPrev(); 

      if (cur != NULL) itm = cur->Get();
      if (lockme) objmutex.UnLock();
      return(itm);
    };

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the next item from the queue (prefix).
    *
    * Get the next item from the queue.  Does not remove the
    * item.  GetHead() or GetTail() needs to be called prior to using this
    * It is possible to move back and forth between items in the queue
    * using GetNext() and GetPrev() and similar operators.
    *
    * @param None
    * @return The next item in the queue.
    */                                                             
    LITEM *operator++() {
      return(GetNext());
    };

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the current item from the queue and increment (postfix).
    *
    * Get the current item from the queue and increment.  Does not remove the
    * item.  GetHead() or GetTail() needs to be called prior to using this
    * It is possible to move back and forth between items in the queue
    * using GetNext() and GetPrev() and similar operators.
    *
    * @param None
    * @return The current item in the queue.
    */                                                             
    LITEM *operator++(int) {
      LITEM *wrk = NULL;
      objmutex.Lock();
      if (cur != NULL) wrk = cur->Get();
      GetNext(false);
      objmutex.UnLock();
      return(wrk);
    };

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the previous item from the queue (prefix).
    *
    * Get the previous item from the queue.  Does not remove the
    * item.  GetHead() or GetTail() needs to be called prior to using this
    * It is possible to move back and forth between items in the queue
    * using GetNext() and GetPrev() and similar operators.
    *
    * @param None
    * @return The previous item in the queue.
    */                                                             
    LITEM *operator--() {
      return(GetPrev());
    };

    /*==============================================================*/
    /** @fn operator--(int)
    * @brief Get the current item from the queue and decrement (postfix).
    *
    * Get the current item from the queue and decrement.  Does not remove the
    * item.  GetHead() or GetTail() needs to be called prior to using this
    * It is possible to move back and forth between items in the queue
    * using GetNext() and GetPrev() and similar operators.
    *
    * @param None
    * @return The current item in the queue.
    */                                                             
    LITEM *operator--(int) {
      LITEM *wrk = NULL;
      objmutex.Lock();
      if (cur != NULL) wrk = cur->Get();
      GetPrev(false);
      objmutex.UnLock();
      return(wrk);
    };

    /*==============================================================*/
    /** @fn AtHead()
    * @brief Check if the current queue position is pointed at the head.
    *
    * Check if the current queue position is pointer at the head of
    * the queue.
    *
    * @param None
    * @return If the current queue position is pointed at the head.
    *   @retval true If the current queue position is at the head.
    *   @retval false Otherwise.
    */                                                             
    bool AtHead() {
      return(cur == qhead);
    };
    
    /*==============================================================*/
    /** @fn AtTail()
    * @brief Check if the current queue position is pointed at the tail.
    *
    * Check if the current queue position is pointer at the tail of
    * the queue.
    *
    * @param None
    * @return If the current queue position is pointed at the tail.
    *   @retval true If the current queue position is at the tail.
    *   @retval false Otherwise.
    */                                                             
    bool AtTail() {
      return(cur == qtail);
    };
    
    /*==============================================================*/
    /** @fn Print(ostream& aStream)
    * @brief Print this queue.
    *
    * Print this queue on an output stream.
    *
    * @param aStream The output stream.
    * @return The output stream.
    */                                                             
    virtual ostream& Print(ostream& aStream) {
      int i;
      QueueItem<LITEM> *p;

      objmutex.Lock(); 
      aStream << "Head=" << qhead << ",Tail=" << qtail << ",Count=" << scnt << ",Max=" << maxscnt << "<br clear=\"all\"/>" << endl;

      p = qhead;
      for (i = 0; i < scnt; i++) { 
        aStream << "  Item " << i << ": ";
        if (p != NULL) {
          aStream << (*p) << "<br clear=\"all\"/>" << endl;
        } else {
          aStream << "(nil pointer)" << "<br clear=\"all\"/>" << endl;
        }
        p = p->GetNext();
      }

      objmutex.UnLock();
      return(aStream);
    }

    /*==============================================================*/
  protected:
    /**
    * @var objmutex
    * @brief Concurrency control mutex.
    *
    * Mutex for synchronizing multiple thread usage of this queue.
    */
    MUTEX objmutex;       // A mutex for this object

    /*==============================================================*/
  private:
    friend class QueueIterator<LITEM,MUTEX>;

    /**
    * @var qhead
    * @brief Head of the queue.
    *
    * Points to the item at the head of the queue.
    */
    QueueItem<LITEM> *qhead;  // Head of queue

    /**
    * @var qtail
    * @brief Tail of the queue.
    *
    * Points to the item at the tail of the queue.
    */
    QueueItem<LITEM> *qtail;  // Tail of queue

    /**
    * @var cur
    * @brief Current queue item.
    *
    * Points to the current queue item being processed by
    * iteration (GetNext(), GetPrev(), etc.)
    */
    QueueItem<LITEM> *cur; // Current queue item (for GetHead and GetTail)

    /**
    * @var usecur
    * @brief Current item has not been processed.
    *
    * Current item has not been processed so process it next.
    */
    bool usecur;          // Current item has not been returned

    /**
    * @var scnt
    * @brief Current number of queue items.
    *
    * Number of items currently stored in the queue.
    */
    int scnt;             // Number of queue items.

    /**
    * @var maxscnt
    * @brief Maximum number of queue items.
    *
    * Maximum number of items that can be stored in this queue.
    */
    int maxscnt;          // Max number of queue items.
};

/**
* @class QueueIterator 
* @brief Template definition for a queue iterator.
*
* Template definition for a queue iterator (see the Queue
* class).  The LITEM template parameter represents the type of
* items that can be added to this queue.  The MUTEX template parameter
* represents an implementation of the MutexIface that can be used
* to control concurrent access to this queue.
*
* WARNING:  This iterator class assumes that you're not
* changing the Queue while iterating.  That is, this iterator
* class is meant to allow multiple threads to read the queue
* simultaneously.  If someone changes the Queue while the threads
* are using it, unexpected results are likely to occur.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LITEM,class MUTEX>
class QueueIterator : public Printable {
  public:
    /*==============================================================*/
    /** @fn QueueIterator()
    * @brief Constructor.
    *
    * Default constructor.
    *
    * @param None
    * @return this
    */                                                             
    QueueIterator() {
      cur = NULL;
      theq = NULL;
    }

    /*==============================================================*/
    /** @fn ~QueueIterator()
    * @brief Destructor.
    *
    * Destroy this queue interator.
    *
    * @param None
    * @return None
    */                                                             
    ~QueueIterator() {
      cur = NULL;
    }

    /*==============================================================*/
    /** @fn GetFirst(Queue<LITEM,MUTEX> *aQ)
    * @brief Get the first item in the queu.
    *
    * Get the first item in the specified queue.
    *
    * @param aQ The queue to iterate over.
    * @return The first item or null.
    */                                                             
    LITEM *GetFirst(Queue<LITEM,MUTEX> *aQ) {
      theq = aQ;
      cur = NULL;
 
      if (theq != NULL) cur = theq->qhead;
      if (cur != NULL) return(cur->Get());
      else return(NULL);
    }

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next item in a queue.
    *
    * Get the next item in a queue specified by an earlier call to
    * GetFirst.
    *
    * @param None
    * @return The next item or null.
    */                                                             
    LITEM *GetNext() {
      if (cur == theq->qtail) return(NULL); 

      if (cur != NULL) cur = cur->GetNext();

      if (cur != NULL) return(cur->Get());
      else return(NULL);
    }

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the next item in a queue.
    *
    * Get the next item in a queue specified by an earlier call to
    * GetFirst.
    *
    * @param None
    * @return The next item or null.
    */                                                             
    LITEM *operator++() {
      return(GetNext());
    }

  private:
    /**
    * @var cur
    * @brief Current queue item.
    *
    * Current queue item (used with GetFirst(), GetNext() and friends).
    */
    QueueItem<LITEM> *cur;

    /**
    * @var theq
    * @brief A pointer to the queue.
    *
    * A pointer to the queue currently under iteration.
    */
    Queue<LITEM,MUTEX> *theq;
};

