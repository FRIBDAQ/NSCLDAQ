/**
* @class QueueItem
* @brief A class for encapsulating queue objects.
*
* A class for encapsulation and linking queue objects
* so they can be added to a queue.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LTYPE>
class QueueItem : public Printable {
  public:
    /*==============================================================*/
    /** @fn QueueItem(LTYPE b)
    * @brief Constructor with an object pointer.
    *
    * Constructor with an object pointer.
    *
    * @param b An object.
    * @return this
    */                                                             
    QueueItem(LTYPE *b) { // Constructor
      myitem = b;        // Objects need to have operator=
      nxt = NULL; prv = NULL;
    }; 

    /*==============================================================*/
    /** @fn ~QueueItem()
    * @brief Destructor.
    *
    * Destroy this queu item.
    *
    * @param None
    * @return None
    */                                                             
    ~QueueItem() {};  // Destructor

    /*==============================================================*/
    /** @fn Get()
    * @brief Get the encapsulated object pointer.
    *
    * Return the encapsulated object pointer.
    *
    * @param None
    * @return The encapsulated object pointer.
    */                                                             
    LTYPE *Get() {return(myitem);}; // Get item encapsulated by this item 

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next queue item.
    *
    * Queue items are linked together using next and
    * previous pointers.  This method returns the queue item 
    * linked following this one.
    *
    * @param None
    * @return The next queue item.
    */                                                             
    QueueItem<LTYPE> *GetNext() {return(nxt);};  // Get next queue item

    /*==============================================================*/
    /** @fn GetPrev()
    * @brief Get the previous queue item.
    *
    * Queue items are linked together using next and
    * previous pointers.  This method returns the queue item 
    * linked prior to this one.
    *
    * @param None
    * @return The previous queue item.
    */                                                             
    QueueItem<LTYPE> *GetPrev() {return(prv);};  // Get prev queue item

    /*==============================================================*/
    /** @fn SetNext(QueueItem<LTYPE> *b)
    * @brief Set the next queue item.
    *
    * Queue items are linked together using next and
    * previous pointers.  This method sets the queue item 
    * linked next this one to the one specified as a parameter.
    *
    * @param b The new next queue item pointer.
    * @return None
    */                                                             
    void SetNext(QueueItem<LTYPE> *b) {nxt = b;};  // Set the next queue item

    /*==============================================================*/
    /** @fn SetPrev(QueueItem<LTYPE> *b)
    * @brief Set the previous queue item.
    *
    * Queue items are linked together using next and
    * previous pointers.  This method sets the queue item 
    * linked prior to this one to the one specified as a parameter.
    *
    * @param b The new next queue item pointer.
    * @return None
    */                                                             
    void SetPrev(QueueItem<LTYPE> *b) {prv = b;};   // Set the prev queue item

    /*==============================================================*/
    /** @fn operator=(const QueueItem<LTYPE>& l)
    * @brief Assignment operator.
    *
    * Assign a different queue item to this one.
    *
    * @param l The queue item to assign to this one.
    * @return this
    */                                                             
    QueueItem<LTYPE>& operator=(const QueueItem<LTYPE>& l) {
      myitem = l.myitem;
      nxt = l.nxt;
      prv = l.prv;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Print(ostream& aStream)
    * @brief Print this queue item.
    *
    * Print this queue item on an output stream.
    *
    * @param aStream The output stream.
    * @return The output stream.
    */                                                             
    ostream& Print(ostream& aStream) {
      if (myitem != NULL) 
         aStream << "Item=[" << (*myitem) << "],Ptr=" << this;
      else 
         aStream << "Item=(nil),Ptr=" << this;

      aStream << ",Next=" << nxt << ",Prev=" << prv;
      return(aStream);
    };

  protected:
    /*==============================================================*/
    /** @fn QueueItem()
    * @brief Default constructor.
    *
    * Default constructor.  Protected and should not be called
    * directly.
    *
    * @param None
    * @return this
    */                                                             
    QueueItem() {nxt = NULL; prv = NULL; myitem = NULL;};  // Default constructor
  private:
    /**
    * @var myitem
    * @brief Encapsulated object pointer.
    *
    * Object pointer encapsulated by this queue item.
    */
    LTYPE  *myitem;  // Object encapsulated by this queue item

    /**
    * @var nxt
    * @brief Next queue item.
    *
    * Queue item linked following this one. 
    */
    QueueItem<LTYPE> *nxt;  // Next queue item

    /**
    * @var prv
    * @brief Previous queue item.
    *
    * Queue item linked prior to this one. 
    */
    QueueItem<LTYPE> *prv;  // Prev queue item
};

