/*===================================================================*/         
// XMLParser::PushState
//                            
// Push a state on the user state stack.  Return the current state.
//
int XMLParser::PushState(int aState) {
  int oldstate;
  oldstate = statestack.Peek();
  statestack.Push(aState); 
  return(oldstate);
}

/*===================================================================*/         
// XMLParser::PopState
//                            
// Pop a state off the user state stack.  
//
int XMLParser::PopState() {
  return(statestack.Pop());
}

/*===================================================================*/         
// XMLParser::PeekState
//                            
// Peek at the user state on top of the user state stack.
//
int XMLParser::PeekState() {
  return(statestack.Peek());
}

/*===================================================================*/         
// XMLParser::UserStates
//                            
// Return the number of user states on the user state stack.
//
int XMLParser::UserStates() {
  return(statestack.Count());
}

