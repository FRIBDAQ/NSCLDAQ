/*===================================================================*/         
// XMLParser::Puts
//                            
// Put a string to output.
//
bool XMLParser::Puts(const char *aStr) {
  if (!OutReady) return(false);

  if (Output == NULL) {
     OutReady = false;
     return(false); 
  }

  if (aStr == NULL) return(true);
  if (strlen(aStr) <= 0) return(true);

  if (!Output->Puts(aStr)) {
    OutReady = false;
    SetError(XML_GetCurrentLineNumber(parser),XML_GetCurrentColumnNumber(parser),XMLPCSTR("output failure"),"Could not put string to XMLOut device");
  }

  return(OutReady);
}

/*===================================================================*/         
// XMLParser::SetError
//                            
// Set the error line and text.
//                          
void XMLParser::SetError(int aLine,int aColumn,const char *pContext,const char *pText) {
  int siz = 0;

  ErrorLine = aLine;
  ErrorColumn = aColumn;

  if (ErrorString != NULL) delete[] ErrorString;
  ErrorString = NULL;

  if (ErrorContext != NULL) delete[] ErrorContext;
  ErrorContext = NULL; 

  if (pText != NULL) {
    siz = strlen(pText);
    ErrorString = new char[siz+1];
    strncpy(ErrorString,pText,siz);
    ErrorString[siz] = '\0';
  }

  if (pContext != NULL) {
    siz = strlen(pContext);
    ErrorContext = new char[siz+1];
    strncpy(ErrorContext,pContext,siz);
    ErrorContext[siz] = '\0';
  }
}

/*===================================================================*/         
// XMLParser::XMLParse
//                            
// Base parser routine called by other parser methods.  
// Return true if all is ok, false otherwise.
//
// Arguments:
//   pBuf - Pointer to a buffer.
//   aLen - Length of data buffer.
//   done - Set when parsing is finished -- no more data following
//          current buffer.
//                          
bool XMLParser::XMLParse(char *pBuf,int aLen,int done) {
  XMLParser *oldparser = NULL;

  // MUTEX_LOCK
  oldparser = XMLOOP_Parser;
  XMLOOP_Parser = this;
 
  if (!XML_Parse(parser,pBuf,aLen,done)) {
    // If we were parsing an external entity then keep the 
    // error generated there.
    if (XML_GetErrorCode(parser) != XML_ERROR_EXTERNAL_ENTITY_HANDLING) {
      SetError(XML_GetErrorLineNumber(parser),XML_GetErrorColumnNumber(parser),XMLPCSTR("parsing internal entity"),(char *)XML_ErrorString(XML_GetErrorCode(parser)));
    }

    XMLOOP_Parser = oldparser;
    // MUTEX_UNLOCK
    return(false);
  }

  XMLOOP_Parser = oldparser;

  // MUTEX_UNLOCK
  if (!OutReady) return(false);
  else return(true);
}

/*===================================================================*/         
// XMLParser::Parse
//                            
// Parse a istream. Return true if all is ok, false if not.
//                          
bool XMLParser::Parse(istream& aStream) {
  int done;
  size_t len;
  bool rc = true;
  char buf[BUFSIZ];

  if (!aStream) {
    SetError(0,0,XMLPCSTR("attempting to parse a stream"),"Input stream is empty");
    return(false);
  }

  do {
    aStream.read(buf,sizeof(buf));
    done = (!aStream);
    len = aStream.gcount();
    rc = XMLParse(buf,len,done);
  } while ((!done)&&(rc));

  return(rc);
}

/*===================================================================*/         
// XMLParser::Parse
//                            
// Parse a FILE*. Return true if all is ok, false if not.
//                          
bool XMLParser::Parse(FILE *fp) {
  int done;
  size_t len;
  bool rc = true;
  char buf[BUFSIZ];

  if (fp == NULL) {
    SetError(0,0,XMLPCSTR("attempting to parse a file pointer"),"File pointer is NULL");
    return(false);
  }

  do {
    len = fread(buf,1,sizeof(buf),fp);
    done = len < sizeof(buf);
    rc = XMLParse(buf,len,done);
  } while ((!done)&&(rc));

  return(rc);
}

/*===================================================================*/         
// XMLParser::Parse
//                            
// Parse an fd. Return true if all is ok, false if not.
//                          
bool XMLParser::Parse(int fd) {
  int done;
  size_t len;
  bool rc = true;
  char buf[BUFSIZ];

  if (fd < 0) {
    SetError(0,0,XMLPCSTR("attempting to parse a file descriptor"),"File descriptor < 0");
    return(false);
  }

  do {
    len = read(fd,buf,sizeof(buf));
    done = len < sizeof(buf);
    rc = XMLParse(buf,len,done);
  } while ((!done)&&(rc));

  return(rc);

  return(true);
}

