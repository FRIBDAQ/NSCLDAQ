/*===================================================================*/
// XMLOOP_ExternalEntity
//                                                                  
// External Entity handler.
//                                                                  
static int XMLOOP_ExternalEntity(XML_Parser aXMLParser,const XML_Char *pContext,const XML_Char *pBase,const XML_Char *pSystemId,const XML_Char *pPublicId)
{
  int rc = 1;  // 1 --> OK, 0 --> failure
  XMLExternalEntityHandler *phand = NULL;

  if ((phand = XMLOOP_Parser->FindExternalEntityHandler()) != NULL) {
    rc = (*phand)(XMLOOP_Parser,(char *)pContext,(char *)pBase,(char *)pSystemId,(char *)pPublicId);
  }

  return(rc);
}

