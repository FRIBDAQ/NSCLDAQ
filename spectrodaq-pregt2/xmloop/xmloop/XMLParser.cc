// Variable shared between incarnations of the XMLParser object.
// Handle carefully and beware when multithreading -- will need to
// add mutex handling before calling any of the Parse() methods!
static XMLParser *XMLOOP_Parser = NULL;

/*===================================================================*/
// XMLOOP_StartElem
//                                                                  
// Start element handler
//                                                                  
static void XMLOOP_StartElem(void *pUserData,const XML_Char *pTarget,const XML_Char **pAtts)
{
  XMLStartHandler *phand = NULL;
  
  if ((phand = XMLOOP_Parser->FindStartHandler((const char *)pTarget)) != NULL) {
    (*phand)(XMLOOP_Parser,pTarget,pUserData,pAtts);
  } 

  XMLOOP_Parser->IncDepth();
}

/*===================================================================*/
// XMLOOP_EndElem
//                                                                  
// End element handler
//                                                                  
static void XMLOOP_EndElem(void *pUserData,const XML_Char *pTarget)
{
  XMLEndHandler *phand = NULL;

  XMLOOP_Parser->DecDepth();

  if ((phand = XMLOOP_Parser->FindEndHandler((const char *)pTarget)) != NULL) {
    (*phand)(XMLOOP_Parser,pTarget,pUserData);
  } 
}

