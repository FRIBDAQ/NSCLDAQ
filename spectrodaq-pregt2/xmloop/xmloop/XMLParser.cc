// XMLParser::ExpandInternal
//                            
// Set/Unset internal entity expansion.
//
void XMLParser::ExpandInternal(bool aBool) {
  if (ExpInt != aBool) {
    ExpInt = aBool;
    if (ExpInt) {
      XML_SetDefaultHandlerExpand(parser,XMLOOP_Default);
    } else {
      XML_SetDefaultHandler(parser,XMLOOP_Default);
    }
  }
}

