/*===================================================================*/
// XMLOOP_StartNamespaceDecl
//                                                                  
// Start namespace declaration handler.
//                                                                  
static void XMLOOP_StartNamespaceDecl(void *pUserData,const XML_Char *pPrefix,const XML_Char *pURI)
{
  XMLStartNamespaceDeclHandler *phand = NULL;

  if ((phand = XMLOOP_Parser->FindStartNamespaceDeclHandler(pPrefix)) != NULL) {
    XMLOOP_Parser->PushNamespace(pPrefix,pURI);
    (*phand)(XMLOOP_Parser,pUserData,pPrefix,pURI);
  }
}

/*===================================================================*/
// XMLOOP_EndNamespaceDecl
//                                                                  
// End namespace declaration handler.
//                                                                  
static void XMLOOP_EndNamespaceDecl(void *pUserData,const XML_Char *pPrefix)
{
  XMLEndNamespaceDeclHandler *phand = NULL;

  if ((phand = XMLOOP_Parser->FindEndNamespaceDeclHandler(pPrefix)) != NULL) {
    (*phand)(XMLOOP_Parser,pUserData,pPrefix);
    XMLOOP_Parser->PopNamespace();
  }
}

/*===================================================================*/
// XMLParser::XMLParser                                             
//                                                                  
// Constructor.                                                     
//                                                                  
XMLParser::XMLParser() 
{
  ErrorString = NULL;
  ErrorContext = NULL;
  ErrorLine = -1;
  ErrorColumn = -1;
  ExpInt = true;

  Output = NULL;
  OutReady = false;

  nsstack.Push(new XMLNamespace());  // Default namespace

  parserdepth = 0;
  parser = XML_ParserCreateNS(NULL,':');
  XML_SetElementHandler(parser,XMLOOP_StartElem,XMLOOP_EndElem);
  XML_SetCharacterDataHandler(parser,XMLOOP_CharacterData);
  XML_SetProcessingInstructionHandler(parser,XMLOOP_PI);
  XML_SetDefaultHandlerExpand(parser,XMLOOP_Default);
  XML_SetExternalEntityRefHandler(parser,XMLOOP_ExternalEntity);
  XML_SetCommentHandler(parser,XMLOOP_Comment);
  XML_SetCdataSectionHandler(parser,XMLOOP_StartCdata,XMLOOP_EndCdata);
  XML_SetUnparsedEntityDeclHandler(parser,XMLOOP_UnparsedEntityDecl);
  XML_SetNotationDeclHandler(parser,XMLOOP_NotationDecl);
  XML_SetNamespaceDeclHandler(parser,XMLOOP_StartNamespaceDecl,XMLOOP_EndNamespaceDecl);

  if (XMLOOP_Parser == NULL) XMLOOP_Parser = this;
}

/*===================================================================*/
// XMLParser::XMLParser                                             
//                                                                  
// Special constructor.                                                     
//                                                                  
XMLParser::XMLParser(XML_Parser aXMLParser,XMLParser& aParser)
{
  ErrorString = NULL;
  ErrorContext = NULL;
  ErrorLine = -1;
  ErrorColumn = -1;
  ExpInt = aParser.ExpInt;

  Output = aParser.Output;
  OutReady = aParser.OutReady;

  nsstack = aParser.nsstack; 
  parserdepth = aParser.parserdepth;
  parser = aXMLParser;

  XML_SetElementHandler(parser,XMLOOP_StartElem,XMLOOP_EndElem);
  XML_SetCharacterDataHandler(parser,XMLOOP_CharacterData);
  XML_SetProcessingInstructionHandler(parser,XMLOOP_PI);
  XML_SetDefaultHandlerExpand(parser,XMLOOP_Default);
  XML_SetExternalEntityRefHandler(parser,XMLOOP_ExternalEntity);
  XML_SetCommentHandler(parser,XMLOOP_Comment);
  XML_SetCdataSectionHandler(parser,XMLOOP_StartCdata,XMLOOP_EndCdata);
  XML_SetUnparsedEntityDeclHandler(parser,XMLOOP_UnparsedEntityDecl);
  XML_SetNotationDeclHandler(parser,XMLOOP_NotationDecl);
  XML_SetNamespaceDeclHandler(parser,XMLOOP_StartNamespaceDecl,XMLOOP_EndNamespaceDecl);

  if (XMLOOP_Parser == NULL) XMLOOP_Parser = this;
}

/*===================================================================*/
// XMLParser::~XMLParser                                            
//                                                                  
// Destructor.                                                      
//                                                                  
XMLParser::~XMLParser()
{
  if (XMLOOP_Parser == this) XMLOOP_Parser = NULL;
  XML_ParserFree(parser);
  if (ErrorString != NULL) delete[] ErrorString;
  if (ErrorContext != NULL) delete[] ErrorContext;
}

