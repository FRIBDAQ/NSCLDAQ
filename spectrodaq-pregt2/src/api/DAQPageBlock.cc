/** @fn DAQPageBlock()
* @brief Default constructor.
*
* Default constructor.
*
* @param None
* @return this
*/                                                             
DAQPageBlock::DAQPageBlock() {
  my_datasize = 0;
  len = 0;
  data = NULL;
}

/*==============================================================*/
/** @fn DAQPageBlock(DAQBufferPage& aPage)
* @brief Constructor using a DAQBufferPage.
*
* Constructor with a DAQBufferPage.
*
* @param aPage A reference to a DAQBufferPage. 
* @param aDataSiz Size of the data type used by the block.
* @return this
*/                                                             
DAQPageBlock::DAQPageBlock(DAQBufferPage& aPage,unsigned int aDataSiz) {
  int port = -1; 
  netio_fptr net_funcs;

  url = daq_server_config.GetBufferURL();
  net_funcs = netio_setproto(url.GetProto());
  if (net_funcs) port = netio_getportbyname(net_funcs,SVC_SDAQ_BUFF); 
  if (port > 0) url.SetPort(port);

  my_page = aPage;
  my_datasize = aDataSiz;
  data = (void*)(aPage.GetSeg().GetSeg()+aPage.GetStart());

  if (aDataSiz <= 0) len = 1;
  else len = aPage.GetLength()/my_datasize;

  refcnt = 1; 
}

/*==============================================================*/
/** @fn DAQPageBlock(DAQPageBlock& blk)
* @brief Copy constructor.
*
* Copy constructor.  Constructs this page block using the
* definition of another.
*
* @see CopyToThis(DAQPageBlock&)
* @param blk A reference to a another page block.
* @return this
*/                                                             
DAQPageBlock::DAQPageBlock(DAQPageBlock& blk) {
  CopyToThis(blk); 
}

/*==============================================================*/
/** @fn ~DAQPageBlock()
* @brief Destructor.
*
* Destroy this page block.
*
* @param None
* @return None
*/                                                             
DAQPageBlock::~DAQPageBlock() {
  CheckReference();
  data = NULL;
  len = 0;
  my_datasize = 1;
}

/*==============================================================*/
/** @fn operator=(const DAQPageBlock<BTYPE>& blk)
* @brief Assignment.
*
* Assign another page block to this page block.
*
* @see CopyToThis(DAQPageBlock<BTYPE>&)
* @param blk a Reference to a page block.
* @return A reference to this
*/                                                             
DAQPageBlock& DAQPageBlock::operator=(const DAQPageBlock& blk) {
  CopyToThis(blk);
  return(*this);
}

/*==============================================================*/
/** @fn GetLength()
* @brief Get the length of this page block in native units.
*
* Get the length of this page block as a count of the number
* of elements of my_datasize that can be stored in this page block.
* For instance, the length returned will be the number 
* Words stored in this page block if this block was typed
* as a Word page block.
*
* @param None
* @return A count of native units.
*/                                                             
int DAQPageBlock::GetLength() {
  return(len);
}

