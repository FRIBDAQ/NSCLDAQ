/** @fn ReleaseData()
* @brief Release the underlying data structures.
*
* Release the underlying data structures.  
*
* @param None
* @return None
*/                                                             
void DAQPageBlock::ReleaseData() {
  my_page.SetRelease(); 
  ResetInUse(); 
  data = NULL;
  len = 0;
  my_datasize = 0;
}

/*==============================================================*/
/** @fn CheckReference()
* @brief Check the reference count prior to deleting.
*
* Check the reference count for this page block and delete the
* underlying data structures if it has gone zero.  Prevents
* deletion of data as long as the data is in use.
*
* @param None
* @return None
*/                                                             
void DAQPageBlock::CheckReference() {
  refcnt.CheckReference();

  if (refcnt() <= 0) {
    ReleaseData();
  }
}

/*==============================================================*/
/** @fn CopyToThis(const DAQPageBlock& blk)
* @brief Copy a DAQPageBlock to this one.
*
* Copy a different DAQPageBlock to this one.  Simply
* points to other page block's data and sets the reference count
* appropriately.
*
* @param blk The other DAQPageBlock.
* @return None
*/                                                             
DAQPageBlock& DAQPageBlock::CopyToThis(const DAQPageBlock& blk) {
  CheckReference();

  my_page = blk.my_page;
  my_datasize = blk.my_datasize;

  url = blk.url;

  data = blk.data;
  len = blk.len; 

  refcnt = blk.refcnt;                 // There can be only one
  refcnt++;                            // Increment new counter;

  return(*this);
}
