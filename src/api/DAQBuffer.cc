/** @fn void toStream(PrintStreamIface& aStream)
* @brief Print this object to a PrintStream.
* 
* Print this object to aPrintStream.
* 
* @param aStream The PrintStream.
* @return None
*/                                                             
void DAQBuffer::toStream(PrintStreamIface& aStream) {
  DAQPageBlock *bw = NULL;  
  unsigned char ch;
  unsigned short v;
  int i,j,cnt = 0,bsiz,bcnt,len;
  unsigned char *p = NULL,tmp[BUFFER_DUMP_CHARCOUNT+1];
   
  len = (rlen*my_datasize);

  bw = (DAQPageBlock*)(blocks.GetFirst());
  if (!blocks.Bottom()) {
    bsiz = bw->GetLength() * my_datasize;
    bcnt = 0;
    p = (unsigned char *)bw->Get();
  }

  aStream.hex();
  aStream.width(8);
  aStream.fill('0');
  aStream.print(0);
  aStream.print(": ");

  for (i = 0; (i < len)&&(!blocks.Bottom()); i++) {
    if (cnt > BUFFER_DUMP_CHARCOUNT) {
      aStream.println();
      aStream.print("          ");

      for (j = 0; j < cnt; j++) {
        ch = tmp[j];

        if (isprint(ch)&&(!iscntrl(ch))) aStream.printf(" %c",ch);
        else aStream.printf("  ");
      }
   
      aStream.hex();
      aStream.width(8);
      aStream.fill('0');
      aStream.print(i);
      aStream.print(": ");
      cnt = 0;
    }
  
    v = (*p);  
    aStream.hex();
    aStream.width(2);
    aStream.fill('0');
    aStream.print(v);
    tmp[cnt] = (*p);
    cnt++; p++; bcnt++;

    if (bcnt >= bsiz) {
      bw = (DAQPageBlock*)(blocks.GetNext());
      if (!blocks.Bottom()) {
        bsiz = bw->GetLength() * my_datasize;
        bcnt = 0;
        p = (unsigned char *)bw->Get();
      }
    }
  }

  if (cnt > 0) {
    aStream.println();
    aStream.print("          ");
  }

  for (j = 0; j < cnt; j++) {
    ch = tmp[j];

    if (isprint(ch)&&(!iscntrl(ch))) aStream.printf(" %c",ch);
    else aStream.printf("  ");
  }
   
  aStream.println();
} 

/*==============================================================*/
/** @fn Release()
* @brief Release this buffer
*
* Release all the underlying storage blocks associated with
* this buffer.  After the Release() the buffer's length will
* be zero.
*
* @param None
* @return None
*/                                                             
void DAQBuffer::Release() {
   DAQPageBlock *bw = NULL;  

   bw = (DAQPageBlock *)(blocks.GetFirst());
   while (!blocks.Bottom()) {
     bw->SetPos(0);
     bw->SetLastPage();
     bw->SetReqLen(rlen);
     bw->SetByteLen(rlen*my_datasize);
     bw->SetUsedLen(scnt);
     bw->SetModified();
     bw->SetTag(BUFFER_TAG_BITBUCKET);
     bw->ResetNode();
     bw = (DAQPageBlock *)(blocks.GetNext());
   }

   Nullify();
   cmax = 0;
   rlen = 0;
   scnt = 0;
   lastidx = -1;
   lastblk = NULL;
   my_allocator.Release(&blocks);
   blocks.Empty();
} 

/*==============================================================*/
/** @fn Route()
* @brief  Route this buffer (and release the buffer)
*
* Send this buffer up to the SpectroDAQ server for transmital
* to another machine and/or process.  This buffer will of zero
* length once Routed.
*
* @parm None
* @return None
*/                                                             
void DAQBuffer::Route() {
   LinkedList lst;
   bool fndlast = false;
   DAQPageBlock *bw = NULL;  
   unsigned int tmptag = 0;

   bw = (DAQPageBlock*)(blocks.GetFirst());
   if (bw != NULL) {
     bw->SetReqLen(rlen);
     bw->SetByteLen(rlen*my_datasize);
     bw->SetUsedLen(scnt);
     bw->SetModified();
   }

   // Don't route pages that are beyond the last page.
   // Note that a Release is simply a Route with the right
   // tag set (BUFFER_TAG_BITBUCKET).
   bw = (DAQPageBlock *)(blocks.GetFirst());
   while (!blocks.Bottom()) {
     if (fndlast) {  // Relase the page
       bw->SetPos(0);
       bw->SetLastPage();
       bw->SetReqLen(rlen);
       bw->SetByteLen(rlen*my_datasize);
       bw->SetUsedLen(scnt);
       bw->SetModified();
       bw->SetTag(BUFFER_TAG_BITBUCKET);
       bw->ResetNode();
     }
     if (bw->IsLastPage()) fndlast = true; 
     bw = (DAQPageBlock *)(blocks.GetNext());
   }
  
   Nullify();
   cmax = 0;
   rlen = 0;
   scnt = 0;
   lastidx = -1;
   lastblk = NULL;
   my_allocator.Route(&blocks);
   blocks.Empty();
} 

/*==============================================================*/
/** @fn Accept(struct timeval *pTO = NULL)
* @brief Accept a list of blocks from elsewhere 
* 
* This method accepts a buffer (as a list of blocks) that was
* transmited using Route().  This buffer should be tagged
* with the appropriate tag/mask combination to match the
* desired type(s) of buffers for which reception is desired.
* The pTO parameter indicates the length of time to wait for
* an arriving buffer.  If pTO is NULL, then wait forever.
* 
* For a buffer on the server to be accepted by this buffer
* the tag/mask of the buffer and the buffer on the server 
* must be as follows:
*
*  (server_tag & buffer_mask) == (buffer_tag & buffer_mask) 
*
* @param pTO Indicates how long to wait for a buffer.
* @return If a buffer was accepted
*   @retval true A buffer was accepted 
*   @retval false A buffer wasn't accepted 
*/                                                             
bool DAQBuffer::Accept(struct timeval *pTO) {
   LinkedList *lst = NULL;
   DAQPageBlock *bw = NULL;  

   Release();

   lst = my_allocator.Accept(routetag,routemask,pTO);

   if (lst != NULL) {
     blocks.Append(*lst);
     cmax = 0;
     delete lst;

     bw = (DAQPageBlock*)(blocks.GetFirst());

     if (bw != NULL) { // Only the first block has this data set
        routetag = bw->GetTag();
        originip = bw->GetIP();
        cmax = CountList(blocks);
        rlen = bw->GetReqLen();
        scnt = bw->GetUsedLen();
     } 

     return(true);
   } 

   return(false);
} 

/*==============================================================*/
/** @fn Resize(int siz,bool w)
* @brief Resize this buffer
*
* Resizes this buffer to a larger or smaller size.  The size
* is specified as a count of the number of primative units.
*
* @param siz The desired new size for the buffer
* @param w   Set to true if you want this method to block
* @return If the resize succeeded
*   @retval true if the resize succeeded
*   @retval false if the resize failed
*/                                                             
bool DAQBuffer::Resize(int siz,bool w) {
  LinkedList *lst = NULL;

  if (siz <= cmax) {
     rlen = siz;
     lastidx = -1; 
     lastblk = NULL;
     if (scnt > siz) scnt = siz;
     cmax = CountList(blocks);
     return(true);
  } 

  if (w) lst = my_allocator.Request(siz - cmax);
  else lst = my_allocator.Try(siz - cmax);

  if (lst != NULL) {
    blocks.Append(*lst);
    cmax = CountList(blocks);
    rlen = siz;
    delete lst;
    return(true);
  } 

  return(false);
}

