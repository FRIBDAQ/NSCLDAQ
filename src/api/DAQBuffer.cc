void DAQBuffer::Init(int l,unsigned int aDataSize) {
  my_datasize = aDataSize;
  my_allocator.SetDataSize(my_datasize);

  LinkedList *lst = NULL;

  routetag = 0;
  originip = 0;
  routemask = ALLBITS_MASK;
  cmax = 0;
  rlen = 0;
  scnt = 0;
  lastidx = -1; 
  lastblk = NULL;

  if (l > 0) {
    lst = my_allocator.Request(l); 
    if (lst != NULL) {
      blocks.Append(*lst);
      cmax = CountList(blocks);
      if (cmax < l) rlen = cmax;
      else rlen = l;
      delete lst;
    }
  }
}

