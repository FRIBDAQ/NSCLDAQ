/** @fn Write(int aFd,size_t aStart,size_t aLen)
* @brief Write a buffer to a file descriptor.
*
* Write the contents of this buffer to a file descriptor.
* The write begins at the position indicated (in primitive 
* type units) within the buffer and continues for the 
* specified length.
*
* @param aFd The file descriptor on which to write
* @param aStart Buffer position frow which to begin writing. 
* @param aLen Number of primitive units to write
* @return The number of primitive units written
* @throw DAQOutOfBounds If aLen or aStart are out-of-bounds 
* @throw OSException On file descriptor write errors
*/                                                             
size_t DAQBuffer::Write(int aFd,size_t aStart,size_t aLen) {
  DAQPageBlock *bw = NULL;  
  int bsiz = 0;
  int bytesize = 0;
  unsigned char *p = NULL;
  int sc = 0,lc = 0, c = 0, rc = 0, soff = 0;

  if (rlen < aLen) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Buffer::Write() requested write exceeds buffer length"),DAQEXCPID(DAQOutOfBounds)));
  }

  bytesize = aLen * my_datasize;

  bw = (DAQPageBlock*)(blocks.GetFirst());
  if (bw == NULL) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Buffer::Write() Attempting to write a buffer of zero length"),DAQEXCPID(DAQOutOfBounds)));
  }

  // Find the starting block
  sc = 0; 
  bsiz = bw->GetLength();  
  while ((bw != NULL)&&((sc+bsiz) < aStart)) {
    sc += bsiz;
    bw = (DAQPageBlock*)(blocks.GetNext());
    if (bw != NULL) bsiz = bw->GetLength(); 
  }

  // Starting byte intrablock offset
  soff = (aStart - sc) * my_datasize;
  soff = (soff < 0) ? 0 : soff;

  if (bw == NULL) {
    LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Buffer::Write() ran out of page blocks???"),DAQEXCPID(DAQOutOfBounds)));
  } 

  lc = 0;
  bsiz = bw->GetLength() * my_datasize;

  p = (unsigned char *)bw->Get();
  p += soff;    // Adjust for the offset
  bsiz -= soff; // Adjust for the offset

  if (bsiz > bytesize) bsiz = bytesize; // Don't write more than
                                        // user specified.
  c = 0; 

  while (lc < bytesize) {
    if ((bsiz-c) <= 0) {
      bw = (DAQPageBlock*)(blocks.GetNext());
      if (bw != NULL) {
        bsiz = bw->GetLength() * my_datasize;
        if ((bsiz + lc) > bytesize) {
          bsiz = (bytesize - lc);
        } 
        p = (unsigned char *)bw->Get();
        c = 0;
      } else {
        LOG_AND_THROW(daq_exception_factory.CreateException(DAQCSTR("Buffer::Write() ran out of page blocks???"),DAQEXCPID(DAQOutOfBounds)));
      }
    }

    rc = write(aFd,p+c,bsiz-c);
    if (rc > 0) {  /* Write ok */
      c += rc;
      lc += rc;
    } else if ((rc == 0)&&(errno != EINTR)) { /* EOF or EINTR ??? */
      return(lc/my_datasize);
    } else if (rc < 0) {
      switch(errno) {
        case EINTR:   // Posix interrupt
          break;
        case EAGAIN:  // Nonblocking and no data
        case EPIPE:   // EOF ???
          return(lc/my_datasize);
          break;
        default:      // Error
          LOG_AND_THROW(os_exception_factory.CreateBaseSystemException(DAQCSTR("Buffer::Write() error encountered while writing buffer")));
          if (errno) return(-errno);
          else return(rc);
          break;
      }
    } 
  }

  return(lc/my_datasize);
}

