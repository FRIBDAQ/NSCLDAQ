/** @fn Dump(PrintStreamIface& aStream,int aCnt)
* @brief Dump the buffer as hex and character data.
*
* Dump part of this buffer to a PrintStream.  The contents are
* printed as both hex characters and (if printable) ascii
* characters.  Printing begins at the start of the buffer.
*
* @param aStream the PrintStream on which to print 
* @param aCnt The number of characters to print
* @return None
*/                                                             
void DAQBuffer::Dump(PrintStreamIface& aStream,int aCnt) {
  DAQPageBlock *bw = NULL;  
  std::ios::fmtflags flags;
  unsigned char ch;
  unsigned short v;
  int i,j,cnt = 0,bsiz,bcnt,len;
  unsigned char *p = NULL,tmp[BUFFER_DUMP_CHARCOUNT+1];
   
  flags = aStream.flags();
  len = aCnt < (rlen*my_datasize) ? aCnt : (rlen*my_datasize);

  bw = (DAQPageBlock*)(blocks.GetFirst());
  if (!blocks.Bottom()) {
    bsiz = bw->GetLength() * my_datasize;
    bcnt = 0;
    p = (unsigned char *)bw->Get();
  }

  aStream.hex();
  aStream.width(8);
  aStream.fill('0');
  aStream.print(0);
  aStream.print(": ");

  for (i = 0; (i < len)&&(!blocks.Bottom()); i++) {
    if (cnt > BUFFER_DUMP_CHARCOUNT) {
      aStream.println();
      aStream.print("          ");

      for (j = 0; j < cnt; j++) {
        ch = tmp[j];

        if (isprint(ch)&&(!iscntrl(ch))) aStream.printf(" %c",ch);
        else aStream.printf("  ");
      }
   
      aStream.println();
      aStream.hex();
      aStream.width(8);
      aStream.fill('0');
      aStream.print(i);
      aStream.print(": ");
      cnt = 0;
    }
  
    v = (*p);  
    aStream.hex();
    aStream.width(2);
    aStream.fill('0');
    aStream.print(v);
    tmp[cnt] = (*p);
    cnt++; p++; bcnt++;

    if (bcnt >= bsiz) {
      bw = (DAQPageBlock *)(blocks.GetNext());
      if (!blocks.Bottom()) {
        bsiz = bw->GetLength() * my_datasize;
        bcnt = 0;
        p = (unsigned char *)bw->Get();
      }
    }
  }

  if (cnt > 0) {
    aStream.println();
    aStream.print("          ");
  }

  for (j = 0; j < cnt; j++) {
    ch = tmp[j];

    if (isprint(ch)&&(!iscntrl(ch))) aStream.printf(" %c",ch);
    else aStream.printf("  ");
  }
   
  aStream.println();
  aStream.flags(flags);
  aStream.flush();
}

