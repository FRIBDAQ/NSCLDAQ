/**
* @class LinkedList
* @brief A template class definition for a linked list.
*
* Template class definition for general pointer list object.                 
* This object acts as a list for pointers to other objects.  
* The LITEM template parameter represents the type of items
* that can be added to this list.  The MUTEX template parameter
* represents an implementation of the MutexIface class that
* can be used to control concurrent access to the list.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LITEM,class MUTEX>
class LinkedList : public Printable {
  public:
    /*==============================================================*/
    /** @fn LinkedList()
    * @brief Default constructor.
    *
    * Default constructor.  By default, the linked list will be
    * dynamically resized.
    *
    * @param None
    * @return this
    */                                                             
    LinkedList() 
      {qtail = NULL; qhead = NULL; cur = NULL; scnt = 0; maxscnt = 0; usecur = false;}; 

    /*==============================================================*/
    /** @fn LinkedList(int aSize)
    * @brief Constructor with a maximum size.
    *
    * Constructor with maximum number of items.  A size <= 0
    * indicates that the queue should be dynamically resized.
    *
    * @param aSize The maximum size of this linked list.
    * @return this
    */                                                             
    LinkedList(int aSize) 
      {qtail = NULL; qhead = NULL; cur = NULL; scnt = 0; maxscnt = aSize; usecur = false;}; 

    /*==============================================================*/
    /** @fn Empty() 
    * @brief Set the list to an empty state.
    *
    * Remove all items from this linked list.
    *
    * @param None
    * @return None
    */                                                             
    void Empty() {
       LinkedListItem<LITEM> *i,*i2;
    
       objmutex.Lock();
  
       i = qhead; 
       while (i != NULL) {
         i2 = i->GetNext(); 
         delete i;
         i = i2;
       }

       usecur = false;
       cur = qhead = qtail = NULL;
       scnt = 0;

       objmutex.UnLock();
    };
  
    /*==============================================================*/
    /** @fn ~LinkedList()
    * @brief Destructor.
    *
    * Destroy this linked list.
    *
    * @param None
    * @return None
    */                                                             
    ~LinkedList() {
       Empty();
     };

    /*==============================================================*/
    /** @fn Prepend(LITEM *nitm,bool lockme = true)
    * @brief Prepend an item to the list.
    *
    * Prepend an item to this linked list.  This method allows
    * the same item to be added multiple times.
    *
    * @param nitem The item to prepend.
    * @param lockme Set to true if list should be locked during prepend.
    * @return Whether the operation was successful.
    *   @retval true If the prepend was successful.
    *   @retval false Otherwise
    */                                                             
    bool Prepend(LITEM *nitm,bool lockme = true) {
      LinkedListItem<LITEM> *itm;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();
  
      if ((maxscnt > 0)&&(scnt >= maxscnt)) {
        if (lockme) objmutex.UnLock();
        return(false); // Full
      }

      itm  = new LinkedListItem<LITEM>(nitm);

      if (itm != NULL) {   
        if (qhead == NULL) {
          qhead = itm;
          qtail = itm; 
          itm->SetNext(NULL);
          itm->SetPrev(NULL);
        } else {
          itm->SetNext(qhead);
          itm->SetPrev(NULL);
          qhead->SetPrev(itm); 
          qhead = itm;
        }
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }
      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn Append(LITEM *nitm,bool lockme = true)
    * @brief Append an item to the list.
    *
    * Append an item to this linked list.  This method allows
    * the same item to be added multiple times.
    *
    * @param nitem The item to prepend.
    * @param lockme Set to true if list should be locked during append.
    * @return Whether the operation was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise
    */                                                             
    bool Append(LITEM *nitm,bool lockme = true) {
      LinkedListItem<LITEM> *itm;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if ((maxscnt > 0)&&(scnt >= maxscnt)) {
        if (lockme) objmutex.UnLock();
        return(false); // Full
      }

      itm  = new LinkedListItem<LITEM>(nitm);

      if (itm != NULL) {   
        if (qhead == NULL) {
          qhead = itm;
          qtail = itm; 
          itm->SetNext(NULL);
          itm->SetPrev(NULL);
        } else {
          qtail->SetNext(itm);
          itm->SetPrev(qtail);
          qtail = itm;
        }
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }
      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn AppendUniq(LITEM *nitm,bool lockme = true)
    * @brief Append an item to the list uniquely.
    *
    * Append an item to this linked list after deleting the first
    * (if it exists) insertion of this item from the list.  That is,
    * if an earlier insertion of this item was completed, delete the
    * first case and then append this item to the list.  Uniqueness
    * is really only guaranteed if all insertions are done using
    * the unique insertion methods, since the other append and prepend
    * operations allow multiple copies of an item to be added to the
    * list.
    *
    * @param nitem The item to append.
    * @param lockme Set to true if list should be locked during append.
    * @return Whether the operation was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise
    */                                                             
    bool AppendUniq(LITEM *nitm,bool lockme = true) {
      bool rc = false;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      Delete(nitm,false);
      rc = Append(nitm,false);

      if (lockme) objmutex.UnLock();

      return(rc);
    };

    /*==============================================================*/
    /** @fn PrependUniq(LITEM *nitm,bool lockme = true)
    * @brief Prepend an item to the list uniquely.
    *
    * Prepend an item to this linked list after deleting the first
    * (if it exists) insertion of this item from the list.  That is,
    * if an earlier insertion of this item was completed, delete the
    * first case and then prepend this item to the list.  Uniqueness
    * is really only guaranteed if all insertions are done using
    * the unique insertion methods, since the other append and prepend
    * operations allow multiple copies of an item to be added to the
    * list.
    *
    * @param nitem The item to prepend.
    * @param lockme Set to true if list should be locked during prepend.
    * @return Whether the operation was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise
    */                                                             
    bool PrependUniq(LITEM *nitm,bool lockme = true) {
      bool rc = false;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if (Find(nitm,false) != NULL) Delete(nitm,false);
      rc = Prepend(nitm,false);

      if (lockme) objmutex.UnLock();

      return(rc);
    };

    /*==============================================================*/
    /** @fn InsertAfter(LITEM *nitm,bool lockme = true)
    * @brief Insert an item after the current one.
    *
    * Insert an item after the current one as established by
    * the GetFirst, GetLast and GetNext methods.  If the current
    * item has not be established insertion will fail.
    * This method allows the same item to be added multiple times.
    *
    * @param nitem The item to insert.
    * @param lockme Set to true if list should be locked during insertion.
    * @return Whether the operation was successful.
    *   @retval true If the insertion was successful.
    *   @retval false Otherwise
    */                                                             
    bool InsertAfter(LITEM *nitm,bool lockme = true) {
      LinkedListItem<LITEM> *itm;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if (cur == NULL) {
         objmutex.UnLock();
         return(false);
      }

      if ((maxscnt > 0)&&(scnt >= maxscnt)) {
        if (lockme) objmutex.UnLock();
        return(false); // Full
      }

      itm  = new LinkedListItem<LITEM>(nitm);

      if (itm != NULL) {   
        itm->SetNext(cur->GetNext());
        itm->SetPrev(cur);
        cur->SetNext(itm);

        if (cur == qtail) qtail = itm;
 
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }
      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn InsertBefore(LITEM *nitm,bool lockme = true)
    * @brief Insert an item before the current one.
    *
    * Insert an item before the current one as established by
    * the GetFirst, GetLast and GetNext methods.  If the current
    * item has not be established insertion will fail.
    * This method allows the same item to be added multiple times.
    *
    * @param nitem The item to insert.
    * @param lockme Set to true if list should be locked during insertion.
    * @return Whether the operation was successful.
    *   @retval true If the insertion was successful.
    *   @retval false Otherwise
    */                                                             
    bool InsertBefore(LITEM *nitm,bool lockme = true) {
      LinkedListItem<LITEM> *itm;

      if (nitm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if (cur == NULL) {
         objmutex.UnLock();
         return(false);
      }

      if ((maxscnt > 0)&&(scnt >= maxscnt)) {
        if (lockme) objmutex.UnLock();
        return(false); // Full
      }

      itm  = new LinkedListItem<LITEM>(nitm);

      if (itm != NULL) {   
        itm->SetPrev(cur->GetPrev());
        itm->SetNext(cur);
        cur->SetPrev(itm);
 
        if (cur == qhead) qhead = itm;
 
        scnt++;
        if (lockme) objmutex.UnLock();
        return(true);
      }
      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn Append(LinkedList<LITEM,MUTEX>& l)
    * @brief Append a list to this list.
    *
    * Append a list to this linked list.  This method allows
    * the same items to be added multiple times.
    *
    * @param l The list to be appended.
    * @param lockme Set to true if list should be locked during append.
    * @return Whether the operation was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise
    */                                                             
    bool Append(LinkedList<LITEM,MUTEX>& l) {
      LinkedListItem<LITEM> *itm;

      objmutex.Lock();

      if (maxscnt > 0) {
        if (scnt >= maxscnt) {
          objmutex.UnLock();
          return(false); // Full
        }

        if ((maxscnt - scnt) < l.Count()) {
          objmutex.UnLock();
          return(false); // Will be full
        }
      }

      itm = l.qhead;
      while (itm != NULL) {   
        Append(itm->Get(),false);
        itm = itm->GetNext();
      }

      objmutex.UnLock();
      return(true);
    };

    /*==============================================================*/
    /** @fn AppendUniq(LinkedList<LITEM,MUTEX>& l)
    * @brief Append a list to the list uniquely.
    *
    * Append a list to this linked list using the 
    * AppendUniq(LITEM *nitm,bool lockme = true) method for item
    * insertion.
    *
    * @param l The list to append.
    * @param lockme Set to true if list should be locked during append.
    * @return Whether the operation was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise
    */                                                             
    bool AppendUniq(LinkedList<LITEM,MUTEX>& l) {
      LinkedListItem<LITEM> *itm;

      objmutex.Lock();

      if (maxscnt > 0) {
        if (scnt >= maxscnt) {
          objmutex.UnLock();
          return(false); // Full
        }

        if ((maxscnt - scnt) < l.Count()) {
          objmutex.UnLock();
          return(false); // Will be full
        }
      }

      itm = l.qhead;
      while (itm != NULL) {   
        AppendUniq(itm->Get(),false);
        itm = itm->GetNext();
      }

      objmutex.UnLock();
      return(true);
    };

    /*==============================================================*/
    /** @fn Prepend(LinkedList<LITEM,MUTEX>& l)
    * @brief Prepend a list to this list.
    *
    * Prepend a list to this linked list.  This method allows
    * the same items to be added multiple times.
    *
    * @param l The list to be prepended.
    * @param lockme Set to true if list should be locked during prepend.
    * @return Whether the operation was successful.
    *   @retval true If the append was successful.
    *   @retval false Otherwise
    */                                                             
    bool Prepend (LinkedList<LITEM,MUTEX>& l) {
      LinkedListItem<LITEM> *itm;

      objmutex.Lock();

      if (maxscnt > 0) {
        if (scnt >= maxscnt) {
          objmutex.UnLock();
          return(false); // Full
        }

        if ((maxscnt - scnt) < l.Count()) {
          objmutex.UnLock();
          return(false); // Will be full
        }
      }

      itm = l.qtail;
      while (itm != NULL) {   
        Prepend(itm->Get(),false);
        itm = itm->GetPrev();
      }

      objmutex.UnLock();
      return(true);
    };

    /*==============================================================*/
    /** @fn Delete(LITEM *itm,bool lockme = true)
    * @brief Delete an item from the list.
    *
    * Delete an occurance of an item from the linked list.
    * If an item was inserted multiple times, no assumption is
    * made about which occurance will be deleted.  Moreover, only
    * one occurance of an item is deleted.
    *
    * @param itm The item to delete.
    * @param lockme Set to true if list should be locked during deletion.
    * @return Whether the operation was successful.
    *   @retval true If an item was deleted.
    *   @retval false Otherwise
    */                                                             
    bool Delete(LITEM *itm,bool lockme = true) {
      LinkedListItem<LITEM> *bit,*wrk,*pbit;

      if (itm == NULL) return(false);

      if (lockme) objmutex.Lock();

      if (qhead != NULL) {
        if (cur != NULL) if (cur->Get() == itm) {
           cur = cur->GetNext();
           usecur = true;
        }

        if (qhead->Get() == itm) {
          wrk = qhead; 
          qhead = qhead->GetNext();
          if (qhead != NULL) qhead->SetPrev(NULL);
          scnt--;
          delete wrk;
          if (qhead == NULL) qtail = NULL;
          if (lockme) objmutex.UnLock();
          return(true);
        } else if (qtail->Get() == itm) {
          wrk = qtail; 
          qtail = qtail->GetPrev();
          if (qtail != NULL) qtail->SetNext(NULL); 
          scnt--;
          delete wrk;
          if (lockme) objmutex.UnLock();
          return(true);
        } else {
          bit = qhead;
          while (bit != NULL) {
            if (bit->Get() == itm) {
              wrk = bit->GetNext();
              pbit = bit->GetPrev(); 
              if (pbit != NULL) pbit->SetNext(wrk);
              if (wrk != NULL) wrk->SetPrev(pbit);
              scnt--;
              delete bit;
              if (lockme) objmutex.UnLock();
              return(true);
            } 
            bit = bit->GetNext();
          }
        }
      } 
      if (lockme) objmutex.UnLock();
      return(false);
    };

    /*==============================================================*/
    /** @fn Find(LITEM *itm,bool lockme = true)
    * @brief Find an item from in the list.
    *
    * Find an occurance of an item in the linked list.
    * If an item was inserted multiple times, no assumption is
    * made about which occurance will be found. 
    *
    * @param itm The item to find.
    * @param lockme Set to true if list should be locked during the hunt.
    * @return The item found or null if no occurance was located.
    */                                                             
    LITEM *Find(LITEM *itm,bool lockme = true) {
      LinkedListItem<LITEM> *bit = NULL,*wrk,*pbit;

      if (itm == NULL) return(NULL);

      if (lockme) objmutex.Lock();

      bit = qhead;
      while (bit != NULL) {
        if (bit->Get() == itm) {
          if (lockme) objmutex.UnLock();
          return(itm);
        }
        bit = bit->GetNext();
      }

      if (lockme) objmutex.UnLock();
      return(NULL);
    };

    /*==============================================================*/
    /** @fn IsEmpty()
    * @brief Check if the linked list is empty.
    *
    * Check if the linked list contains no items.
    *
    * @param None
    * @return If the linked list is empty.
    *   @retval true If the list is empty.
    *   @retval false Otherwise
    */                                                             
    bool IsEmpty() {if (scnt < 1) return(true); else return(false);};
  
    /*==============================================================*/
    /** @fn Count()
    * @brief Return the number of items in the list.
    *
    * Return the number of items stored in the linked list.
    * Items inserted multiple times are counted multiple times. 
    *
    * @param None
    * @return The number of items in the list.
    */                                                             
    int Count() {return(scnt);};
  
    /*==============================================================*/
    /** @fn IsFull()
    * @brief Check if the linked list is full.
    *
    * Check if no further items can be added to the linked list.
    *
    * @param None
    * @return If the linked list is full.
    *   @retval true If the list is empty.
    *   @retval false Otherwise
    */                                                             
    bool IsFull() {return((scnt >= maxscnt)&&(maxscnt > 0));}
  
    /*==============================================================*/
    /** @fn GetFirst()
    * @brief Get the first item in the list.
    *
    * Return the first item in the list.
    * 
    * @param None
    * @return The first item or null.
    */                                                             
    LITEM *GetFirst() {
      LITEM *itm;

      objmutex.Lock();

      if (qhead == NULL) {
        cur = NULL;
        objmutex.UnLock();
        return(NULL);
      }

      cur = qhead;
      itm = cur->Get();
      objmutex.UnLock();
      return(itm);
    };

    /*==============================================================*/
    /** @fn GetLast()
    * @brief Get the last item in the list.
    *
    * Return the last item in the list.
    * 
    * @param None
    * @return The last item or null.
    */                                                             
    LITEM *GetLast() {
      LITEM *itm;

      objmutex.Lock();

      if (qtail == NULL) {
        cur = NULL;
        objmutex.UnLock();
        return(NULL);
      }

      cur = qtail;
      itm = cur->Get();
      objmutex.UnLock();
      return(itm);
    };

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next item in the list.
    *
    * Return the next item in the list.  GetFirst() needs to be
    * called to initialize this iteration prior to calling
    * GetNext().
    * 
    * @param None
    * @return The next item or null.
    */                                                             
    LITEM *GetNext(bool lockme=true) {
      LITEM *itm = NULL;

      if (lockme) objmutex.Lock();

      if (qhead == NULL) {
        cur = NULL;
        if (lockme) objmutex.UnLock();
        return(NULL);
      }

      if (usecur) {
        usecur = false;
        if (cur != NULL) itm = cur->Get();
        if (lockme) objmutex.UnLock();
        return(itm);
      } else {
        if (cur != NULL) cur = cur->GetNext();

        if (cur != NULL) itm = cur->Get();
        if (lockme) objmutex.UnLock();
        return(itm);
      }
    };

    /*==============================================================*/
    /** @fn GetPrev()
    * @brief Get the previous item in the list.
    *
    * Return the previous item in the list.  GetLast() needs to be
    * called to initialize this iteration prior to calling
    * GetPrev().
    * 
    * @param None
    * @return The previous item or null.
    */                                                             
    LITEM *GetPrev(bool lockme=true) {
      LITEM *itm = NULL;

      if (lockme) objmutex.Lock();

      if (qhead == NULL) {
        cur = NULL;
        if (lockme) objmutex.UnLock();
        return(NULL);
      }

      if (cur != NULL) cur = cur->GetPrev(); 

      if (cur != NULL) itm = cur->Get();
      if (lockme) objmutex.UnLock(); 
      return(itm);
    };

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the next item in the list (prefix).
    *
    * Return the next item in the list.  GetFirst() needs to be
    * called to initialize this iteration prior to calling
    * this operator.
    * 
    * @param None
    * @return The next item or null.
    */                                                             
    LITEM *operator++() {
      return(GetNext());
    };

    /*==============================================================*/
    /** @fn operator++(int)
    * @brief Get the current item in the list and increment (postfix).
    *
    * Return the current item in the list and iterate.  GetFirst() 
    * needs to be called to initialize this iteration prior to calling
    * this operator.
    * 
    * @param None
    * @return The current item or null.
    */                                                             
    LITEM *operator++(int) {
      LITEM *wrk = NULL;
      objmutex.Lock();
      if (cur != NULL) wrk = cur->Get();
      GetNext(false);
      objmutex.UnLock();
      return(wrk);
    };

    /*==============================================================*/
    /** @fn operator--()
    * @brief Get the previous item in the list (prefix).
    *
    * Return the previous item in the list.  GetFirst() needs to be
    * called to initialize this iteration prior to calling
    * this operator.
    * 
    * @param None
    * @return The previous item or null.
    */                                                             
    LITEM *operator--() {
      return(GetPrev());
    };

    /*==============================================================*/
    /** @fn operator--(int)
    * @brief Get the current item in the list and decrement (postfix).
    *
    * Return the current item in the list and decrement.  GetFirst() 
    * needs to be called to initialize this iteration prior to calling
    * this operator.
    * 
    * @param None
    * @return The current item or null.
    */                                                             
    LITEM *operator--(int) {
      LITEM *wrk = NULL;
      objmutex.Lock();
      if (cur != NULL) wrk = cur->Get();
      GetPrev(false);
      objmutex.UnLock();
      return(wrk);
    };

    /*==============================================================*/
    /** @fn operator[](const int& idx)
    * @brief Subscripting operator.
    *
    * Return the nth item in the list or null if out of bounds.
    *
    * @param idx The index of the item to return.
    * @return The nth item.
    */                                                             
    inline LITEM *operator[](const int& idx) {
      LITEM *wrk = NULL;
      LinkedListItem<LITEM> *p;

      objmutex.Lock();

      if ((idx < 0)||(idx >= scnt)) {
        objmutex.UnLock();
        return(NULL);
      }

      p = qhead;
      for (i = 0; ((i < idx)&&(p != NULL)); i++) { 
        p = p->GetNext();
      }

      if (p != NULL) wrk = p->Get();
      else wrk = NULL;
 
      objmutex.UnLock();

      return(wrk);
    };

    /*==============================================================*/
    /** @fn Bottom()
    * @brief Check if there are no more items to process.
    *
    * Check if there are no more items to process next.  Used
    * in conjunction with GetNext() and friends.  Really just
    * checks if the current item is null.
    *
    * @param None
    * @return If there are any more items to process next.
    *   @retval true If there are more items.
    *   @retval false otherwise.
    */                                                             
    bool Bottom() {
      return(cur == NULL);
    };
    
    /*==============================================================*/
    /** @fn Top()
    * @brief Check if there are no more items to process.
    *
    * Check if there are no more items to process prior to the current
    * item.  Used in conjunction with GetPrev() and friends.  Really just
    * checks if the current item is null.
    *
    * @param None
    * @return If there are any more items to process prior.
    *   @retval true If there are more items.
    *   @retval false otherwise.
    */                                                             
    bool Top() {
      return(cur == NULL);
    };
    
    /*==============================================================*/
    /** @fn Print(ostream& aStream)
    * @brief Print this linked list on an output stream.
    *
    * Print this linked list on an output stream.
    *
    * @param aStream An output stream.
    * @return The output stream.
    */                                                             
    virtual ostream& Print(ostream& aStream) {
      int i;
      LinkedListItem<LITEM> *p;
 
      objmutex.Lock();

      aStream << "Head=" << qhead << ",Tail=" << qtail << ",Count=" << scnt << ",Max=" << maxscnt << endl;

      p = qhead;
      for (i = 0; i < scnt; i++) { 
        aStream << "  Item " << i << ": ";
        if (p != NULL) {
          aStream << (*p) << endl;
        } else {
          aStream << "(nil pointer)" << endl;
        }
        p = p->GetNext();
      }

      objmutex.UnLock();

      return(aStream);
    }

    /*==============================================================*/
  protected:
    /**
    * @var objmutex
    * @brief A mutex for threading 
    *
    * A mutex for controlling concurrent access.
    */
    MUTEX objmutex;       // Mutex for threading 

    /*==============================================================*/
  private:
    friend class LinkedListIterator<LITEM,MUTEX>;

    /**
    * @var qhead
    * @brief Pointer to the head of the list queue.
    *
    * Pointer to the head of the list queue.  
    */
    LinkedListItem<LITEM> *qhead;  // Head of list queue

    /**
    * @var qtail
    * @brief Pointer to the tail of the list queue.
    *
    * Pointer to the tail of the list queue.  
    */
    LinkedListItem<LITEM> *qtail;  // Tail of list queue

    /**
    * @var cur
    * @brief Current list item.
    *
    * Current list item (used with GetFirst(), GetNext() and friends).
    */
    LinkedListItem<LITEM> *cur; // Current list item (for GetFirst and GetNext)

    /**
    * @var usecur
    * @brief Current item has not been processed.
    *
    * Current item has not been processed so process it next.
    */
    bool usecur;          // Current item has not been returned

    /**
    * @var scnt
    * @brief Current number of list items.
    *
    * Number of items currently stored in the list.
    */
    int scnt;             // Number of list items.

    /**
    * @var maxscnt
    * @brief Maximum number of list items.
    *
    * Maximum number of items that can be stored in this list.
    */
    int maxscnt;          // Maximum number of list items.
};

/**
* @class LinkedListIterator 
* @brief Template definition for a linked list iterator.
*
* Template definition for a linked list iterator (see the LinkedList
* class).  The LITEM template parameter represents the type of
* items that can be added to this list.  The MUTEX template parameter
* represents an implementation of the MutexIface that can be used
* to control concurrent access to this linked list.
*
* WARNING:  This iterator class assumes that you're not
* changing the LinkedList while iterating.  That is, this iterator
* class is meant to allow multiple threads to read the list 
* simultaneously.  If someone changes the LinkedList while the threads
* are using it, unexpected results are likely to occur.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LITEM,class MUTEX>
class LinkedListIterator : public Printable {
  public:
    /*==============================================================*/
    /** @fn LinkedListIterator()
    * @brief Constructor.
    *
    * Default constructor.
    *
    * @param None
    * @return this
    */                                                             
    LinkedListIterator() {
      cur = NULL;
      llst = NULL;
    }

    /*==============================================================*/
    /** @fn ~LinkedListIterator()
    * @brief Destructor.
    *
    * Destroy this linked list interator.
    *
    * @param None
    * @return None
    */                                                             
    ~LinkedListIterator() {
      cur = NULL;
    }

    /*==============================================================*/
    /** @fn GetFirst(LinkedList<LITEM,MUTEX> *aLlst)
    * @brief Get the first item in the list.
    *
    * Get the first item in the specified list.
    *
    * @param aLlst The list to iterate over.
    * @return The first item or null.
    */                                                             
    LITEM *GetFirst(LinkedList<LITEM,MUTEX> *aLlst) {
      llst = aLlst;
      cur = NULL;
 
      if (llst != NULL) cur = llst->qhead;
      if (cur != NULL) return(cur->Get());
      else return(NULL);
    }

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next item in a list.
    *
    * Get the next item in a list specified by an earlier call to
    * GetFirst.
    *
    * @param None
    * @return The next item or null.
    */                                                             
    LITEM *GetNext() {
      if (cur != NULL) cur = cur->GetNext();

      if (cur != NULL) return(cur->Get());
      else return(NULL);
    }

    /*==============================================================*/
    /** @fn operator++()
    * @brief Get the next item in a list.
    *
    * Get the next item in a list specified by an earlier call to
    * GetFirst.
    *
    * @param None
    * @return The next item or null.
    */                                                             
    LITEM *operator++() {
      return(GetNext());
    }

    /*==============================================================*/
    /** @fn operator=(const LinkedListIterator<LITEM,MUTEX>& l)
    * @brief Assignment.
    *
    * Assign a different linked list iterator to this one.
    *
    * @param l The linke list iterator to assign to this one.
    * @return A reference to this.
    */                                                             
    LinkedListIterator<LITEM,MUTEX>& operator=(const LinkedListIterator<LITEM,MUTEX>& l) {
      cur = l.cur;
      llst = l.llst;
      return(*this);
    };

  private:
    /**
    * @var cur
    * @brief Current list item.
    *
    * Current list item (used with GetFirst(), GetNext() and friends).
    */
    LinkedListItem<LITEM> *cur;

    /**
    * @var llst
    * @brief A pointer to the linked list.
    *
    * A pointer to the linked list currently under iteration.
    */
    LinkedList<LITEM,MUTEX> *llst;
};

