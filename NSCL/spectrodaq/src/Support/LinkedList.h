/**
* @class LinkedListItem
* @brief Template defintion of items that can be added to a LinkedList.
*
* Template defintion of items that can be added to a LinkedList.
* The LTYPE template parameter represents the type of items that can
* be stored in this type of LinkedListItem.
*
* @author  Eric Kasten
* @version 0.4.0
*/
template <class LTYPE>
class LinkedListItem : public Printable {
  public:
    /*==============================================================*/
    /** @fn LinkedListItem(LTYPE *b)
    * @brief Constructor with an item to store.
    *
    * Constructor with an item to store.
    *
    * @param b The item to store in this LinkedListItem.
    * @return this
    */                                                             
    LinkedListItem(LTYPE *b) { // Constructor
      myitem = b;        // Objects need to have operator=
      nxt = NULL; prv = NULL;
    }; 

    /*==============================================================*/
    /** @fn ~LinkedListItem()
    * @brief Destructor.
    *
    * Destor this linked list item.
    *
    * @param None
    * @return None
    */                                                             
    ~LinkedListItem() {};  // Destructor

    /*==============================================================*/
    /** @fn Get()
    * @brief Get the item stored in this item.
    *
    * Get a pointer to the item stored in this item.
    *
    * @param None
    * @return A pointer to the item stored in this item or null.
    */                                                             
    LTYPE *Get() {return(myitem);}; // Get item encapsulated by this item 

    /*==============================================================*/
    /** @fn GetNext()
    * @brief Get the next item following this one.
    *
    * Get the next item linked next in line to this one.
    *
    * @param None
    * @return The next item or null if this is the end of the list.
    */                                                             
    LinkedListItem<LTYPE> *GetNext() {return(nxt);};  // Get next list item

    /*==============================================================*/
    /** @fn GetPrev()
    * @brief Get the previous item prior to this one.
    *
    * Get the previous item linked prior to this one.
    *
    * @param None
    * @return The prior item or null if this is the end of the list.
    */                                                             
    LinkedListItem<LTYPE> *GetPrev() {return(prv);};  // Get prev list item

    /*==============================================================*/
    /** @fn SetNext(LinkedListItem<LTYPE> *b)
    * @brief Set the next item following this one.
    *
    * Set the next item following this one.
    *
    * @param b A pointer to a linked list item.
    * @return None
    */                                                             
    void SetNext(LinkedListItem<LTYPE> *b) {nxt = b;};  // Set the next list item

    /*==============================================================*/
    /** @fn SetPrev(LinkedListItem<LTYPE> *b)
    * @brief Set the previous item prior to this one.
    *
    * Set the previous item prior to this one.
    *
    * @param b A pointer to a linked list item.
    * @return None
    */                                                             
    void SetPrev(LinkedListItem<LTYPE> *b) {prv = b;};   // Set the prev list item

    /*==============================================================*/
    /** @fn operator=(const LinkedListItem<LTYPE>& l)
    * @brief Assignment operator.  
    *
    * Assign a different linked list item to this one.
    *
    * @param l The linked list item to assign to this one.
    * @return A reference to this.
    */                                                             
    LinkedListItem<LTYPE>& operator=(const LinkedListItem<LTYPE>& l) {
      myitem = l.myitem;
      nxt = l.nxt;
      prv = l.prv;
      return(*this);
    };

    /*==============================================================*/
    /** @fn Print(ostream& aStream)
    * @brief Print this item.
    *
    * Print this item on an output stream.
    *
    * @param aStream The output stream.
    * @return The output stream.
    */                                                             
    ostream& Print(ostream& aStream) {
      if (myitem != NULL) 
         aStream << "Item=[" << (*myitem) << "],Ptr=" << this; 
      else 
         aStream << "Item=(nil),Ptr=" << this;

      aStream << ",Next=" << nxt << ",Prev=" << prv;
      return(aStream);
    };

  protected:
    /*==============================================================*/
    /** @fn LinkedListItem()
    * @brief Default constructor.
    *
    * Default constructor.  Protected to limit usage.
    *
    * @param None
    * @return this
    */                                                             
    LinkedListItem() {myitem = NULL; nxt = NULL; prv = NULL;};  // Default constructor
  private:
    /**
    * @var myitem
    * @brief Item stored by this.
    *
    * Object encapsulated by this list item.
    */
    LTYPE  *myitem;  // Object encapsulated by this list item

    /**
    * @var nxt
    * @brief Next item following this one.
    *
    * Pointer to the next LinkedListItem following this one.
    */
    LinkedListItem<LTYPE> *nxt;  // Next list item

    /**
    * @var prv
    * @brief Previous item prior to this one.
    *
    * Pointer to the previous LinkedListItem prior to this one.
    */
    LinkedListItem<LTYPE> *prv;  // Prev list item
};

