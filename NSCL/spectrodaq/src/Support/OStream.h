class Basic_OStream_Iface : public ios {
  public:
    ~Basic_OStream_Iface() { };

    typedef Basic_OStream_Iface& (*__daqostream_iface_omanip)(Basic_OStream_Iface&);
    typedef ios& (*__daqostream_iface_manip)(ios&);
    typedef ios& (*__daqostream_ios_manip)(void);

    // Need to be overridden by the implementor 
    virtual Basic_OStream_Iface& flush() = 0;
    virtual int pcount() = 0;
    virtual Basic_OStream_Iface& write(const char*,int) = 0;
    virtual Basic_OStream_Iface& operator<<(char aChar) = 0;
    virtual Basic_OStream_Iface& operator<<(const char *pChar) = 0;
    virtual Basic_OStream_Iface& operator<<(const void *pVoid) = 0;
    virtual Basic_OStream_Iface& operator<<(long aLong) = 0;
    virtual Basic_OStream_Iface& operator<<(long long aLong) = 0;
    virtual Basic_OStream_Iface& operator<<(unsigned long aULong) = 0;
    virtual Basic_OStream_Iface& operator<<(unsigned long long aULong) = 0;
    virtual Basic_OStream_Iface& operator<<(bool aBool) = 0;
    virtual Basic_OStream_Iface& operator<<(double aDouble) = 0;

    // Overridden in Basic_OStream_Impl
    virtual Basic_OStream_Iface& operator<<(unsigned char aUChar) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(signed char aChar) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(streambuf *aBuf) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(const unsigned char *pUChar) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(const signed char *pChar) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(int aInt) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(unsigned int aUInt) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(short aShort) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(float aFloat) { return(*this); };
    virtual Basic_OStream_Iface& operator<<(unsigned short aUShort) { return(*this); };
    virtual void Close() { };

    virtual Basic_OStream_Iface& operator<<(__daqostream_iface_omanip aFunc) { 
       return (*aFunc)(*this); 
    }

    virtual Basic_OStream_Iface& operator<<(__daqostream_iface_manip aFunc) {
       (*aFunc)(*this); return(*this);
    }

    virtual Basic_OStream_Iface& operator<<(__daqostream_ios_manip aFunc) {
       (*aFunc); return(*this);
    }

  protected:
    Basic_OStream_Iface() { };
};

static inline Basic_OStream_Iface& flush(Basic_OStream_Iface& oStream) {
  return oStream.flush();
}

static inline Basic_OStream_Iface& endl(Basic_OStream_Iface& oStream) {
  oStream << "\n";
  return oStream.flush();
}

static inline Basic_OStream_Iface& ends(Basic_OStream_Iface& oStream) {
  oStream << "\0";
  return oStream.flush();
}

/*=========================================================================*/
template <class TYP> 
class daq_ostream_smanip {
  public:
    daq_ostream_smanip(Basic_OStream_Iface& (*aFunc)(Basic_OStream_Iface&, TYP), TYP aArg) : _func(aFunc), _arg(aArg) {}

    Basic_OStream_Iface& operator()(Basic_OStream_Iface& oStream) const {
       return (*_func)(oStream,_arg); 
    }

  private:
    Basic_OStream_Iface& (*_func)(Basic_OStream_Iface&, TYP);
    TYP _arg;
};

typedef Basic_OStream_Iface& (*daq_ostream_smanip_int)(Basic_OStream_Iface &, int);
typedef Basic_OStream_Iface& (*daq_ostream_smanip_fmt)(Basic_OStream_Iface &, ios::fmtflags);

/*=========================================================================*/
static Basic_OStream_Iface& __daqostream_setw(Basic_OStream_Iface& aStream,int aWidth)
  {aStream.width(aWidth); return(aStream);}

static Basic_OStream_Iface& __daqostream_setprecision(Basic_OStream_Iface& aStream,int aPrec)
  {aStream.precision(aPrec); return(aStream);}

static Basic_OStream_Iface& __daqostream_setfill(Basic_OStream_Iface& aStream,int aChar)
  {aStream.fill(aChar); return(aStream);}

static Basic_OStream_Iface& __daqostream_setbase(Basic_OStream_Iface& aStream,int aBase)
  {
     switch(aBase) {
       case 8: aStream << oct; break;
       case 16: aStream << hex; break;
       case 10: 
       default: aStream << dec; break;
     }
     return(aStream);
  }

static Basic_OStream_Iface& __daqostream_setiosflags(Basic_OStream_Iface& aStream,ios::fmtflags aFlags)
  {aStream.setf(aFlags); return(aStream);}

static Basic_OStream_Iface& __daqostream_resetiosflags(Basic_OStream_Iface& aStream,ios::fmtflags aMask)
  {aStream.unsetf(aMask); return(aStream);}

