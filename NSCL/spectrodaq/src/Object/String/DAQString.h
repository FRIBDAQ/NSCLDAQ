// No argument manipulators
template <class IFACE,class MANIPS>
Basic_OStream_Impl<IFACE,MANIPS>& format(Basic_OStream_Impl<IFACE,MANIPS>& oStream) {
  oStream.format(); return(oStream);
}

template <class IFACE,class MANIPS>
Basic_OStream_Impl<IFACE,MANIPS>& noformat(Basic_OStream_Impl<IFACE,MANIPS>& oStream) {
  oStream.noformat(); return(oStream);
}

class daq_string_smanip_funcs {
  public:
    template <class IFACE,class MANIPS>
    static inline Basic_OStream_Impl<IFACE,MANIPS>& noformat(Basic_OStream_Impl<IFACE,MANIPS>& oStream) {
      return oStream.noformat();
    }

    template <class IFACE,class MANIPS>
    static inline Basic_OStream_Impl<IFACE,MANIPS>& format(Basic_OStream_Impl<IFACE,MANIPS>& oStream) {
      return oStream.format();
    }
};

typedef Basic_OStream_Impl<DAQString_Stream_Iface,daq_string_smanip_funcs> DAQString_Stream;

typedef OStreamManip<daq_string_smanip_funcs> DAQStringManip;

