    virtual void Append(const char *) = 0; // Append
    virtual void Append(const char *,int) = 0; // Append
    virtual void Append(const void *) = 0; // Append
    virtual void Append(const long) = 0;   // Append
    virtual void Append(const long long) = 0;   // Append
    virtual void Append(const unsigned long) = 0; // Append
    virtual void Append(const unsigned long long) = 0; // Append
