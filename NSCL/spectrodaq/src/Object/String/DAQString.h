    void Append(const long);             // Append
    void Append(const long long);        // Append
    void Append(const int);              // Append
    void Append(const short);            // Append
    void Append(const unsigned long);    // Append
    void Append(const unsigned long long);  // Append
    void Append(const unsigned int);     // Append
    void Append(const unsigned short);   // Append
    void Append(const double);           // Append
