    DAQString& operator+=(const unsigned long long);   // Append
    DAQString& operator+=(const unsigned int);   // Append
    DAQString& operator+=(const unsigned short);   // Append
    DAQString& operator+=(const double); // Append
    DAQString& operator+=(const float); // Append
    DAQString& operator+=(const void *); // Append
    DAQString& operator+=(const bool);   // Append

    DAQString& operator=(const char *);  // Assignment
    DAQString& operator=(const char);    // Assignment
    DAQString& operator=(const long);    // Assignment
    DAQString& operator=(const long long); // Assignment
    DAQString& operator=(const int);     // Assignment
    DAQString& operator=(const short);   // Assignment
    DAQString& operator=(const unsigned long);  // Assignment
    DAQString& operator=(const unsigned long long);  // Assignment
    DAQString& operator=(const unsigned int);   // Assignment
    DAQString& operator=(const unsigned short); // Assignment
    DAQString& operator=(const double);  // Assignment
    DAQString& operator=(const float);   // Assignment
    DAQString& operator=(const void *);  // Assignment
    DAQString& operator=(const bool);    // Assignment

