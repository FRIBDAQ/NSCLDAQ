    void Resize(int siz);   // Resize this string
    void Set(const DAQString& s); // Set this string to a new value
    void Set(const char *s,int l); // Set this string to a new value
    DAQString& operator=(const DAQString& s); // Set this string to a new value
    void Append(const DAQString& s); // Append a string to this one
    DAQString& operator+=(const DAQString& s); // Append a string to this one

