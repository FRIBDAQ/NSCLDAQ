/** @fn DAQNodeId::Peers(DAQNodeId& aId)
* @brief Check if two node ids are peers.
*
* Check if this node Id is on the same machine as the provided
* Id.
*                                                                   
* @param aId Another node id.
* @return Whether aId is a peer of this.
*   @retval true The node ids are peers.    
*   @retval false The node ids are not peers.    
*/
bool DAQNodeId::Peers(DAQNodeId& aId)
{
  if (memcmp(aId.location.val.ipv4_raw,location.val.ipv4_raw,NODEID_ADDR_SIZE) == 0) {
    return(true);
  }

  return(false);
}

/*===================================================================*/
/** @fn DAQNodeId::operator==(const DAQNodeId& aId)
* @brief Check if a node id is equal to this.                                  
*
* Check if the specified node id is equal to this.  That is,
* return true if the specified node id represents the name host
* and process as the one represented by this.
*
* @param aId Another node id.
* @return If aId is equivalent to this.
*   @retval true aId is equivalent to this.
*   @retval false aId is not equivalent to this.
*/                                                                  
bool DAQNodeId::operator==(const DAQNodeId& aId)
{
  if (memcmp(aId.location.val.ipv4_raw,location.val.ipv4_raw,NODEID_ADDR_SIZE) == 0) {
    if (aId.location.val.pid == location.val.pid) return(true);  
    else return(false);
  }

  return(false);
}

