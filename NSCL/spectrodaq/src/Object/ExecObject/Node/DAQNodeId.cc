/** @fn DAQNodeId::EncodeAsString()
* @brief Encode Id as a string
*
* Return a DAQString representation of this.  Typically, this is a
* string of the form <IPaddr>:<PID>.  
*
* @param None
* @return A DAQString representation of this.
*/                                                                  
DAQString DAQNodeId::EncodeAsString()
{
  long v;
  DAQString aStr;

  aStr = inet_ntoa(location.val.ipv4_addr);
  aStr += ":";
  v = location.val.pid;
  aStr += v;
  
  return(aStr);
}

/*===================================================================*/
/** @fn DAQNodeId::DecodeFromNetwork(unsigned int aNetid)
* @brief Decode node id from a network format id.
*
* Initialize this from a network format representation of an IP
* address.
*
* @param aNetid A network format IP address.
* @return Success or failure of the string decoding. 
*   @retval true If the decoding was successful.
*   @retval false If the decoding wasn't successful.
*/                                                                  
bool DAQNodeId::DecodeFromNetwork(unsigned int aNetid)
{
  memset(location.raw,0,sizeof(DAQNodeLocation_t));
  location.val.pid = 0;
  location.val.ipv4_addr.s_addr = aNetid;
  return(true);
}

/*===================================================================*/
/** @fn DAQNodeId::DecodeFromString(DAQString& aStr)
* @brief Decode node id from a DAQString
*
* Initialize this from a string representation of a node id.
* The string representation must be of the form <IPaddr>:<PID>.
*
* @param aStr A string representation of a node id.
* @return Success or failure of the string decoding. 
*   @retval true If the decoding was successful.
*   @retval false If the decoding wasn't successful.
*/                                                                  
bool DAQNodeId::DecodeFromString(DAQString& aStr)
{
  long v;
  int len = 0;
  char *p = NULL;
  DAQString wStr,wStr2;
  struct hostent *hp = NULL;

  memset(location.raw,0,sizeof(DAQNodeLocation_t));
  location.val.pid = 0;

  wStr = aStr.Head(":");
  p = wStr.Get();
  if (p == NULL) return(false);

  if (!is_ip_address(p)) { // Must be a host name -> resolve
    if ((hp = reentrant_gethostbyname(p)) == NULL) {
      LOG_AND_THROW(os_exception_factory.CreateResolvException(DAQCSTR("DAQNodeId::DecodeFromString() reentrant_gethostbyname failed")));  
      return(false);      
    }               
    if ((hp->h_addr != NULL)&&(hp->h_length > 0)) {
      int l = NODEID_ADDR_SIZE;
      if (l > hp->h_length) l = hp->h_length;
      memcpy(location.val.ipv4_raw,hp->h_addr,l);
    } else {
      return(false);
    }
  } else {
    if (!inet_aton(p,&location.val.ipv4_addr)) return(false);
  }

  wStr2 = aStr.Tail(":");
  wStr = wStr2.Head(".");
  if (wStr.GetLen() > 0) p = wStr.Get(); // tid on the end of the id
  else p = wStr2.Get();
  if (p == NULL) return(true);    // No pid at all
  location.val.pid = atoi(p);
  
  return(true);
}

/*===================================================================*/
/** @fn DAQNodeId::DecodeFromString(char *pStr)
* @brief Decode node id from a string (char*)
*
* Initialize this from a string representation of a node id.
* The string representation must be of the form <IPaddr>:<PID>.
*
* @param pStr A string (char*) representation of a node id.
* @return Success or failure of the string decoding. 
*   @retval true If the decoding was successful.
*   @retval false If the decoding wasn't successful.
*/
bool DAQNodeId::DecodeFromString(char *pStr)
{
  DAQString wStr;
  wStr.Set(pStr);
  return(DecodeFromString(wStr)); 
}

