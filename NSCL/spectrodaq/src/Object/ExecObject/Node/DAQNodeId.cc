/** @fn DAQNodeId::Reset()                                             
* @brief Reset the node id. 
*
* Reset this node id such that it represents a node on this
* host.
*
* @param None
* @return An indication of failure or success.
*   @retval 0 Success
*   @retval !=0 An error code typically indicating a host error (h_errno) .
*/                                                                  
int DAQNodeId::Reset()
{
  struct hostent *hp;

  memset(location.raw,0,sizeof(DAQNodeLocation_t));

  daqnode_global_info.infolock.Lock();

  if (!daqnode_info_inited) {
    daqnode_info_inited = true;
    memset(daqnode_global_info.location.raw,0,sizeof(DAQNodeLocation_t));

    if (reentrant_gethostname(daqnode_global_info.hname,MAX_HOSTNAME) < 0) {
      LOG_AND_THROW(os_exception_factory.CreateResolvException(DAQCSTR("DAQNodeId::Reset() reentrant_gethostname failed")));  
      return(h_errno);
    }; 

    if ((hp = reentrant_gethostbyname(daqnode_global_info.hname)) == NULL) {
      LOG_AND_THROW(os_exception_factory.CreateResolvException(DAQCSTR("DAQNodeId::Reset() reentrant_gethostbyname failed")));  
      return(h_errno);      
    }               

    if ((hp->h_addr != NULL)&&(hp->h_length > 0)) {
      daqnode_global_info.iplen = NODEID_ADDR_SIZE;
      if (daqnode_global_info.iplen > hp->h_length) daqnode_global_info.iplen = hp->h_length;
      memcpy(daqnode_global_info.location.val.ipv4_raw,hp->h_addr,daqnode_global_info.iplen);
    } else {
      LOG_AND_THROW(os_exception_factory.CreateResolvException(DAQCSTR("DAQNodeId::Reset() reentrant_gethostbyname returned null h_addr")));  
      return(h_errno);      
    }
  }

  memcpy(location.val.ipv4_raw,daqnode_global_info.location.val.ipv4_raw,daqnode_global_info.iplen);

  daqnode_global_info.infolock.UnLock();

  location.val.pid = getpid();
  location.val.tid = (daqthread_t)0;  // Hmmmmmm(?)

  return(0);
}

