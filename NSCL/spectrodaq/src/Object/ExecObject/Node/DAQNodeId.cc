/** @fn DAQNodeId::Copy(const DAQNodeLocation& aLoc)
* @brief Copy a location into this.
*
* Copy the data structure (DAQNodeLocation) of another node id
* in this this.  After the copy this and the one supplied are
* considered equal under operator==.
*
* @param aLoc A DAQNodeLocation
* @return If the the copy was successful.
*   @retval true The copy was successful.
*   @retval false The copy wasn't succesful.
*/                                                                  
bool DAQNodeId::Copy(const DAQNodeLocation& aLoc)
{
  if (memcpy(location.val.ipv4_raw,aLoc.val.ipv4_raw,NODEID_ADDR_SIZE) != NULL) {
    location.val.pid = aLoc.val.pid;
    location.val.tid = aLoc.val.tid;
    return(true);
  }
  
  return(false);  
}

/*===================================================================*/
/** @fn DAQNodeId::Copy(const DAQNodeLocation_t& aLoc)
* @brief Copy a location struct into this.
*
* Copy the data structure (struct DAQNodeLocation_t) of another node id
* in this this.  After the copy this and the one supplied are
* considered equal under operator==.
*
* @param aLoc A struct DAQNodeLocation_t
* @return If the the copy was successful.
*   @retval true The copy was successful.
*   @retval false The copy wasn't succesful.
*/                                                                  
bool DAQNodeId::Copy(const DAQNodeLocation_t& aLoc)
{
  if (memcpy(location.val.ipv4_raw,aLoc.ipv4_raw,NODEID_ADDR_SIZE) != NULL) {
    location.val.pid = aLoc.pid;
    location.val.tid = aLoc.tid;
    return(true);
  }

  return(false);  
}

